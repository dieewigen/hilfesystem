GFX packs

GFX packs allow you to change the theme of your DE Browser.

== Resetting your GFX pack to default == (In the web-based browser):
If you happen to accidentally mess up your theme by typing an invalid path, you can set it back to default by removing your GFX pack path.
Alternatively, you can disable your theme temporarily by logging out and logging back in while the option "Deactivate GFX pack" is checked.