Der Premium Account (PA) kann im Spiel unter dem Punkt Optionen erwroben werden. 

Der erwerb gilt für alle in diesem Moment aktiven Spielaccounts.

Sollte ein Account gelöscht werden geht der Premiumaccount verloren.
Sollte ein Account erst später angelegt werden, so wird der Premiumaccount nicht automatisch von den anderen Accounts übernommen.
Dies dient als Belohnung dafür einen Account lange zu halten, ohne zu löschen um zu hoppen.

Je nach Server gibt es grundlegend folgende Vorteile: 
- einen erh&ouml;hten Kollektorenoutput
- Freund/Feind-Status in der Sektoransicht
- Speicherung von Scans (Sondenscan, Allianztag, Schiffs&uuml;bersicht, Turm&uuml;bersicht)
- mehr Speicher beim Hyperfunk (Archiv, Freundesliste, Ignoreliste)
- Information &uuml;ber abgeschlossene Missionen
- Verschiedene kostenlose Komfortfunktionen in der Erweiterten Arch&auml;ologie und EFTA
- Hinweis in der Erweiterten Arch&auml;ologie bei inaktivem Forschungsmodul im Raumschiff