Waffen werden bei Schiffseinheiten und Verteidigungsanlagen verwendet. Sie unterscheiden sich in 2 Bereichen:

<b>Konventionelle Waffen</b>
Konventionelle Waffen richten direkten Schaden an und führen zu Verlusten bei dem Gegner im Kampf.

<b>EMP Waffen<b>
EMP Waffen dienen zum Lähmen der feindlichen Schiffen, so dass diese nicht mehr aktiv am Kampf teilnehmen können.
