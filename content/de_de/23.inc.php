<b>Flottenzusammensetzung</b>

Bei Die Ewigen stehen jeder Rasse acht Schiffsklassen zur Verfügung.
In diesem Artikel wollen wir uns verschiedene Konzepte zur Zusammenstellung der Flotte anschauen.


<b>Grundsätzliche Überlegungen</b>

Zu den acht Schiffsklassen gehören drei besondere, die wir kurz abhandeln und danach aus den Betrachtungen ausklammern wollen.

Transmitterschiffe: Notwendig zum Erbeuten von Kollektoren. Unabhängig von der Restflotte sollte ein Spieler mindestens so viele Transmitterschiffe besitzen, wie er bei einem durchschnittlichen Ziel in drei Wellen an Kollektoren erbeuten kann. Aggressive Spieler bauen mehr, defensive Spieler weniger Transmitterschiffe. Schickt man eine Flotte ohne Transmitterschiffe zu einem Angriff aus, so erfolgt eine diesbezügliche Warnung.

Bomber: Effektiv im Kampf gegen Türme. Es ist sinnvoll, vor einem Angriff auf einen Spieler mit vielen Türmen große Mengen davon zu bauen, ansonsten sind sie nicht hilfreich. Allerdings sind Titanen ihr nächstes Angriffsziel nach den Türmen.

Trägerschiffe: Erhöhen Träger- und Supportkapazität in der Flotte. Sind nützlich, um überdurchschnittliche Mengen an Jägern oder Bombern oder Jagdbooten in einer Flotte möglich zu machen. Träger kämpfen auch selbst: ihre bevorzugten Ziele sind Jäger, Orbitaljäger und Bomber. Befinden sich allerdings keine anderen großen Schiffe in der eigenen Flotte, sind die Träger ein gefundenes Fressen für die feindlichen Schiffe. Besonders gegen Zerstörer sind sie anfällig.


Nun bleiben die fünf Schiffstypen, welche die eigentliche Kampfflotte ausmachen. Hier haben wir zwei Ansätze zur Verfügung: 
Die <i>Mischflotte</i>, die alle Schiffsklassen ausgewogen beinhaltet.
Oder <i>spezialisierte Flotten</i>, die für ganz bestimmte Situationen vorteilhaft und in anderen nachteilig sind.


<b>Mischflotte</b>

Die Mischflotte klingt zunächst einfach und durchschnittlich; viele Spieler entscheiden sich dagegen, weil sie meinen, "mehr" aus ihrer Flotte herausholen zu müssen.
Tatsächlich hat sie aber genau wie spezialisierte Flotten Stärken und Schwächen.


Zusammensetzung:
Ein Vorschlag für den Aufbau einer Mischflotte wäre zum Beispiel folgender:

Wir bauen 10 Schlachtschiffe.
Damit die Schlachtschiffe keinen Geschwindigkeitsnachteil hinnehmen müssen, bauen wir 30 Kreuzer und 50 Zerstörer dazu.
Die Schlachtschiffe und Kreuzer können nur ihre volle Stärke erlangen, wenn sie Begleitschutz von Jägern erhalten. Wir brauchen mindestens 10 * 20 plus 30 * 10 Jäger, das macht 500.
Die Kreuzer und Schlachtschiffe bieten Support für maximal genauso viele Jagdboote, wie sie Jägergeleitschutz brauchen; wir bauen also erst einmal 250 Jagdboote.

Jetzt ist das Grundgerüst fertig, wir nutzen gerade die Mindestwerte des Supports. Um nicht nach den ersten kleinen Verlusten mit unsupporteten Schiffen dazustehen, bauen wir eine kleine Reserve der supportenden Schiffe, beispielsweise:

10 Schlachtschiffe / 40 Kreuzer / 75 Zerstörer / 250 Jagdboote / 750 Jäger

Bei den einzelnen Rassen schwankt der nötige Support leicht, so brauchen Z'tah-ara beispielsweise mehr Jäger als die anderen und Ishtar weniger Support für Jagdboote. Da sind Erfahrungswerte gefragt.
Wir können jetzt nach Geschmack einzelne Schiffsklassen im Verhältnis zu den anderen aufstocken, um gewisse Akzente zu setzen; wir können auch die restlichen freien Trägerkapazitäten für Bomber nutzen, wenn wir das wollen. Wir müssen beachten, dass beim Bauen bestimmter Schiffe auch die supportenden Schiffe nachgebaut werden müssen.


Vorteile:
Die Mischflotte ist unter allen Bedingungen einsetzbar und richtet auf jeden Fall ihren Schaden an.
Sie weist alle Fähigkeiten in gewissen Maßen auf, wie beispielsweise das Blocken feindlicher Großkampfschiffe oder schnelle Schiffe (Zerstörer zum Deffen bei ETA 5).
Nur Mischflotten können effektiv Schlachtschiffe einsetzen, die Kreuzer zerstören und einen Teil der eigenen Verluste recyclen können (Jäger, Bomber und Jagdboote).
Durch das Vorhandensein von Jägern und Jagdbooten muss man fast nie Verluste an schweren Schiffen hinnehmen, selten Verluste bei Zerstörern.

Nachteile:
Die Mischflotte ist auf keinem Gebiet so überragend, wie es eine spezialisierte Flotte auf ihrem jeweiligen Gebiet ist.
Wenn man mit einer Hand voll anderer Spieler zusammen attet oder defft, kann es passieren, dass die anderen Spieler Spezialflotten haben und bestimmte Schiffstypen kaum oder gar nicht dabei haben. Dann tragen wir mit unserer Mischflotte in dieser Schiffsklasse sämtliche Verluste alleine. Dieser Nachteil kann vermieden werden, wenn man vor der gemeinsamen Aktion die Flotten abgleicht und gegebenenfalls die Teile der Flotte zu Hause lässt, die in den Flotten der anderen nicht vorhanden sind.


<b>Spezialisierte Flotten</b>

Wenn ein Spieler eine Flotte kreieren möchte, die für eine bestimmte Situation ganz besonders geeignet ist, will er nur die dafür besonders effektiven Schiffe einsetzen. Zusätzlich muss er fast immer ein Minimum anderer Schiffe bauen, die zum Supporten oder Tragen nötig sind.

Alle spezialisierten Flotten teilen den Nachteil, dass sie nur schlecht mit andersartigen Flotten harmonieren; Verluste werden sehr ungleichmäßig verteilt, wenn unter zwei bis vier beteiligten Flotten ein Spieler eine bestimmte Schiffsklasse als einziger mitführt.
Wir betrachten nun einige beliebte Spezialflotten.


<b>Blockerflotte</b>

Diese Flotte soll möglichst viel der Feindflotte blocken, um aus jedem Gefecht mit möglichst geringen Verlusten hervorzugehen.

Zusammensetzung: 
Wir bauen prinzipiell Jagdboote. Diese benötigen Support; wir bauen also entweder Trägerschiffe zum supporten (1 Träger ~35 Boote), oder Kreuzer (1 Kreuzer ~10 Boote), wobei die Kreuzer wiederum Jägersupport brauchen (1 Kreuzer ~10 Jäger).
Wollen wir Schlachtschiffe zum Jagdbootsupport einsetzen, brauchen diese wiederum Zerstörer dazu und wir landen wieder bei einer Mischflotte.

Vorteile: 
Geringe Verluste

Nachteile:
Auch der Gegner hat wenig Verluste; dadurch werden wir selber zu einem gut angreifbaren Ziel.
Wir richten auch dann wenig Schaden an, wenn wir eigentlich kräftig austeilen möchten, wie zum Beispiel im Krieg.
Wir werden selten mehrere Wellen fliegen können, da wir in der Hauptwelle zu wenige Schiffe zerstören.
Keine Möglichkeit zum späten Deffen. (Ausnahme: Ewigen-Jagdboote fliegen ebenfalls schnell, also ETA 5 in andere Sektoren, allerdings betragen ihre Kampf- und Blockkraft ohne Support nur 50%.)

Rassen:
Als Blocker eignen sich Ishtar und Z'tah-ara gut, K'tharr schlecht.


<b>Zerstörerflotte</b>

Zusammensetzung:
Ausschließlich Zerstörer. Diese Schiffe können ohne Support agieren.

Vorteile:
Schnelle Flotte, die noch spät rechtzeitig zum Deffen kommt.
Effektiv gegen Blockerflotten, da Zerstörer gegen Jagdboote eingesetzt werden.
Als Z'tah-ara ist die gesamte Zerstörerflotte getarnt. Angreifende Zerstörer werden nicht gesehen. Ihre Zahl kann nur durch Spionage in Erfahrung gebracht werden.

Nachteile:
Anfällig gegen Flotten mit vielen Kreuzern, verhältnismäßig schwach gegen Flotten ohne Jagdboote. Führen sie Transmitterschiffe mit sich, büßen sie ihren Geschwindigkeitsvorteil ein.

Rassen:
Z'tah-ara eignen sich besonders gut, Ishtar schlecht.


<b>Kreuzer-/Jägerflotte</b>

Zusammensetzung:
Ausschließlich Kreuzer und Jäger. Mindestens so viele Jäger, wie zum Support der Kreuzer benötigt werden; maximal so viele Jäger, wie von den Kreuzern getragen werden können.

Vorteile: 
Effektiv gegen Zerstörerflotten, da Kreuzer gegen Zerstörer eingesetzt werden.

Nachteile:
Anfällig gegen starke Blockerflotten, da die Kreuzer geblockt werden und die Jäger gute Ziele für Jagdboote abgeben.
Keine Möglichkeit zum späten Deffen.

Rassen:
K'tharr eignen sich besonders gut, Ishtar schlecht.


<b>Titanenflotte</b>

Titanen sind für ihre Größe schnell (e4 sektorintern, e6 für andere Sektoren), stark bewaffnet, stark gepanzert und können blocken. Sie benötigen keinen Support, aber sie supporten oder recyclen auch nicht. Doch sind sie teuer, und man benötigt außerdem für ihren Bau Titanen-Energiekerne, die nur schwer erhältlich sind (mit Agentenmissionen, als Fundstücke in den Vergessenen Systemen oder bei Auktionen). Sie sind zudem bei allen Schiffen außer bei Trägern unter den ersten fünf Zielen in der Angriffsreihenfolge und werden mit mindestens 80% Effizienz angegriffen. Da Titanen eine neue Erfindung sind, liegen noch keine verlässlichen Erfahrungswerte für Kämpfe vor. Es empfiehlt sich wahrscheinlich nicht, eine reine Titanenflotte zu verwenden, jedoch dürften sie als Verstärkung für alle anderen Flottentypen geeignet sein.


<b>Frachter</b>

Frachter werden für die Erschließung der vergessenen Systeme und für die Handelsmissionen benötigt. Sie sind nicht für den Kampf gedacht. Von Baukosten und Panzerung her sind sie etwas besser als Jagdboote ausgerüstet, und sie können auch blocken, doch sind ihre konventionellen Waffen schwächer als die von Jägern, und ihre EMP-Waffen sind in einem Kampf praktisch unbedeutend. Sie müssten schon in sehr großer Zahl auftreten, um Kampfergebnisse beeinflussen zu können. Allerdings liegen sie zusammen mit den Transmitterschiffen in der Angriffsreihenfolge ganz hinten und bleiben bei Kämpfen daher fast immer verschont.