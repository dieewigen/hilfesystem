<b>Umfragen</b>

Damit die Entwickler bei Ihrer Meinungsfindung wissen, wie die Spieler über ein bestimmtes Thema denken, gibt es für diese die Möglichkeit Umfragen zu starten.

Jeder User ist verpflichtet, seine Stimme nach bestem Gewissen abzugeben. Nur so ist eine faire Auswertung zu filtern.

Wurde eine Umfrage durch das DET gestartet, so muss jeder User seine Stimme abgeben. Erst nachdem er dies getan hat, kann er das Spiel normal fortsetzen.

Die Umfragen befinden sich im Menü "Community / Umfragen". Hier können auch die Umfrageergebnisse vergangener Umfragen nachgesehen werden.

Umfragen sind serverintern. In SDE z.B. können daher andere Umfragen aufscheinen als in EDE.

Wichtig: Es handelt sich um Umfragen, nicht um Abstimmungen. Die Entwickler sind nicht an die Umfrage gebunden, sondern diese dienen nur dazu, sich eine Meinung zu bilden und anhand der Daten eine passende Entscheidung zu treffen.