Das Sektorforum dient dem Zwecke der sektorinternen Kommunikation.

Alle Mitglieder eines Sektors haben volle Einsicht in das Sektorforum und können dort Beiträge erstellen oder andere Beiträge beantworten. So lassen sich recht einfach eigene Strategien & Informationen austauschen, die nur für die Mitglieder eines Sektors relevant sind.

Der Sektorkommandant hat zudem die Möglichkeit & Aufgabe, das Forum und die einzelnen Beiträge zu moderieren (löschen, schliessen). 