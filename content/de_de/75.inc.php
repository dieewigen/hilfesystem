<b>Spezialisierung</b>

Im Menü "Spezialisierung" kann man insgesamt 5 verschiedene Vergünstigungen auswählen. Diese sogenannten Spezialisierungen werden durch die Errungenschaften freigeschaltet.
Die Errungenschaften finden sich im Menü "Übersicht", hier werden verschiedene Aufgaben vorgegeben, die man erfüllen kann. Diese Erfüllung erfolgt in mehreren Stufen, und die Anzahl der Stufen schaltet mögliche Spezialisierungen frei.

Die Spezialisierungen sind grob in drei Themenbereiche aufgeteilt:
Grün: Verteidigung & Spionage
Rot: Kampf/Angriff
Lila: Soziales, den Sektor betreffend

In der Tabelle der Spezialisierungen ist in der linken Spalte angegeben, wieviele Errungenschaftsstufen erreicht werden müssen, um die gesamte Zeile dieser Spezialsierungen freizuschalten. Als Tooltip wird angezeigt, wieviele Errungenschaftspunkte man bereits hat. Die notwendige Anzahl der Errungenschaftspunkte ist von Server zu Server unterschiedlich, da sie von der Rundenlaufzeit abhängt.

Ist die Spezialisierungszeile freigeschaltet, dann werden die möglichen Spezialisierungen groß dargestellt, und man kann eine davon auswählen. Was welche Spezialisierung bewirkt, wird ebenfalls detailliert als Tooltip angezeigt.
Je nach Themenbereich kann z.B. die Bauzeit für Einheiten verkürzt, Flottentransportkapazität vergrößert, Missionsdauer verkürzt, die Wahrscheinlichkeit für Spionage geändert, oder auch der Planetare Rohstoffertrag und das Recycling nach Angriffen erhöht werden, und vieles anderes mehr.

Man kann aus jeder Zeile <u>eine</u> Spezialisierung auswählen, aber man kann in jeder Zeile eine Spezialisierung aus einem beliebigen unterschiedlichen Bereich auswählen.

Die Auswahl einer bestimmten Spezialisierung ist nicht endgültig. Man kann die gesamte Spezialisierungsauswahl zurücksetzen. Allerdings muss man 480 Wirtschaftsticks warten, bis man sie danach wieder zurücksetzen kann. Das Zurücksetzen ist kostenlos.


