<b>Auktion</b>

Unter der Schaltfläche "Auktion" kann man diverse Artikel wie Rohstoffe (auch T und Palenium), Artefakte und Titanen-Energiekerne ersteigern.
Die Kosten dafür sind sehr unterschiedlich: meist kann man mit Ressourcen bezahlen, die man in den Vergessenen Systemen abgebaut hat, seltener auch mit den Haupt-Ressourcen (M, D, I oder E) oder mit Credits.

Es handelt um eine Holländische Auktion, bei der ein Startpreis vorgegeben wird, der sich dann über einen festgelegten Zeitraum bis zum Minimumwert immer weiter verringert, bis ein Bieter zuschlägt.
Ist die Zeit abgelaufen, ohne dass jemand ein Angebot gemacht hat, verschwindet die Ware.

Klickt man auf "bieten", so wird der aktuelle Preis abgebucht und man erhält sofort die Ware.
Für jeden in der Auktion gekauften Artikel bekommt man Handelspunkte.
Wieviele Handelspunkte man bekommt, steht unterhalb der Artikelbezeichnung, wie lange das Angebot noch gilt, steht unterhalb des Preises.

Hat man nicht genügend von der Ressource, die als Kaufpreis benötigt wird, so wird der Preis in roter Schrift dargestellt. Versucht man trotzdem, den Artikel zu erwerben, so erhält man die Fehlermeldung "Du kannst Dir diese Auktion nicht leisten".
Hat man im Artefaktgebäude keinen Platz frei, so wird bei allen Artefakten die Aktion "bieten" nicht angezeigt.
Verbilligte Angebote sind neben der Laufzeit mit dem Vermerk "25 % Preisnachlass" in grüner Schrift gekennzeichnet.

Im Zuge des <b>Allianzbonus</b> wird neben den anderen Bonusgeschenken auch eine Auktion gestartet, bei der man 25% Preisnachlass hat.
Mit dem Artefakt <i>Autacon</i> kann man eine Auktion starten, bei der man ebenfalls einen Preisnachlass von 25% bekommt.


