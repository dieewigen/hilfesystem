<b>Schwarzmarkt</b>

Im Schwarzmarkt werden diverse Waren angeboten, die für Credits erhältlich sind.

Die Credits erhält man durch "Aktivität" alle x Minuten (dieser Wert ist serverabhängig), auch in den Vergessenen Systemen kann man Credits als Fundstücke bekommen, und es gibt das Artefakt "Creditrüssel", das bei Aktivierung 3 bis 5 Credits beschafft.

Um das Spiel ausgewogener zu gestalten, wurde der Schwarzmarkt schon mehrmals überarbeitet, und die Angebote können sich auch jederzeit wieder ändern.
Derzeit werden im Schwarzmarkt nur Rohstofflieferungen, Troniclieferungen und Kriegsartefakte angeboten.


