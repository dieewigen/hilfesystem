<b>Schlachtschiffsupport</b>

Der Schlachtschiffsupport ermöglicht, auf Schlachtschiffe zurückzugreifen, ohne Einschränkungen zu erhalten. Schlachtschiffe fliegen im gewöhnlichen Fall ETA 7 in andere Sektoren bzw. ETA 5 sektorintern (ohne Berücksichtigung von Forschungen und Gebäuden zur Beeinflussung der Flugzeit) und verlangsamen damit die Flotten massiv.
Mit dem Schlachtschiffsupport ist es möglich, seine Flotte mit Schlachtschiffen zu unterstützen ohne diesen Malus zu erleiden.

Der Schlachtschiffsupport ist unabhängig von der Rasse. Für jedes Schlachtschiff sind 3 Kreuzer und 5 Zerstörer notwendig, damit das Schlachtschiff ETA 6 bzw. ETA 4 fliegen kann.

Die unterstützenden Einheiten müssen sich dabei in der selben Flotte befinden. Schickt man z.B. zwei Flotten parallel, dann unterstützen sich diese nicht. Es wird daher eimpfohlen immer eine Flotte zu schicken in der sich alle Schiffe befinden.

Damit stehen allgemein die Vorzüge von Schlachtschiffen zur Verfügung, ohne dass wertvolle Flugzeit aufgegeben werden muss. Gleichzeitig bewirkt die Beschränkung zusammen mit dem Jägersupport eine Ausgeglichenheit der Flotten.


