<b>Sektorgebäude</b>

Eine Übersicht über die Sektorgebäude, die gebaut werden können, und ihre Kosten, findet man im Menü "Ressourcen", im Abschnitt "Übersicht Sektorlager", wenn man mit der Maus auf das Fragezeichen zeigt, als Tooltip. Dies soll den Spielern erleichtern, Rohstoffe für das Sektorlager gezielt zu spenden, da der BK keine Möglichkeit hat, Ressourcen in andere zu tauschen. Der BK hat im BK-Menü einen detaillierteren Überblick.
Im Sektorchat wird eine automatische Meldung angezeigt, wenn der BK einen Bauauftrag für ein Sektorgebäude gibt.
Welche Sektorgebäude bereits vorhanden sind, kann man in der Sektoransicht sehen: im Menü "Sektor" als Tooltip, wenn man den Mauszeiger auf die Grafik der Sektorraumbasis bewegt.


<i>Sektorraumbasis</i>
  	
Die Sektorraumbasis ist eine riesige Weltraumstation, die meist mittig im Sektor gebaut, Heimat und Wohn- und Arbeitsstätte für alle Bewohner des Sektors sein kann.
Vornehmlich dient sie als Navigations- und Handelsstützpunkt, kann aber durch Erweiterung auch als Flottenbasis für die Sektorflotte dienen.
In Ihrer freien Sektion in der Mitte kann man zudem einen Sektorsprungfeldbegrenzer errichten.
Die Sektorraumbasis ist aufgrund der unglaublichen Größe sehr teuer in der Konstruktion, besonders Tronic wird für eine derartig große Konstruktion viel benötigt.


<i>Sektorsprungfeldbegrenzer</i>

Der Sektorsprungfeldbegrenzer ist eine Erweiterung zur Sektorraumbasis.
Nach seinem Bau ist er sofort aktiv und bremst alle sektorfremden Flotten im Angriffsflug um 1 Kampftick runter. Das gilt für Flotten anderer Sektoren  <u>und</u> für die Flotten von angreifenden Spielern. 


<i> Sektorraumwerft </i>

Es steht nur eine Sektorschiffsklasse zur Verfügung, die nach Fertigstellung der Sektorraumwerft kontrollierbar ist: das Sektorraumschiff.
Der BK kann die Sektorflotte teilen, in Wachflotte und Sektorflotte.
Neu gebaute Sektorraumschiffe scheinen nach Fertigstellung vorerst in der Wachflotte auf.
Die Wachflotte bleibt stets im Sektorensystem und sorgt dort für den Schutz des Sektors analog zur Heimatflotte eines Spielers. Angriffe können nur mit der Sektorflotte geflogen werden! Hierfür verlegt man die gewünschte Anzahl Schiffe von der Wachflotte auf die Sektorflotte, genau wie man auch Schiffe von der Heimatflotte in eine der drei Einsatzflotten im Menü "Flotten" verlegt.


<i>Sektorhandelszentrum</i>

Das Sektorhandelszentrum dient als Handelsforum und lokales Logistikzentrum für die Handelsgilde. Die gewonnene Flexibilität kann genutzt werden um den Sektorsteuersatz zu ändern. Die Änderungen nimmt der Sektorkommandant (SK) vor.
Bei Abschluss jeder Rohstoffhandelsmission wird ein bestimmter Steuersatz dem Sektorlager gutgeschrieben. Der SK kann den Steuersatz zwischen 2% und 5% einstellen.


<i>Scannerphalanx</i>

Die Scannerphalanx lässt sich einsetzen, um andere Sektorbasen nach deren Stationierungsstärke, Sektorlagerbestände und dort registrierte Sektorflottenbewegungen zu scannen. Anders als bei der agentenbasierten Spionage kann diese Form nicht entdeckt werden, hat jedoch ihren Preis:
Ein Scan Level 1 kostet 5 T. Damit kann man Stationierungsstärke, Anzahl der in Bau befindlichen Sektorschiffe und die Lagerbestände eines Sektors auszuspionieren.
Ein Scan Level 2 kostet 10 T. Er dient dazu, um alle im Zentralcomputer der Zielbasis registrierten Sektorflottenbewegungen auszuforschen: Ziel, Herkunftssektor, Aktion (Angriff oder Verteidigung), Reisezeit und Größe der ankommenden Sektorflotten.


<b>Sektorlager</b>

Das Sektorlager ist kein Sektorgebäude in dem Sinne, dass es erst gebaut werden muss. Auch wenn noch kein Sektorgebäude vorhanden ist, können bereits Spenden in das Sektorlager getätigt werden. Im Sektorlager werden jene Ressourcen gelagert, die der BK verwendet, um die Sektorgebäude  und Sektorraumschiffe zu bauen. Außerdem werden Tronic für die Scans anderer Sektoren benötigt.
Im Ressourcenmenü kann jedes Sektormitglied den Sektorlagerbestand einsehen. 
Die Sektorlagerspenden können im Menü "Sektorpolitik", im Abschnitt "Sektorlagereinzahlungen" eingesehen werden. Auch bezahlte Steuern, die bei Handelsmissionen fällig werden, werden als Sektorlagereinzahlungen geführt.
Diese Einzahlungen erfüllen die Errungenschaft "Spende Rohstoffe".