<b>Allianz</b>

Allianzen sind Zusammenschlüsse von mehreren Spielern (wobei auch 1-Mann Allianzen möglich sind) unter einem Namen mit bestimmten Zielen, Vorsätzen und Versprechen.

<b>Vor- und Nachteile einer Allianz:</b>

In Spielen wie DE stellt sich dies meist mit Angriffs- und Verteidigungsbündnissen und allgemeinem Tipps- & Strategieaustausch dar. Die Allianzen zeichnen sich durch einen gemeinsamen Allianznamen und ein eigenes Allianzzeichen (auch "TAG" genannt) aus.

Als neuer Spieler (sog. Newbie oder auch N00b) sollte man erwägen, einer dieser Allianzen beizutreten. Sie bringt eigentlich selten irgendwelche besonderen Verpflichtungen mit sich, außer der Ally (Kurzname für Allianz) zu dienen, sich an etwaige Regeln oder die Satzungen zu halten und sich im allgemeinen der Allianz und ihren Mitgliedern zu verpflichten. Im Gegenzug erhält man den Schutz anderer Ally-Mitglieder und die Unterstützung bei Angriffen untereinander.

Außerdem herrscht ja an sich immer ein Kriegszustand in der Galaxie, da jeder auf seinen eigenen Vorteil aus ist. Und wenn jemand ein Opfer sucht, so wird er sich zuerst jemanden ohne "Verbündete" aussuchen, da hier die Rachegefahr wohl wesentlich geringer ausfällt...

Umgekehrt kann es jedoch vorkommen, dass auch unter den Allianzen Krieg herrscht, und man dann als Allianzmitglied dem Gegner als Ziel dient.

Die maximal mögliche Anzahl der Mitglieder, die eine Allianz haben kann, hängt von der Anzahl der Spieler ab und ist von Server zu Server unterschiedlich.

<b>Allianzbonus</b>
Weiters bietet die Mitgliedschaft in einer Allianz täglich einen Bonus. Die Größe dieses Allianzbonus hängt von der Anzahl der von allen Allianzmitgliedern in Summe geworbenen Spieler ab. Diese müssen außerhalb von Sektor 1 sein, müssen jedoch nicht unbedingt in Deiner Allianz sein. Der Allianzbonus wird täglich um Mitternacht gutgeschrieben und kann so lange abgeholt werden, bis es den nächsten Allianzbonus gibt. Wurde er bis dahin nicht beansprucht, dann verfällt er.
Der Allianzbonus beträgt schon alleine bei 0 geworbenen Spielern 1 Kriegsartefakt, <nobr>1 Tronic,</nobr> 1.000 XP für Verteidigungsanlagen, 1 zufälliges Stufe-1-Artefakt (aus folgender Liste: Pesara, Vakara, Geangrus, Geabwus, Agsora, Feuroka, Bloroka, Turak, Turla, Recarion, Pekasch, Pekek, Empala, Empdestro, Recadesto; es wird allerdings das Artefaktgebäude mit einem freien Platz benötigt, ansonsten wird das Artefakt nicht gutgeschrieben), 1  gestartete Auktion im Auktionshaus mit einem Preisnachlass, sowie 1 Titanenenergiekern.
Ab einem geworbenen Spieler erhöhen sich einige dieser Mengen und zusätzlich bekommt die Allianz 1 Quantenglimmer gutgeschrieben. Für mehrere geworbene Spieler erhöht sich der Allianzbonus noch weiter (Stand: Februar 2020, Änderungen sind möglich).
Die genauen Allianzboni sind ingame im Menü "Allianz" zu finden, wenn man im Abschnitt "Allianzübersicht" auf das Symbol für den Allianzbonus klickt. Hat man den Allianzbonus noch nicht abgeholt, ist es grün, ansonsten grau. Dieses Symbol findet sich auch als Link in der oberen Infoleiste rechts neben dem Symbol, das als Link zu den Nachrichten führt.

<b>Allianzaufgaben</b>
Einer Allianz werden Aufgaben vorgegeben, deren Erfüllung Allianz-Siegartefakte bringt. Diese sind für die Wertung in der Rangliste der Allianzen maßgeblich. Die Aufgaben werden umso schwieriger, je mehr Mitglieder eine Allianz hat, jedoch ist der Mindestschwierigkeitsgrad der Aufgaben für eine Allianzgröße von fünf Mitgliedern bemessen.
Welches die aktuelle Allianzaufgabe ist, findet man im Menü "Allianz" ganz oben: In grüner Schrift wird die Allianzaufgabe angezeigt, sowie deren Fortschritt und die noch verbleibende Zeit. Die zur Verfügung stehende Zeit wird in WT angegeben. Weiters wird angezeigt, wie hoch die Belohnung aktuell ist; sie sinkt mit dem Ablauf der Zeit. Die nächste Allianzaufgabe wird gestellt, wenn die aktuelle Aufgabe erfüllt wurde (mit dem nächsten WT) oder wenn die zur Verfügung stehende Zeit abgelaufen ist. Die Allianzaufgaben lauten:
Erteilt den Bauauftrag für Kollektoren
Erobert Kollektoren von anderen Spielern
Erhöht Eure Punktezahl
Erobert Kollektoren von den DX61a23 aus den Protektorats-Sektoren
Startet Missionen unter dem Menüpunkt Missionen
Erhaltet Kriegsartefakte durch Kämpfe
Führt Artefaktverschmelzungen und Artefaktupgrades durch
(Stand: Februar 2020, Änderungen sind möglich).
Danach beginnen sie wieder von neuem.
Die Allianzwertung findet man im Menü "Rangliste / Allianz / Siegartefakte". Der Kampf um die Allianzwertung endet, wenn der Erhabenenkampf beginnt. Die Sieger in xDE und SDE werden in der <a href="https://www.die-ewigen.com/c_toplist.php" target="_blank">Ewigen Rangliste</a> festgehalten. 


Ist man noch in keiner Allianz, so hat man die Möglichkeit einer Allianz beizutreten oder eine neue Allianz zu gründen.

<b>Gründen einer Allianz</b>
Eine Allianz kann man unter dem Menüpunkt "Allianz" gründen.
Füllt hier von oben nach unten Allykürzel, Art der Allianz (Demokratie, Diktatur usw.), politische Ausrichtung, den Allynamen und eine kurze Info über eure Allianz aus.
Die URL zu eurer Homepage und eine Info kann der Allianzleader nachträglich ändern.
Wenn ihr alle Angaben zu eurer Zufriedenheit gemacht habt so drückt auf "Abschicken".
Der Allianzleader wird vorerst automatisch derjenige, der eine neue Allianz gründet. Dieser kann seinen Posten dann später nachträglich an andere Member seiner Allianz abgeben. Der Leader hat unter "Allgemein" die Möglichkeit, seine Allianzdaten zu verwalten.

<b>Einer bestehenden Allianz beitreten</b>
Einer Allianz kann man unter dem Menüpunkt "Allianz" beitreten.
Ihr gelangt zu einem Suchfenster, welches es euch ermöglicht, nach dem Namen oder dem Teil eines Namens zu suchen. Gebt dazu den Namensteil oder das Kürzel in das Suchfeld ein und klickt auf "Suchen".
Es wird darunter eine Liste ausgegeben, welche Allianzen zu eurer Suche passen. Durch klicken auf "Details / Bewerben" kommt ihr zu den Allianzinformationen, von wo aus ihr euch auch bewerben könnt.
Um sich bei der Allianz zu bewerben, klickt unten rechts auf "Bei dieser Allianz bewerben...". Gebt einen kurzen Text ein, aus welchen Gründen ihr dieser Allianz beitreten möchtet, und sendet mit dem Button "Bewerbung absenden" eure Bewerbung ab.
Der Allianzleader erhält dann euren Antrag mit der Info und entscheidet, ob er euch annimmt oder ablehnt. Keine Allianz ist verpflichtet, Anträge anzunehmen!
Ebenfalls wird euch hier eine Liste mit bereits laufenden Bewerbungen angezeigt.
Der Eintritt in eine Allianz kostet Tronic. Der Preis steigt mit der Anzahl der Kollektoren, die man hat. Es ist also empfehlenswert, sich möglichst zu Rundenbeginn einer Allianz anzuschließen, solange es noch 0 T kostet.

<b>Austritt aus einer Allianz</b>
Man kann jederzeit seine Allianz ohne Angabe von Gründen verlassen. Ebenso kann der gesamte Führungsstab (jeder, der einen Allianzposten innehat) jeden beliebigen Spieler aus der Allianz entlassen. Der Austritt kostet wiederum Tronic, deren Menge von der Anzahl der Kollektoren abhängt, die der betreffende Spieler hat. Tritt der Spieler aus eigener Entscheidung aus der Allianz aus, so muss der Spieler bezahlen, wird der Spieler von der Allianz gekickt, so trägt die Allianz die Kosten. Bei einvernehmlichem Austritt aus der Allianz ist es daher ratsam, sich mit der Allianzleitung diesbezüglich abzusprechen.


<i>Erklärung des Menüs "Allianz":</i>

Nicht jedem Mitglied werden alle Schaltflächen dargeboten. Manche sind nur für den Führungsstab sichtbar.

<b>Allgemein</b>
Hier findet man zuerst eine Übersicht mit den Allianzinformationen wie Name, Tag (das Kürzel einer Allianz), Mitgliederlimit, die Anzahl aller Mitglieder, eine URL-Angabe unter der man die Homepage findet, falls es eine gibt, usw.

Darunter findet sich eine Liste mit bereits vergebenen Posten.

Darunter findet sich die Allianzbiographie zur Einsicht.

Ist man Leader der Allianz, so kann man unter der Biographie die Daten ändern. Dazu füllt man der Reihe nach alle notwendigen Informationen aus und klickt dann unten auf ''Abschicken''.

<b>Co-Leader</b>
Der Allianzleader hat hier die Möglichkeit, weitere Posten zu benennen und zu vergeben. An die Posten sind gegebenenfalls Sonderfunktionen gekoppelt.

<b>Mitglieder</b>
Eine Liste aller Mitglieder und deren Koordinaten in DE (standardmäßig nach Koordinaten aufsteigend sortiert). Die Mitgliederliste lässt sich auch nach Name, Anzahl der Kollektoren, Punkten oder nach Rasse sortieren.

<b>Anträge</b>
Hier kann man erlesen, welche Member der eigenen Allianz beitreten möchten bzw. welche anderen Allianzen einen Antrag auf ein Meta-Bündnis stellen.

<b>Bündnis</b>
Hier werden Bündnisanträge gestellt und die existierenden Bündnisse angezeigt.

<b>Krieg</b>
Hier kann man einer Allianz den Krieg erklären. Außerdem werden Kriegsgegner hier aufgelistet.

<b>HF-Leader</b>
Hier kann man an den Leader einer anderen Allianz eine Nachricht zukommen lassen.

<b>HF-Member</b>
Hier hat der Leader die Möglichkeit, an alle Member eine Hyperfunknachricht zu senden sowie die Weiterleitungsfunktion für Nachrichten der Allianzmitglieder zu ändern. Wird die Hyperfunkweiterleitung deaktiviert, so werden Nachrichten der Mitglieder nicht mehr automatisch versendet sondern müssen von einer bevollmächtigten Person freigegeben werden. Standardmäßig ist sie zu Rundenbeginn <u>nicht</u> aktiviert, es muss also jemand aus dem Führungsstab der Allianz die automatische Weiterleitung erst aktivieren.

<b>Finanzen</b>
In diesem Fenster kann der Leader Tronic-Zahlungen seiner Member festlegen und verwalten. Es ist möglich, aus diesem Menü heraus Überweisungen durchzuführen in dem man bei "Tronic ins Allianzdepot überweisen" einen zu überweisenden Betrag ein und klicken Sie anschließend auf "Überweisen".
Der Allianzleiter und Bevollmächtigte können hier auch Mahnungen  an Mitglieder, die das Tronic-Zahlungsziel nicht erfüllt haben, verschicken. Der Textinhalt ist vom System vorgegeben und kann nicht geändert werden: "Sie haben ein negatives Allianzsteuerkonto. Bitte begleichen Sie schnellstmöglich den ausstehenden Betrag. Bei einem weiteren Versäumnis könnte Ihre Allianzmitgliedschaft gefährdet sein. Ihre Allianzleitung".
Die Höhe der von der Allianzleitung festlegten Tronic-Zahlungen ist unabhängig von der Errungenschaftsaufgabe "Erreiche den gewünschten Tronic-Einzahlungsstatus bei Deiner Allianz", die Einzahlungen werden jedoch ebenfalls als Erfüllung angerechnet.

<b>Allianz-Forum</b>
Dieses Forum betrifft nur die Mitglieder einer Allianz.

<b>Allianzsuche</b>
Dieses Menü erlaubt es, nach einer Allianz zu suchen. Dabei wird einfach der zu suchende Allianzname oder das Kürzel in das Suchfeld eingegeben und dann die Suche mit "Suche starten" gestartet.

<b>Löschen</b>
Wie der Name schon sagt … hier kann die eigene Allianz gelöscht werden.
Hinweis: Löscht ein Allianzleader seinen Account, ohne vorher einen neuen Leader zu ernennen, so rückt automatisch der erste CoLeader auf dessen Posten.

<b>Allianzflotten</b>
Dieses Menü zeigt den Status aller Flotten der Allianz. 
Dabei wird zwischen angreifenden, zurückkehrenden und in der Heimat stationierten Flotten unterschieden. Sie dient der Übersicht für Flottenkommandeure. Hier sehen die Mitglieder, die einen Posten im Führungsstab bekleiden, mehr Information als alle anderen Mitglieder.

<b>Allianzhistory</b>
Die Allianzbiographie listet Ereignisse auf, welche in Verbindung mit der Allianzgeschichte stehen. Dazu gehören die Aufnahme von Mitgliedern und eine Dokumentation über den Ausbau von Allianzprojekten.

<b>Projekte</b>
Als Allianzprojekt werden Allianzgebäude bezeichnet, die nur der Allianzleiter bauen bzw. ausbauen kann. Sie kosten Allianzartefakte und Tronic, in einigen Fällen auch Quantenglimmer. (Allianzartefakte sind nicht zu verwechseln mit Allianz-Siegartefakten).
Für jeden erfolgreichen Angriff, den ein Allymember auf ein Alien (NPC-Sektoren) durchführt, wird der Allianz ein Allianzartefakt gutgeschrieben. Das Allianzartefakt wird auch gutgeschrieben, wenn die Kollektoren der Aliens nicht mehr erbeutet sondern nur noch zerstört werden können. Ebenso bekommt die Allianz in regelmäßigen Abständen ein Allianzartefakt (alle 96 WT).
Tronic müssen die Allymember in entsprechender Menge spenden.
Quantenglimmer kann nur in den Battlegrounds bei Kämpfen im System "Battleground Gamma" erworben werden, und ist in der Allianz mindestens ein Mitglied, das mindestens einen Spieler geworben hat (der nicht in Sektor 1 ist), so wird der Allianz pro abgeholtem Allianzbonus auch 1 Quantenglimmer gutgeschrieben.

Die <u>Allianzgebäude</u> bestehen aus:

- <i>Allianzraumbasis:</i>
Sie dient als Grundgebäude. Erst wenn sie vorhanden ist, können weitere Allianzprojekte gebaut werden.

- <i>Diplomatiezentrum:</i>
Wird benötigt, um ein Bündnis mit einer anderen Allianz begründen zu können (Meta-Allianz). Dieses Gebäude ist ausbaubar: pro Ausbaustufe erhält die Allianz 1% der Allianzboni der Partnerallianz (maximal 50%).

- <i>Informationsphalanx:</i>
Bewirkt, dass Geheimdienstinformationen, die ein Allymember mit seinen Agenten oder Sonden in Erfahrung bringt, für alle Spieler, die sich zum Zeitpunkt der Spionage in der Allianz befinden, sichtbar sind.

- <i>Leitzentrale Feuroka:</i>
Verstärkt die Angriffskraft der Flotten aller Allymember. Ausbaubar: pro Ausbaustufe wird die Angriffskraft der Schiffe um 1% verstärkt (maximal 10%).

- <i>Leitzentrale Bloroka:</i>
Verstärkt die Lähmkraft der Blockerschiffe aller Allymember. Ausbaubar: pro Ausbaustufe wird die Lähmkraft der Schiffe um 1% verstärkt (maximal 10%).

- <i>Artefaktgebäudeerweiterung:</i>
Ausbaubar: pro Ausbaustufe erhalten die Allymember einen zusätzlichen Platz in ihrem Artefaktgebäude (maximal 10 Plätze). Tritt man aus der Allianz aus, so gehen keine Artefakte verloren, das Artefaktgebäude schrumpft jedoch jedesmal, sobald Artefakte weg kommen (verbrauchen, verschmelzen oder Transfer in ein Basisschiff), bis es seine normale Größe ohne Allianzerweiterung erreicht hat.

- <i>Notfallrohstoffkonverter:</i>
Ermöglicht, dass man im Menü "Ressourcen" Rohstoffe (M, D, I und E) gegeneinander umwandeln kann. Im Gegensatz zu einer Rohstoffhandelsmission geschieht dies sofort, aber mit Verlust. Ausbaubar: Die Verluste werden umso geringer, je höher der Notfallrohstoffkonverter ausgebaut ist.

- <i>Kommunikationsphalanx:</i>
Verlängert die Dauer der Sichtbarkeit des Sektorstatus für die Allianz (Ausgangswert: 1 KT). Ausbaubar: Pro Ausbaustufe wird die Anzeigedauer um 1% erhöht (maximal 100%). Für Bau und Ausbau der Kommunikationsphalanx wird auch Quantenglimmer benötigt.

- <i>Fundbüro:</i>
Das Fundbüro bewirkt, dass die Position von in den Vergessenen Systemen entdeckten Fundstücken an alle Allymember übermittelt wird. Ausbaubar: pro Ausbaustufe werden die Fundstücke angezeigt, die bis zu der gleichen Fabrikausbaustufe gefunden wurden. Diese Information ist spielergebunden. Verlässt ein Spieler die Allianz, so gehen alle Hinweise über Fundstücke, die nur dieser Spieler entdeckt hat, verloren. Für Bau und Ausbau des Fundbüros wird auch Quantenglimmer benötigt. Das Fundbüro wird nur angezeigt, wenn auf dem Server die Vergessenen Systeme aktiv sind.

- <i>Kollektorensynthetisierer:</i>
Ausbaubar: Bei jedem Ausbau erhalten alle Mitglieder, die zum Zeitpunkt des Ausbaus in der Allianz sind, einen Kollektor. Es gibt 50 Ausbaustufen, man kann somit bis zu 50 Kollektoren von einer Allianz erhalten. Für Bau und Ausbau wird auch Quantenglimmer benötigt.


