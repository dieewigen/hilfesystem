Kosten, Voraussetzungen, Dauer und Beschreibung: Ebenso kann man einstellen, dass die Kosten, die Voraussetzungen bzw. eine Beschreibung dargestellt oder als Tooltip angezeigt werden. Die Dauer wird immer angezeigt und lässt sich nicht ausblenden.
Und zuletzt kann man ein akustisches Signal einstellen, das meldet, wenn eine Technologie abgeschlossen ist.
Die Änderungen werden aktuell, sobald man das Menü Technologien das nächste mal aufruft.

Mit den Schaltflächen "alle", "Gebäude", "Forschungen", "Basisschiffe" und "V-Systeme" aktiviert man einen Filter, der wahlweise nur einen bestimmten Teil der Technologinnen anzeigt, oder alle.

Durch Anklicken des Feldes startet man den jeweiligen Bau. Die  benötigte Zeit zur Fertigstellung wird danach angezeigt. Es ist möglich, Technologien abzubrechen, wobei die Kosten rückerstattet werden.
Stehen für den Bau unzureichende Ressourcen zur Verfügung wird dies durch die Meldung " Es sind nicht alle benötigten Rohstoffe/Voraussetzungen vorhanden " gemeldet.


