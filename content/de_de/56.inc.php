Über Bonuspunkte kann man den Wert eines Accounts auch über das Rundenende hinaus steigern, indem man Preisnachlässe im Schwarzmarkt erhält.

Je älter ein Account auf einem der Spielserver (z.B. xDE/SDE) wird, desto mehr Bonuspunkte erhält man.

Es ist nicht mehr möglich, Credits zu kaufen, doch hat man noch Credits auf dem Hauptaccount, so erhält man Bonuspunkte, wenn man welche auf den jeweiligen Spielserver transferiert 

Weiters erhält man Bonuspunkte für einen aktiven Premiumaccount.