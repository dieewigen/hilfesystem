<b>Sektorlager</b>

Im Sektorlager befinden sich alle Rohstoffe, die für den Bau der Sektorgebäude und der Sektorraumschiffe benötigt werden.

Die Rohstoffe können durch Spielerspenden, Sektorkollektoren oder Handelssteuer erhalten werden.


<b>Sektorlagerspende</b>
Im Menü "Ressourcen", im Abschnitt "Übersicht Sektorlager" hat jeder Spieler die Möglichkeit, in das Sektorlager zu spenden. Die Spenden sind keine Pflicht, jedoch sind sie notwendig, um die Sektorgebäude und die Sektorflotte bauen zu können.
Zudem erfüllen Spenden in das Sektorlager die Errungenschaft "Spende Rohstoffe", und die Errungenschaften wiederum schalten Spezialisierungen frei.
Es ist Aufgabe des Basiskommandanten, die Sektorgebäude zu bauen und die Sektormitglieder zu entsprechenden Spenden anzuregen.


<b>Sektorkollektoren</b>
Sektorkollektoren bringen Rohstoffe in das Sektorlager. Der Basiskommandant kann den Prozentsatz einstellen, wieviel von welchem Rohstoff produziert werden soll. Der Ertrag durch die Sektorkollektoren ist jedoch relativ gering, außerdem sind sie nur schwer erhältlich. Es ist für die Spieler ratsam, nicht wahllos in das Sektorlager zu spenden, sondern sich zu informieren, welche Rohstoffe gebraucht werden.


<b>Handelssteuer</b>
Jede Handelsmission ist mit einem Steuersatz belegt, der mit Abschluss der Mission abgebucht wird, sie kommt dem Sektorlager zugute. Die Höhe der Steuer kann der Sektorkommandant festlegen, sie beträgt zwischen zwei und fünf Prozent. Die aktuelle Höhe der Sektorhandelssteuer kann man im Menü "Sektorpolitik", im Abschnitt "Allgemeine Infos" nachsehen.
Die Handelssteuer wird, obgleich nicht freiwillig abgeführt, als Sektorlagerspende gewertet und auch zur Erfüllung der Errungenschaft "Spende Rohstoffe" gutgeschrieben.


<b>Spezialisierungen</b>
In den Spezialisierungen gibt es auch zwei, die dem Sektor Vorteile verschaffen, und damit dem Spieler, der sie gewählt hat, nur indirekt:
Dies sind die Spezialisierung II-lila, die bewirkt, dass der Bau von Sektorraumschiffen weniger Rohstoffe kostet, und die Spezialisierung V-lila, die bewirkt, dass die Sektorraumbasis mehr Rohstoffertrag erhält, so als ob sie mehr Kollektoren hätte. Beide Spezialisierungen können von mehreren Sektormitgliedern gewählt werden und summieren sich dabei. Begrenzt sind sie mit dem zehnfachen Wert, also bis zu zehn Spieler können sie wählen und bewirken damit eine Steigerung des jeweiligen Bereichs.


Hinweis:
Sektorlager oder Sektorkollektoren werden im Sektorkampf <u>nicht</u> geplündert bzw. erobert; der Sektorkampf dreht sich ausschließlich um Sektorartefakte.
Das Sektorlager wird durch den Sektorkampf nur indirekt belastet, da Sektorschiffe durch den Kampf verlorengehen und durch Neubauten ersetzt werden müssen. Aber auch hier hat der BK die Möglichkeit, die Sektorflotte zu ziehen und das Sektorartefakt dem Angreifer kampflos zu überlassen – oder einen Counter zu fliegen. Hier sind jedoch Scans über die Flotte und die Bauaktivitäten des angreifenden Sektors angebracht, sowie ein genauer Scan über mögliche verteidigende Flotten.


