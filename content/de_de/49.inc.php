<b>Ressourcen/Rohstoffe</b>

Im Universum von DE gibt es mehrere Ressourcen (Rohstoffe):

<u>Hauptrohstoffe:</u>

<b>Multiplex</b>
Multiplex ist eine silbern schimmernde, stahlähnliche Legierung. Es ist der Grundstoff schlechthin und wird praktisch für alles in großer Menge gebraucht. Der Errungenschaftenbonus wird in Multiplex ausgezahlt.

<b>Dyharra</b>
Dyharra ist ein von sich aus blau leuchtender Kristall. Es ist der zweitwichtigste Rohstoff und wird ebenso wie Multiplex für alle Bautätigkeiten (Gebäude, Forschungen und Einheiten) benötigt, allerdings in geringerer Menge als Multiplex.

<b>Iradium</b>
Iradium wird zur Lagerung in einer grün leuchtenden Flüssigkeit aufgelöst, da sich so die Gefahr einer Explosion verringert. Iradium wird nur für hochwertigere Gebäude, Forschungen und Einheiten verwendet.

<b>Eternium</b>
Bei Eternium handelt es sich um ein stark strahlendes Element, welches durch seine rote Färbung leicht erkennbar ist. Wie Iradium wird auch Eternium nur für Hochwertigeres benötigt, allerdings meist in geringerer Menge. 

<b>Tronic</b>
Tronic ist der fünfte Rohstoff im Spiel.
Es ist der einzige Rohstoff, der nicht durch Erzeugung oder Umwandlung gewonnen werden kann. Man kann Tronic nur durch Zufallsereignisse, durch Missionen oder bei Versteigerungen erhalten, selten sind Tronic auch unter den Fundstücken in den Vergessenen Systemen.
Da Tronic extrem selten vorkommt und seine Menge nur begrenzt ist, ist er sicherlich die wertvollste Ressource im ganzen Spiel. Eine aktive Mitwirkung an Versteigerungen für Tronic ist somit nicht wegzudenken.
Tronic ist auch in bestimmten Zeitabständen im Schwarzmarkt erhältlich.

<b>Palenium</b>
Palenium erhält man durch Zerstören eines Artefakts, als Fundstück in den Vergessenen Systemen oder in den Auktionen. Palenium wird für die Forschung der Titanen benötigt, weiters für den Ausbau der Artefaktplätze in den Basisschiffen, sowie für die Upgrades des Basissterns für die Battlegrounds.

<b>Quantenglimmer</b>
Für den Bau von Allianzgebäuden braucht der Allianzleiter Allianzartefakte, Tronic und für einige auch Quantenglimmer. Quantenglimmer kann nur in den Battlegrounds bei Kämpfen im System "Battleground Gamma" erworben werden.

<b>Credits</b>
Credits sind kein Rohstoff im eigentlichen Sinn. Sie sind ursprünglich als Kaufwährung entstanden, mittels derer man sich sehr geringe Vorteile erkaufen konnte, und das für den Kauf von Credits eingenommene Geld wurde vom Spielanbieter für die Systemerhaltung verwendet; doch wurde mit 31.12.2018 der Verkauf von Credits eingestellt. Eine Begründung ist <a href="https://rde.bgam.es/newspaper.php?id=373" target="_blank">hier</a> nachzulesen (öffnet neues Fenster).
Credits bleiben aber weiterhin im Spiel. Man erhält Credits als Aktivitätsbonus, es gibt das Artefakt <i>Creditrüssel</i>, das Credits erbringt, und auch in den Vergessenen Systemen befinden sich Credits unter den Fundstücken.
Mit Credits kann man den <a href="https://help.bgam.es/index.php?thread=de_de&post=44">Premiumaccount</a> (PA) kaufen, man kann im <a href="https://help.bgam.es/index.php?thread=de_de&post=54">Schwarzmarkt</a> Ressourcen und Kriegsartefakte kaufen, und man kann in den <a href="https://help.bgam.es/index.php?thread=de_de&post=6">Auktionen</a> auf manche Angebote mit Credits bieten.
In der <a href="https://help.bgam.es/index.php?thread=abl_de&post=1" target="_blank">Erweiterten Archäologie</a> (EA) kann man mit Credits Zeitverkürzungen beim Ausbauen von Gebäuden kaufen und auch die Wartezeit für die Raumschiffreparatur nach einem verlorenen Kampf mit Piraten beenden.


<u>Ressourcenbeschaffung und -verwaltung:</u>

<b>Menüpunkt Ressourcen</b>
Eines der wichtigsten Menüs ist das Ressourcenmenü. Hier werden neue Kollektoren gebaut. Sie liefern die notwendige Energie zur Rohstoffumwandlung. Die Energieverteilung auf die einzelnen Rohstoffe werden prozentual (von der Gesamtenergie) festgelegt (siehe auch: Kollektor).

<b>Bau von Kollektoren</b>
Menü "Ressourcen", Abschnitt "Kollektorenbau": Zuerst baut man natürlich über das Technologiemenü eine Kollektorenfabrik, dann ebenfalls im Technologiemenü die einzelnen Wandlersysteme für die 4 Rohstoffe (Multiplex, Dyharra, Iradium, Eternium). Tronic ist ein besonderer Rohstoff und kann nicht selber gewonnen werden.
Im Ressourcenmenü wird nun parallel dazu die Produktion von Kollektoren in Auftrag gegeben. Ein Kollektor benötigt stets 4 WT zur Fertigstellung. Je mehr Kollektoren gebaut werden, desto höher die Stückpreise.

<b>Energieverteilung</b>
Menü "Ressourcen", Abschnitt "Ressourcenertrag": Je nach benötigten Rohstoffen, aktueller Gesamtausbringung und Verteilungsschlüssel, kann man so seine eigene Rohstoffherstellung pro WT einstellen.
Der Verteilungsschlüssel ist unter Verhältnis angegeben:
"2:1" bedeutet also, dass man für 2 Einheiten Energieinput eine Einheit Multiplex als Rohstoff erhält. Steigert man den Energieinput erhöht sich natürlich auch der Output. Das kann über mehr Kollektoren (= mehr Gesamtenergie) oder über einen, für diesen Rohstoff zugeteilten höheren Energieinput, geregelt werden.
Je nach benötigten Rohstoffen (für Forschung, Produktion oder Bau) kann man also kurzfristig eine Energieumschichtung vornehmen, um z.B. schnellstens nur noch Dyharra zu produzieren.

<b>Sektorlager</b> 
Menü "Ressourcen", Abschnitt "Übersicht Sektorlager": Das Sektorlager ist die Sammelstelle für Ressourcen mit denen der BK die Sektorgebäude und Sektorschiffe finanziert. Um eine Spende zu tätigen, gibt man die entsprechende Menge an Rohstoffen in das nebenstehende Feld ein und drückt anschließend auf "Transferieren".
Achtung: Die Ressourcen können nicht zurückgebucht werden!

<b>Allianzdepot</b>
Menü "Allianz / Finanzen": Für den Bau der Allianzgebäude wird T benötigt. Dies muss von den Allianzmitgliedern aufgebracht werden. Im Menü "Allianz / Finanzen" kann man spenden. Die Allianzleitung kann einen Soll-Wert angeben, der unabhängig von der Errungenschaft "Erreiche den gewünschten Tronic-Einzahlungsstatus bei Deiner Allianz" ist.
Achtung: Getätigte Spenden können nicht mehr rückgebucht werden.

<b>Schwarzmarkt</b>
Menü "Schwarzmarkt": Im Schwarzmarkt sind in bestimmten Zeitabständen Rohstofflieferungen erhältlich. Diese bestehen aus Multiplex, Dyharra, Iradium und Eternium. Die erhältliche Menge wird mit steigender Rundenlaufzeit immer größer. Auch Tronic sind im Schwarzmarkt erhältlich.

<b>Rohstoffhandelsmissionen</b> und <b>Agentenmissionen</b>
Menü "Missionen": Mit Handelsmissionen lassen sich Rohstoffe tauschen. Dies gilt nur für Multiplex, Dyharra, Iradium und Eternium. Tronic können nicht eingetauscht oder erworben werden.
Tronic sind jedoch mit Agentenmissionen zu bekommen.


<u>Andere Rohstoffe:</u>

<b>Vergessene Systeme</b>
In den Vergessenen Systemen (VS) können Rohstoffe abgebaut werden, die man wiederum für den weiteren Ausbau der VS benötigt. Dies sind:
Eisen
Titan
Mexit
Dulexit
Tekranit
Ylesenium
Serodium
Rowalganium
Sextagit
Octagium

Die Namen dieser Rohstoffe sind mit jenen der Erweiterten Archäologie (EA) identisch, jedoch besteht kein Zusammenhang zwischen den Rohstoffen der VS und der EA. In der Erweiterten Archäologie abgebaute Rohstoffe können nicht für die Vergessenen Systeme verwendet werden und umgekehrt auch nicht.


