Fehler sind unvermeidlich und tauchen auch noch nach Jahren hin und wieder auf.

Wenn du etwas siehst, mach uns vorsichtshalber einen Screenshot (DRUCK-Taste drücken, dann ist der Inhalt des Bildschirms im Speicher, den du dann einfach in jedem Paint-Programm durch "einfügen" und "speichern" sichern kannst) und schreibe die Meldung dann unter dem Forumspunkt http://forum.bgam.es/board.php?boardid=9 "Bugs im Spiel" ins DE-Forum.

Wie man einen Bugreport erstellen sollte, steht hier: https://forum.bgam.es/thread.php?threadid=20744

Entweder helfen dir dann die anderen Spieler sofort weiter, oder das DET sieht sich das an und fragt im Zweifel nach mehr Informationen.