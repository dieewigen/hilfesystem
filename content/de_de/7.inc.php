Der Basiskommandant (BK) wird vom Sektorkommandant (SK) bestimmt. Er hat die Aufgabe, sich um die Entwicklung und Fortschreitung der Sektorraumbasis zu kümmern.

Über das Menü "Sektorpolitik / BK-Menü" (nur für den BK sichtbar) gelangt man in das Basiskommandantenmenü. Der BK ist verantwortlich für den Sektorkampf und steuert zu diesem Zweck die Sektorflotte.

Da Die Sektorgebäude aus der Sektorkasse finanziert werden, sollten alle Sektormitglieder in das Sektorlager einzahlen.
Ein guter BK bittet meistens jedes Sektormitglied, einen bestimmten Betrag zu spenden, der von den Kollektoren des Spielers abhängig ist.

Ein Spieler kann nicht Sektorkommandant und Basiskommandant gleichzeitig sein, es geht nur eines von beiden – einzige Ausnahme: solange der Spieler in einem Sektor alleine ist.