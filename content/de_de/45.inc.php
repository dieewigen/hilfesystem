<b>Produktion</b>

Im Menü "Produktion" habt ihr nun die Möglichkeit, eure Flotten auf- bzw. auszubauen, sowie Handelswaren für die Vergessenen Systeme zu produzieren. 


<b>Produktion von Einheiten</b>

Welche Funktionen die jeweiligen Schiffe ausüben, könnt ihr erfahren, indem ihr einfach auf dessen Name klickt. Wichtig hierbei ist die Ausgewogenheit der Flotte, z.B. zwischen Schlachtschiffklassen, Kreuzerschiffklassen und Jägerschiffklassen. Achtet darauf, dass Kreuzer, Schlachtschiffe und Träger ausreichend sowohl Jäger als auch Bomber transportieren können. Die exakten Ladekapazitäten findet Ihr bei der jeweiligen Schiffsbeschreibung.
Dies ist wichtig, da die Jägerschiffklasse allein die Flotte lähmen und somit einen Angriff um viele Ticks (Reisezeit bis zum Ziel) verlängern.
Ebenso ist es wichtig, auf etwaigen nötigen Support zu achten.

Türme dienen ausschließlich der Verteidigung. Sie sind stationär auf dem Planeten vorhanden und können somit nicht wie die Schiffe abgezogen werden, falls man sich entschließt, nicht zu kämpfen.
Zu den Türmen zählen auch die Orbitaljäger.

Die Stärke der Bewaffnung und die Trefferpunkte findet ihr im Menü: "Produktion / Einheiteninformationen".
Bis auf die Bomber der Ishtar verfügt jedes Schiff und jeder Turm über konventionelle Waffen, manche Schiffe und Türme verfügen auch über EMP-Waffen. Die Bomber der Ishtar haben nur EMP-Waffen.
Die Trefferpunkte entsprechen der Panzerung eines Schiffes. Erreichen die Treffer, die ein Schiff erhält, den Wert der Trefferpunkte, so wird es zerstört. Das gilt ebenso für Türme.

Menü "Produktion":

Am oberen Ende der Tabelle seht ihr die Boni durch Artefakte, welche sich reduzierend auf die Baukosten auswirken.

Um einen Bau in Auftrag zu geben, tragt die gewünschte Menge in das Feld rechts ein. Unter der Tabelle wird nun der zu zahlende Rohstoffbetrag angegeben. Gibt man eine höhere Zahl ein als Ressourcen vorhanden sind, so werden jene Ressourcen in roten Zahlen dargestellt. Trotzdem ist nicht der gesamte Bauauftrag ungültig sondern es werden nur so viele Schiffe gebaut wie es möglich ist.

Durch bestätigen der Schaltfläche "Bauen" wird der Bauauftrag gestartet.

Anders als bei der Gebäude- und Forschungsfunktionen, kann man hier mehrere Bauaufträge gleichzeitig durchführen. Das Abbrechen eines Auftrages ist dagegen nicht möglich.

Unterhalb der Bauauftragstabelle werden die in Bau befindlichen Schiffe aufgelistet, samt der noch verbleibenden Zeit bis zur Fertigstellung.

Fertiggestellte Türme sind mit Bauvollendung sofort einsatzbereit, fertiggestellte Schiffe werden automatisch der Heimatflotte zugeteilt und sind ebenfalls sofort einsatzbereit.

Vor- und Nachteile bei Flottenzusammenstellungen mit bestimmten Schiffstypen siehe unter <a href="https://help.bgam.es/index.php?thread=de_de&post=23">Flotte</a>.


<b>Produktion von Handelswaren</b>

Unterhalb der Produktionstabelle für Einheiten befindet sich der Bereich, in dem man Handelswaren produzieren kann. Dieser Teil ist nur aktiv, wenn auf dem betreffenden Server die Vergessenen Systeme aktiviert sind. 
Die Handelswaren I bis V können hier gebaut werden, sowie der Infiltrator Omega.

Die Tabelle beginnt mit der Auflistung, wieviele Ausbaustufen von Omega-, Psi-, Chi-, Phi- und Ypsilon-Fabriken man jeweils insgesamt hat.
Es ist gleichwertig, ob man z.B. zwei Omega-Fabriken jeweils mit der Ausbaustufe 1 hat, oder ob man eine Omega-Fabrik mit der Ausbaustufe 2 hat. Es zählt jeweils die Summe aller Ausbaustufen des betreffenden Fabrikstyps.
Darunter befinden sich die Felder, in denen man die Bauaufträge für die Handelswaren I bis V und den Infiltrator Omega geben kann. Es ist detailliert angegeben, was der Bau an Ressourcen kostet, wie lange die Bauzeit ist und welche Fabrikskapazität von welchem Fabrikstyp man dafür braucht. (Diese Fabrikstypen muss man in den Vergessenen Systemen auf den freien Feldern der Planeten bauen).
Eine Besonderheit stellt der Infiltrator Omega dar. Für dessen Bau braucht man neben anderem einen Drasogi-Kristall, den man zuvor im Warenhandel, im Tausch gegen Handelswaren I, erwerben muss (Menü: "Missionen / W-Handel"). Den Drasogi-Kristall braucht man, um den Planeten shaKer ausbauen bzw. erforschen zu können, der irgendwo unter den Vergessenen Systemen versteckt ist.

Hat man die gewünschte Menge des Produkts, das man bauen möchte, eingetragen, klickt man unten auf die Schaltfläche "Bauen" und der Bau wird begonnen.

Wieviele Handelswaren man hat, kann man im Menü "Ressourcen", ganz unten im Abschnitt <b> Dein Lager</b>, nachsehen.



