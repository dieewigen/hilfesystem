<b>Rassen</b>

Im Spiel gibt es insgesamt fünf Rassen; vier davon kann man für sich als Spieler aussuchen, die fünfte ist ein NPC.
Jede Rasse hat ihre Vor- und Nachteile.


<b>Die Ewigen</b>

Die Ewigen sind ein humanoides Volk, dessen Herkunft ein Geheimnis ist. Man sagt, dieses Volk sei schon alt gewesen, als die anderen alten Völker noch jung waren. Sie beherrschten schon frühzeitig die überlichtschnelle Raumfahrt und haben auch in entfernten Regionen Kolonien gegründet.

Als eine Rasse der Entdecker, tief geprägt durch die Meditation, neigen die Ewigen im Normalfall nicht zu unüberlegter Gewaltanwendung. Ihre Schiffe und Planeten sind daher normal befestigt und sowohl im Angriff als auch bei der Verteidigung gut ausbalanciert.

Nur wenige erfahrene Spieler spielen die Ewigen, da sie keine besonderen Vorteile haben.

Vorteil: Fähigkeiten der Rasse ausgeglichen, daher ideal für Anfänger.

Nachteil: Rasse hat keine Spezialisierungen, sondern beherrscht alle Fähigkeiten gleichermaßen stark.

Besonders starke Schiffe: keine, aber Jagdboote fliegen 1 Tick schneller als die Jagdboote bei anderen Rassen.


<b>Ishtar</b>

Die Ishtar sind die Meisterhändler des Universums. Durch ihre großen Expeditionen im Weltall haben sie viele Kontakte geknüpft. Dies und ihr telepathisches Talent befähigten sie dazu, ohne Übersetzer zu kommunizieren. Schnell stellte sich heraus, dass sie wahre Meister im Handel - mit allen möglichen Gütern und Dingen - waren. Sie betreiben die meisten Handelsgilden und Handelsposten, die es im Universum gibt.

Um ihre Handelsposten zu schützen, haben sie die Verteidigungsanlagen stark entwickelt. Ishtar sind Meister im Blocken.

Vorteil: Bonus im Handelssystem, Starke Verteidigung, und EMP Waffen sind stärker als bei anderen Rassen.

Nachteil: Niedrige Feuerkraft.

Besonders starke Einheiten: Jagdboote, Bomber (die Bomber der Ishtar sind <u>nur</u> mit EMP-Waffen ausgerüstet, im Gegensatz zu anderen Rassen), Verteidigungsanlagen.


<b>K'Tharr</b>

Die K'Tharr sind eine gezüchtete, genmanipulierte, aufrecht gehende Spinnenrasse. Keiner kann sagen, woher sie kommen oder wer ihr Schöpfer war, aber dass sie da sind und existieren, ist für das normale Leben im Universum eine ernste Bedrohung. Sieht man ihre Schiffe kommen, dann kann man keine Gnade erwarten. Bisher wurde alles überrannt, was ihr Ziel war, und es wird eine Herausforderung, sie zu besiegen und zu vertreiben. Doch seid mutig, edle Herren, noch ist nicht aller Tage Abend.

Vorteil: Erhöhte Feuerkraft.

Nachteil: Niedrige Verteidigung.

Besonders starke Schiffe: Jäger, Zerstörer, Kreuzer und Schlachtschiffe.


<b>Z'tah-ara</b>

Die Z'tah-ara sind ein Völkerverbund aus den verschiedensten Insektenrassen. Wenn man auf ihren Planeten wandelt, wird man käfer-, wespen-, libellen- und schrecken-ähnliche Wesen treffen. Durch ihre Vermischung der unterschiedlichsten Arten, haben sie ein Talent zur Anpassung an jede Situation und Rasse entwickelt. In nur wenigen Augenblicken kommen sie überall im Universum zurecht. Dies ließ sie zu Meister der Spionage und Überraschung werden.

Apropos Überraschung, oft werden ihre Flotten unterschätzt, denn es gibt keine bekannten Scanner, die getarnte Schiffe auffinden können, die sich im Anflug befinden. Also Achtung da draußen: Ihr Nachbar könnte ein Z'tah-ara sein!

Vorteil: Die Z'tah-ara sind in der Lage ihre Geisterschrecken (Zerstörerklasse) zu tarnen, bessere Spionage, Massenproduktion, wodurch man den Gegner abschrecken kann, EMP Waffen.

Nachteil: Masse statt Klasse meist hohe Verluste im Kampf.

Besonders starke Schiffe: Jagdboote. Zerstörer haben nur mittelmäßige Feuerkraft, jedoch ihre Tarnung als Vorteil.


