<b>Kopfgeld</b>
Greift ein Spieler einen anderen Spieler an (jedoch nicht bei Angriffen auf Aliens), so wird Kopfgeld auf den Angreifer ausgesetzt.

Das Kopfgeld wird, unabhängig davon, ob der Angriff erfolgreich war oder nicht, ausgesetzt, sobald beim Ziel durch den Angriff Schiffe oder Türme zerstört wurden.
Die Höhe des Kopfgeldes richtet sich nach der Höhe der zerstörten Punkte: Pro zerstörtem Punkt werden 10 Rohstoffeinheiten, verteilt auf M, D, I und E, ausgesetzt.
Ist auf einen Spieler Kopfgeld ausgesetzt, so werden seine Punkte in der Sektoransicht fett dargestellt. Bewegt man den Mauszeiger über die Punktezahl, so wird die Höhe des Kopfgeldes als Tooltip angezeigt. 
In der Kopfgeldrangliste werden jene Spieler aufgelistet, auf die Kopfgeld ausgesetzt ist. (Menü "Rangliste / Spieler / Kopfgeld")


<b>Kopfgeldjagd</b>
Kopfgeld steht dem Angreifer bereits zu, sobald sein Angriff einschlägt. Auch wenn der Angriff erfolglos ist und keine Kollektoren bringt, so wird Kopfgeld ausgezahlt.

Die Höhe des ausgezahlten Kopfgeldes richtet sich nach den Punkten der beim Ziel (nicht aber bei eventuellen anderen Deffern) vernichteten Einheiten: Pro Punkt werden 10 Rohstoffeinheiten ausbezahlt.
Das Kopfgeld wird in Ressourcen M, D, I und E ausgezahlt.
Das zu erhaltende Kopfgeld kann nicht höher sein als jener Betrag, der auf das Ziel ausgesetzt ist, und es beträgt maximal 25% der FP, die der Angreifer bei dem Angriff verloren hat.

Greifen mehrere Spieler ein Ziel an, auf das Kopfgeld ausgesetzt ist, so wird das Kopfgeld nach dem Verhältnis der eingesetzten FP aufgeteilt.

Beachte: Auch auf Kopfgeldjäger wird wiederum Kopfgeld ausgesetzt; dies soll eine Stagnation des Kopfgeldes verhindern. Das Kopfgeld soll ein zusätzlicher Anreiz neben den eroberten Kollektoren sein, indem es die Verluste des Angreifers bis zu 25% verringert (Schlachtschiffrecycling nicht mit eingerechnet).

In der Rangliste der Kopfgeldjäger werden alle erfolgreichen Kopfgeldjäger angezeigt. (Menü "Rangliste / Spieler / Kopfgeldjäger").