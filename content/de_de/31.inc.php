<b>Hyperfunk</b>

Menü: Hyperfunk
Im Gegensatz zu Nachrichten können Hyperfunknachrichten (kurz: HFN) nicht durch Agenten ausspioniert werden.


<b>Senden</b>

Hyperfunknachrichten an einzelne Spieler versendet man über die Schaltfläche <i>Spieler</i>.
Klickt man den Namen eines Spielers in der Sektoransicht an, kommt man ebenfalls in das Sendemenü, wobei in diesem Fall die Zielkoordinaten bereits eingetragen sind. Dies ist ebenfalls so, wenn man bei einer erhaltenen HFN auf "Antworten" klickt.
Man kann auch Nachrichten an den gesamten <i>Sektor</i>, an die eigene <i>Allianz</i> (wobei hier einer der Allianzleiter über die Weiterleitung entscheidet) oder an die auf der Freundesliste eingetragenen <i>Freunde</i> durch Klick auf die jeweilige Schaltfläche versenden!

Freunde lassen sich unter <i>Optionen</i> der Freundesliste hinzufügen.

In einer einfachen Spieler-an-Spieler-HFN müssen die Zielkoordinaten angegeben werden; wenn eine HFN an den Sektor, die Allianz oder die Freunde geschickt werden soll, erübrigt sich das. Das Hyperfunksystem erlaubt einige vorgegebene Smileys und Textformatierungen. Eine Legende dazu gibt es unter der Schaltfläche "?".

Mit Klick auf "Hyperfunknachricht absenden" wird die Nachricht dem Empfänger unverzüglich zugestellt. Von jeder gesendeten HFN wird eine Kopie im Postausgang gespeichert.

Die maximale Länge für eine HFN beträgt 10.000 Zeichen. Zusätzlich sind bis zu 20 Smileys möglich.
Die Schaltfläche "Nachrichtenlänge prüfen" stammt noch aus der Zeit, als maximal 4.000 Zeichen erlaubt waren. So konnte man vor dem Senden noch die Textlänge prüfen, ob sie so vom Server akzeptiert wird. Mittlerweile ist eine Überprüfung der Nachrichtenlänge eigentlich nicht mehr notwendig.


<b>Empfangen</b>

Wenn neue Hyperfunknachrichten empfangen wurden, wird das automatisch in der oberen Infoleiste angezeigt.
Durch Klicken auf das Hyperfunksymbol gelangt man direkt ins Hyperfunkmenü und damit zum Posteingang.


<b>Optionen</b>

Mailservice: Mit dieser Funktion lassen sich alle Hyperfunknachrichten an die eigene E-Mail-Adresse verschicken. Das gilt für den Posteingang, den Postausgang und für das Archiv!
Dabei werden alle HFNs ingame gelöscht - auch das Archiv!

Meine Freunde: Hier lassen sich Freunde auf die Freundesliste durch Eingabe ihrer Koordinaten hinzufügen bzw. wieder entfernen mit dem "löschen"-Link. Es ist möglich über den "Freunde"-Versand oben eine HFN an alle seine Freunde auf einmal zu verschicken.

Ignorelist: Hier lassen sich weniger geliebte Zeitgenossen, Flamer und Spammer auf eine Ignorelist verschieben. HFNs, die einen von ignorierten Personen zugeschickt werden, erscheinen erst gar nicht im Postfach. Ist man selbst auf der Ignorelist des Ziels, so erhält man bei einem Sendeversuch eine Mitteilung, dass gesendete HFNs vom Ziel ignoriert werden.
Nicht ignoriert werden Mehrfachempfängernachrichten wie z.B. eine Allianz-Rund-HFN.


<b>Leader-Optionen</b>

Allianzleiter haben die Möglichkeit im Politikmenü unter HF-Member über die Weiterleitung von Allianz-HFNs zu entscheiden, können das allerdings auch automatisieren: Über eine Schaltfläche lässt sich einstellen, dass alle Allianz-HFNs automatisch an alle Mitglieder weitergeleitet werden.
Natürlich ist die Entscheidung auch unumkehrbar: Die automatische Weiterleitung lässt sich jederzeit wieder deaktivieren. Je nach eingestellter Option ist diese Schaltfläche grün oder rot.


<b>Sonstiges</b>

<span style="color: #ff0000"> Achtung:</span> Eine Hyperfunknachricht wird automatisch nach 14 Tagen gelöscht. Um Nachrichten länger zu speichern, gibt es die Möglichkeit, bis zu 10 komplette HFNs in das <u>Archiv</u> zu verschieben. Die Vorhaltedauer dort ist die aktuelle Runde, also innerhalb einer Runde unbegrenzt.


