Also benehmt euch in den Mails untereinander. Keine Beleidigungen, Drohungen oder sonstige Ausfälle und immer einen freundlichen Ton, wenn es geht!

Andernfalls sind die Konsequenzen Zwangsurlaub oder gar der Verlust des Accounts!