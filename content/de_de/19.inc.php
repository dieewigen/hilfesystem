<b>Errungenschaften</b>

In DE könnt ihr bestimmte Aufgaben lösen, dafür erhaltet ihr Multiplex. Je höher die Stufe, desto mehr Rohstoffe erhaltet ihr.
Errungenschaften spielen für die Berechnung der Erhabenenpunkte eine Rolle, und sie schalten die Spezialisierungen frei.

