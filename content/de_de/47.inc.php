<b>Rangliste</b>

In DE gibt es auf jedem Server mehrere Ranglisten. 

Die Ranglisten erreicht man über das Menü "Rangliste" oder indem man in der oberen Ressourcenleiste auf die Punktezahl oder auf das Symbol für die Punktezahl klickt.

Hier werden die vier großen Themenbereiche angezeigt, für die es Ranglisten gibt: Spieler, Sektor, Allianz und Handel.
Darunter ist angegeben, welche Gewinne bei Rundenende in welcher Kategorie ausbezahlt werden.

Durch Anklicken der Schaltflächen <i>Spieler</i>, <i>Sektor</i>, <i>Allianz</i> oder <i>Handel</i> kann man sich die Top 100 der jeweiligen Rangliste anzeigen lassen. 
(Anmerkung: Sektoren und Allianzen sind erfahrungsgemäß weniger als 100.)

Die danach angezeigte Rangliste kann durch Anklicken der Überschriften in der Tabelle nach unterschiedlichen Kriterien sortiert werden.

Rangliste Spieler:
Punkte, Kollektoren, Türme, Rundenpunkte, Errungenschaften, Kopfgeld, Kopfgeldjäger, Erhabenenpunkte, Executorpunkte.

Rangliste Allianz:
Punkte, Siegartefakte, Bündnisse.

Die Listen werden nur mit jedem Tick neu berechnet.