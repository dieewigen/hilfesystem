<b>Das Spieluniversum</b>

Es gibt im Universum verteilt viele Enklaven, die von den ERBAUERN erschaffen worden sind.

Multidimensionale Planeten

In mehreren Galaxien gibt es Sonnensysteme, in denen es Planeten gibt, die nicht nur einmal vorhanden sind.

Diese Planeten (auch Systeme genannt) sind in unbekannter Anzahl in vielen parallelen Dimensionen verfügbar.

Die Planeten und der sie umgebende Raum befinden sich in einer speziellen dimensionalen Verwerfung in der auch besondere physikalische Bedingungen herrschen.

Diese exotische Physik ermöglicht Rohstoffe (z.B. Multiplex, Dyharra, Iradium, Eternium, Tronic), die im normalen Raum nicht existieren können und die sich auflösen, sobald sie diese besondere Dimension verlassen.
Auch ermöglicht sie die sogenannten Kollektoren, die aus der Hyperenergie der Sonnen exotische Energie schöpfen, die dann in Rohstoffe umgewandelt werden können. Der Bau der Kollektoren ist komplex und je mehr Kollektoren sich in einem System befinden, desto schwieriger wird die Herstellung auf Grund der Störstrahlung, die diese aussenden.

Diese Planeten werden von verschiedenen Rassen bewohnt, wobei nicht mehr bekannt ist, wie und wann deren Kolonisierung stattgefunden hat.

Erzeugt wird das Dimensionsfeld von einer STRUKTUR der ERBAUER. Diese ermöglicht auch die Reise zwischen den einzelnen Planeten. Es wird noch immer gerätselt, ob es in jeder Dimension eine extra Struktur gibt, oder ob es nur eine einzelne Struktur gibt, die in jeder Dimension hineinragt.

Das Reisesystem wird von der STRUKTUR variiert und es gibt Planeten, zwischen denen eine schnellere Reise möglich ist und dieser Zusammenschluss wird Sektor genannt.

Es gibt keine direkte Einflussmöglichkeit auf die STRUKTUR, diese reagiert jedoch auf politische Umwälzungen und immer wenn es einen ERHABENEN gibt, erlöschen die Dimensionsfelder und die Hochtechnologie zerfällt.
Ausgenommen davon ist EDE, dort erlischt nur das Dimensionsfeld des ERHABENEN.

Das ganze Leben spielt sich auf und um diese Planeten ab, denn wenn man sich zu weit entfernt, zerfällt die Technologie. Das Erreichen von anderen Planeten des Sonnensystems müsste also mit primitiver Technik erfolgen, was bisher niemanden interessiert hat, weil der Aufwand zu groß dafür ist.


<b>Das EFTA-Projekt</b>

In einer rollenspielartigen Welt - dem Planeten EFTA - kann man einen ferngesteuerten Cyborg steuern. Der Cyborg trifft auf EFTA auf eine Vielzahl von Wesen, die er bekämpfen kann oder muss. Durch das erfolgreiche Besiegen eines Wesens erhält der Spieler Erfahrungspunkte. Außerdem kann man auf EFTA eine Reihe von Quests lösen, wodurch man Boni erhält.


<b>Die Erweiterte Archäologie (EA)</b>

Dieses ist wie EFTA ein Spiel im Spiel. Man befehligt ein Raumschiff und befindet sich mit anderen Spielern in einer Fraktion. Diese liegt im Wettstreit mit anderen Fraktionen.

Die Aufgabe der Spieler ist es, Sonnensysteme zu erforschen, zu kolonisieren und auszubauen.

Die Sonnensysteme befinden sich in einem weit entfernten Gebiet und der Kontakt zum Raumschiff ist nur per Hyperfunk möglich und aus diesem Grund ist kein Transfer von Rohstoffen möglich. Die EA ist ein geschlossenes System.