<b>Server</b>

Bei DE gibt es mehrere Spielserver, auch Galaxien genannt, die mit unterschiedlichen Geschwindigkeiten und Bedingungen laufen.

Aktuell (Ende 2019)gibt es folgende Server:

- xDE: WT 10 Minuten, KT 1 Stunde; 
- SDE: WT 3 Minuten, KT 12 Minuten
- EDE: WT 3 Minuten, KT 12 Minuten
- RDE: WT 1 Minute, KT 4 Minuten
- CDE (Community Server, auf dem die Spieler selbst die Geschwindigkeit/Einstellungen festlegen können; seit mindestens fünf Jahren stehen unverändert WT 1 Minute, KT 3 Minuten)
- DDE: Testserver mit variabler Ticklänge, je nach Tageszeit. WT immer 3 Minuten, aber KT von 8 Uhr bis 23 Uhr 20 Minuten, und zwischen 23 und 7 Uhr 2 Stunden (zur vollen Stunde)

Historie:
In der Vergangenheit gab es außerdem folgende Server:
tDE oder auch DE:DV (den Alternativserver)
nDE (den früheren Hauptserver)
tDE (alt), einen Testserver
BGDE (Server mit aktiver Unendlichkeitssphäre)
Obenstehender Stand war bis Anfang 2012;
ENSDE (englischsprachiger Server), wurde 2014 aufgegeben


Weitere Anmerkungen:
xDE, ursprünglich der Standardserver, ist inzwischen weniger beliebt als SDE, jedenfalls spielen weniger Spieler xDE als SDE; offenbar sind Kampfticks im Bereich von 12 Minuten bei den Spielern beliebter als Kampfticks von einer Stunde. Das S in SDE bedeutete "Speed", also rasch, aber "rasch" ist mittlerweile zum Standard geworden.
SDE war auch bis 2018 der Testserver, es wurde als Testserver jedoch von DDE abgelöst. DDE existierte bereits seit 2017, wurde jedoch erst 2018 offiziell zum Testserver erklärt.
In RDE hat sich im Oktober 2018 die Mehrheit der Spieler gegen die Vergessenen Systeme ausgesprochen, deshalb sind diese dort nicht aktiviert.
EDE ist aus einem Vorschlag einer Ewigen Runde 2015 entstanden: Erst wurde die Ewige Runde auf SDE getestet, und nachdem sich etwa gleichviele für und gegen die Endlosrunde ausgesprochen haben, wurde der Server verdoppelt: Zurück zu SDE mit Runden und EDE neu als Endlosrunde.
Auf DDE werden momentan getestet: KT, die deutlich länger als 4x WT sind; 100% Recycling beim Angriffsziel (aber damit auch keine FP und keine Kriegsartefakte); KT mit 2-Stunden-Intervallen über Nacht; und ein Hardcore-Rundenmodus, bei dem man 5 mal einen Erhabenen-Teilsieg erringen muss, um Erhabener zu werden (Stand: November 2019).