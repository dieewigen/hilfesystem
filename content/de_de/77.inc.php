<b>Sektorkollektoren</b>

Sektorkollektoren können nicht gebaut werden.
Es gibt aktuell nur zwei Möglichkeiten, wie Sektorkollektoren dazugewonnen werden können:

A) Sekkollus-Artefakte:
Dieses Artefakt erzeugt 1-2 Sektorkollektoren. Es ist im Namen der Sektorgemeinschaft geboten, dieses Artefakt nicht zur Paleniumgewinnung zu zerstören, sondern es zu aktivieren.

B) Aus dem Sektor ausscheidende (löschende oder inaktive) Spieler:
Wenn ein Spieler gelöscht oder vom Inaktivenscript in Sektor 1 verschoben wird, dann erhält der Sektor als Ausgleich die Anzahl der Spielerkollektoren als Sektorkollektoren (max. 75 pro Spieler), wobei diese dem Spieler nicht weggenommen werden. Diese Maßnahme soll Sektoren mit starker Spielerfluktuation einen gewissen Ausgleich verschaffen, da anzunehmen ist, dass solche Spieler keine oder nur geringe Spenden in das Sektorlager tätigen.


Die Sektorkollektoren werden auch schon gesammelt, wenn noch keine Sektorraumbasis besteht, ergeben jedoch nur einen Rohstoffoutput von 50%. Erst wenn die Basis gebaut worden ist, erhöht er sich auf 100%.

Ein Sektorkollektor hat einen Energieoutput von 100 pro WT, im Verhältnis 1:1, 2:1, 3:1 und 4:1 für M, D, I und E.

Es gibt keine Artefakte, die den Output der Sektorkollektoren erhöhen, jedoch bewirkt die Spezialisierung V-lila, dass die Sektorraumbasis mehr Rohstoffertrag erhält, so als ob sie mehr Sektorkollektoren hätte.

Der BK kann das Verteilungsverhältnis der Sektorkollektoren einstellen, jedoch hat er keine Möglichkeit, Rohstoffe zu tauschen oder Tronic zu erzeugen. Es ist für die Spieler ratsam, nicht wahllos in das Sektorlager zu spenden, sondern sich zu informieren, welche Rohstoffe gebraucht werden, denn es ist schwer, fehlende Rohstoffe alleine mit den Sektorkollektoren zu erzeugen. Tronic müssen auf jeden Fall gespendet werden.

Die Spieler, die den BK-Posten nicht bekleiden, können die Anzahl der Sektorkollektoren im Menü "Sektorpolitik", im Abschnitt "Allgemeine Infos" sehen.

Die Anzahl der Sektorkollektoren von anderen Sektoren kann der BK über einen Scan Level 1 (Kosten: <nobr>5 T)</nobr> herausfinden.

Der Sektorkampf geht nur um Sektorartefakte, nicht um Sektorkollektoren oder Rohstoffe im Sektorlager. Sektorkollektoren können weder gestohlen werden, noch sonst wie verlorengehen. Einmal vorhanden, bleiben sie im Sektor bis Rundenende.

Ein Tipp für den BK:
Sektorraumschiffe kosten M, D, I und E im Verhältnis von 4:1:1:4. Genau diese Energiemengen an Rohstoffen bringen die Sektorkollektoren, wenn man sie im Verhältnis 16-8-12-64 einstellt.


