<b>ERBAUER</b>

Von den ERBAUERN handeln viele Legenden, aber echte Informationen sind rar.

Man vermutet, dass es sich um eine uralte Zivilisation handelt, die in vielen Teilen des Universums tätig war und technische Meisterleistungen vollbracht hat.

Auf sie gehen mannigfaltige Artefakte zurück, die multidimensionalen Planeten, dazu der EFTA-Planet und das Reisesystem in der Ablyon-Galaxie.

Ob sie ausgestorben, oder sich nur in einen unbekannten Bereich zurückgezogen haben, ist unbekannt.

Die DX61a23 behaupten, ein Dienervolk der ERBAUER zu sein, aber bestätigen lässt sich dies nicht.