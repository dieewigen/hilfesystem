<b>Kampfscript</b>

<b>Warum wurden keine Kollektoren geklaut, obwohl das Ziel keine Flotte hatte?</b>

* Beispiel:
:Der Angegriffene hat 0 Kollektoren verloren. Deine Flotte hat 0 Kollektoren erbeutet. 

* Antwort: 
:Es waren zu viele Angreifer. Mehr, als es zu verteilende Kollektoren gab. 


Beispiel:

Ziel hat 27 Kollektoren --> 4 zu verteilende Kollektoren (15%) --> bei mind. 5 Angreifern mit etwa gleichgroßer Flotte bekommt keiner Kollektoren, da die Anzahl z.B. 0,8 Kollektoren abgerundet wird und somit 0 ist.
Hätte einer deutlich mehr Flotte dabei gehabt und wäre anteilig über 1,xx Kollektoren gekommen, wäre dieser geklaut worden.


<b>Warum hab ich nicht die volle Anzahl an möglichen Kollis bekommen, wenn mein Mitatter keine Transen dabei hatte? (beide Flotten waren etwa gleich stark)</b>

* Beispiel:
:Ziel hatte 100 Kollis, demnach sind rechnerisch 15 klaubar, habe aber nur 7 bekommen. 
:Der Angegriffene hat 7 Kollektoren verloren. Deine Flotte hat 7 Kollektoren erbeutet. 

* Antwort: 
:Die Kollis werden anteilig zur Fleet berechnet, unabhängig davon ob Transen geschickt wurden oder nicht. 


Beispiel: 

Ziel hat 100 Kollis --> 15 zu verteilende Kollis (15%) --> bei 2 Attern bekommt der eine anteilig der Fleet z.B. 7 Kollis, der Mitatter, welcher keine Transen dabei hatte wird zwar anteilig mit eingerechnet, kann seinen Kollianteil jedoch nicht mitnehmen. Diese Kollis verbleiben somit beim Ziel.


<b>Wie funktioniert das mit dem Jagdbootsupport?</b>

Wieviele große Schiffe brauche ich damit meine Jagdboote ihre volle Kraft zeigen? 


Antwort: 

Der volle Support ist nur möglich, wenn Schlachtschiffe anwesend sind, ansonsten ist der Support 
unter 100%. 

Gesamtsupport setzt sich zusammen aus Grundsupport (ca. 1k Jagdboote sind immer supportet), Kreuzer und Trägersupport, der jedoch eine gewisse Grenze nicht überschreiten kann, und dem Schlachtschiffsupport (volle Unterstützung möglich). 

Richtwerte: Kreuzer -> 10, Schlachter -> 30, Träger -> 50



<b>Sonstiges</b>

<b>Ist Die Ewigen ein Kriegsspiel?</b>

Die Ewigen ist als Strategiespiel konzipiert, das viele Spielkomponenten enthalten soll. Kampf ist eine wichtige Komponente, aber DE soll kein reines Kriegsspiel darstellen.


<b>Was gilt in Die Ewigen als Beleidigung</b>

Generell alle Schimpfworte gelten als Beleidigung, dazu gehört auch das ehemals für Anfänger/Neuling stehende Noob, das inzwischen häufig als Beleidigung verwendet wird. Auch "Spaten" gilt als Beleidigung.
Nur weil hier weitere Worte nicht aufgezählt werden, heißt das nicht, dass sie erlaubt sind. Verhaltet euch gegenüber euren Mitspielern bitte möglichst freundlich.


<b>Wird es jemals die Möglichkeit geben, Ressourcen aus der Sektorkasse an einzelne Spieler zu verteilen?</b>

Nein, dies ist nicht geplant, da man ansonsten hiermit einzelne Spieler aus dem Sektor pushen könnte.


<b>Wie kann ich mit Freunden einen Sektor gründen?</b>

Diese Möglichkeit gibt es nicht auf allen Servern, sondern nur auf Servern auf denen die Unendlichkeitssphäre baubar ist.


<b>Wie kann man andere Planeten einnehmen?</b>

Dies ist in "Die Ewigen" nicht möglich und auch nicht in Planung und stimmt auch nicht mit dem Spielprinzip von DE überein.


<b>Als HC einer Allianz biete ich einer anderen Allianz ein Bündnis an, doch der Antrag kommt auf der anderen Seite nicht an. Warum?</b>

Bist du dir sicher, dass du der RICHTIGEN Allianz den Antrag gestellt hast? 
In der Vergangenheit kam es öfter vor, dass Allianzen ähnliche Namen (z.B.: invidia invedia incendi) hatten und die Anträge deswegen bei der falschen Allianz gelandet sind.


<b>Ist es möglich den Sektor zu wechseln? Wenn ja, wie?</b>

Dieses ist nur möglich indem man (unter Optionen) seinen Account löscht. 
Gesperrte Spieler können auch hinausgevotet werden.


