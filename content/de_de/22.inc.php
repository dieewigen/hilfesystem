<b>Wie gehe ich vor, wenn ich keine Aktivierungsmail erhalte?</b>

Wurde eine Aktivierungsmail nicht erhalten, so habt ihr die Möglichkeit, euch diese via "Passwort vergessen?" erneut zusenden zu lassen. Das Script erkennt, ob ein Account aktiviert wurde oder nicht. Im Falle der Nicktaktivierung bekommt der User nach Eintrag seiner Emailadresse oder Loginnamen, die Aktivierungsmail erneut zugesandt. 

Sollte dies keinen Erfolg bringen, so kann eine E-Mail an DE-Support (issomad@die-ewigen.com)geschrieben werden. 


Folgende Angaben solltest du in einer Anfragemail mitteilen: 

* Login Name und Spielername 

Wichtig! Diese Angaben müssen von der E-Mailadresse gesandt werden, die bei der Registrierung angegeben wurde.

Hinweis! Bitte keinesfalls einen erneuten Account registrieren. Dies verstößt gegen die Nutzungsbedingungen und hat eine Accountsperrung zur Folge.


<b>Wie kann ich Schreibfehler meiner Registrierungsdaten ändern?</b>

Registrierungsdaten können aus Sicherheitsgründen gegenüber den Usern und ihren Accounts nur durch einen Supporter vorgenommen werden. Schreibe dafür ein Ticket in der Accountverwaltung.

In dieser Mail bitte die richtigen Daten angeben. 

<b>Darf man Mehrfachregistrierungen z.B. durch WG, Ehepaare, Uni usw. vollziehen, oder wird man gesperrt?</b>

Dies ist durchaus möglich. 

Viele Spieler in "Die Ewigen" wohnen z.B. in WGs und teilen sich gemeinsam einen Rechner. Wir gehen niemals nur nach identischer IP. Es spielen immer noch mehrere Faktoren eine Rolle, die wir in Betracht ziehen. Kein User muss um seinen Account fürchten, weil noch andere User identische IP teilen.

Dieses muss allerdings dem DE-Support unter issomad@die-ewigen.com mitgeteilt werden. Erforderliche Angaben dazu sind u.a. die Account-IDs (zu finden Ingame unter Übersicht in der Tabelle Systemübersicht) der betroffenen Accounts.

<b>Ich kann die 5. Rasse nicht auswählen, warum?</b>

Diese ist nur der Alienrasse vorenthalten, welche nicht ausgewählt werden kann und nur durch die Alien-KI gesteuert wird.

<b>Weshalb kann ich nach der Aktivierung nicht in meinen Account einloggen?</b>

Die Cookies müssen aktiviert sein. 

<b>Ich habe meine Zugangsdaten vergessen, wie erhalte ich diese?</b>

Auf der Login Seite findet ihr unter den Eingabefeldern der Zugangsdaten den Link "Passwort vergessen?". Mit Klick auf diesen Link gelangt ihr auf die Seite mit den Eingabemöglichkeiten. 

Ihr könnt entweder durch Eintrag eures "Loginnamen" oder eurer "Emailadresse" euch die Zugangsdaten zusenden lassen. Bitte beachtet die Hinweise über den Eingabefeldern. 

<b>Weshalb wurde mein Account gesperrt und an wen wende ich mich?</b>

Ein Account wird gesperrt, wenn ein User gegen die Nutzungsbedingungen verstoßen hat. Welche Gründe zu einer Sperrung geführt haben, kann vom zuständigen Supporter erläutert werden. 
Normalerweise sollte der zuständige Supporter bei der "Sperrmeldung" herausgeschrieben werden, sollte dies einmal nicht vorkommen bitte ein Ticket in der Accountverwaltung erstellen.

Alles Weitere ist dann durch den Supporter zu erfahren. 

<b>Weshalb kann ich nicht spielen und sehe eine Umfrage?</b>

Für das Spiel entscheidende Änderungen sollen ebenso von den Usern mitbestimmt werden können. 

Aus diesem Grund gibt es Ingame die Funktion für Umfragen. Jeder User ist verpflichtet seine Stimme nach bestem Gewissen abzugeben. Nur so kann man eine faire Auswertung erhalten. Wurde eine Umfrage durch das DET gestartet, so muss jeder User seine Stimme abgeben. Erst nachdem er dies getan hat, kann er das Spiel normal fortsetzen.

<b>Ich sehe keine Zahlen beim Login / die Aufgabe beim Login fehlt!</b>

Es kann sein, dass die Cookie-Einstellungen zu restriktiv eingestellt sind wodurch eine Anzeige verwehrt bleibt! 

Daher die Bitte, nochmal die Sicherheitseinstellungen des Browsers zu überprüfen, diese könnten evtl. zu hoch eingestellt sein.


<b>Was muss ich beachten, wenn ich meinen Account lösche?</b>

Ingame kann man im Menü unter der Rubrik "Optionen" seinen Account löschen. Hierfür muss das Passwort eingegeben werden und die Löschung durch zweimaliges Setzen der Häkchen bestätigen. Danach wird eine Frist angezeigt, wann der Account gelöscht wird. Via Email erhaltet ihr eine Bestätigung dieser Löschung. 

Solltest du mit dieser Löschung nicht einverstanden sein, reicht ein Einloggen in den Account innerhalb dieser Frist aus, um die Löschung zu deaktiviert.


<b>Wann wird mein Account durch Inaktivität aus dem Sektor entfernt?</b>

Ja nach Server ist die Frist unterschiedlich, beträgt aber meist 7 Tage.

<b>Weshalb kann ich die Registrierungsdaten nicht persönlich ändern?</b>

Dies ist bedingt für die Sicherheit eines jeden Accounts eingeschränkt. Auf diese Weise können Löschungen durch Hackaktionen unterbunden werden. Ebenso ist durch diese Einschränkung das Verschenken eines Accounts unterbunden. Die Registrierungsdaten werden (z.B.: bei einem Umzug) nur durch abgleichen der alten Registrierungsdaten geändert. 

Wichtig! Daten werden nur geändert, wenn eine Mail mit dem Änderungsantrag von der Emailadresse aus gesandt wurde, die Ingame bei der Registrierung angegeben wurde. 

