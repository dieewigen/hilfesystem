<b>Was hat es mit der dynamischen Wartezeit nach dem Voten eines Spielers aus dem Sektor auf sich?</b>
Da die Votefunktion weiterhin missbraucht wird um sich Wunschspieler in den Sektor zu holen, gibt es eine dynamische Wartezeit in der kein neuer Spieler nach einem Vote in den Sektor kommen kann.

Diese Sperre greift zusätzlich bei Spielern die ihren Account löschen, bzw. aufgrund von Inaktivität in Sektor 1 verschoben werden.


<b>Urlaubsmodus</b>

<b>Wie lange muss der Urlaubsmodus mindestens dauern, bevor man ihn abbrechen kann?</b>
Nach 3 Tagen (sofern nicht vorher anders festgelegt) kann man den Urlaubsmodus frühestens abbrechen.


<b>Wird ein Spieler, der mehr als eine Woche im Urlaubsmodus ist, in Sektor 1 verschoben?</b>
Nein! Der Urlaubsmodus ist dazu da, dass man nicht verschoben oder durch das Inaktivenscript gelöscht wird.


<b>Kostet das vorzeitige Abbrechen des Urlaubsmodus Credits?</b>
Der Urlaubsmodus kann innerhalb der ersten drei Tage nicht abgebrochen werden. Credits sind nur dann (im Voraus) zu entrichten, wenn der Urlaubsmodus kürzer als 3 Tage eingestellt wird. Die Gebühr dafür beträgt 150 Credits.

Die soll dazu dienen, dass der Urlaubsmodus nicht als taktisches Mittel eingesetzt wird.


<b>Löschmodus</b>

<b>Wie lange dauert es, bis ein Spieler, der sich im Löschmodus befindet, tatsächlich gelöscht wird?</b>
Der Spieler wird nach 3 Tagen (ohne erneuten Login) gelöscht.



