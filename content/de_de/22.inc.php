<b>Hintergrund</b>

<b>Was ist ein Kollektor?</b>

Über Kollektoren bekommt man Rohstoffe, je mehr Kollektoren, desto mehr Rohstoffe.

Diese werden mittels Ressourcenumwandler in eine brauchbare Form gebracht. 

Kollektoren kann man entweder selbst bauen oder mit Transmitterschiffen von einem anderen Mitspieler klauen. Mit viel Glück bekommt man auch über ein Ereignis Kollektoren "geschenkt". 


<b>Warum kann ich pro Flotte nur maximal 16.777.215 Schiffe jeder Art haben?</b>

Dies ist die Grenze des von uns verwendeten MySQL-Datentyps. 

Dies Grenze wird jedoch nur in der BattleRound erreicht und ist für den normalen Spielverlauf nebensächlich. 


<b>Weshalb kann man das Sektorbild trotz Eintragung des 
Links im SK-Menü nicht sehen?</b>

Befindet sich als Bild nur ein Kreuz, so ist die URL im SK-Menü fehlerhaft oder die Seite, auf der das Bild liegt, evt. zur Zeit nicht erreichbar. 

Auch könnte ein Nichtanzeigen des Sektorbildes durch "Sektorbild nicht anzeigen" im Optionenmenü eine Erklärung sein.


<b>Ich kann keine Bauaufträge mehr aufgeben, die Serverzeit bleibt gleich oder die Ressourcenanzeige verändert sich nicht mehr.</b>

In solchen Fällen erstmal den Cache/Temporäre Internetdateien im Browser leeren. 


