<b>Jägersupport</b>

Großkampfschiffe sind darauf ausgelegt, ihre Geschütze gegen andere große Schiffe zur Geltung zu bringen; sie haben jedoch Probleme gegen kleinere Schiffe und benötigen deshalb Schutz durch Jäger.

Kreuzer und Schlachtschiffe benötigen daher Support durch Jäger, um ihre Waffen zur vollen Geltung bringen zu können.
Ohne Jägersupport erbringt ein Großkampfschiff nur 50% seiner Waffenleistung!

Kreuzer benötigen dabei ungefähr 9 Jäger als Begleitschutz und Schlachtschiffe benötigen etwas mehr als 20 Jäger.
Das sind Richtwerte; die genauen Werte findet man im Menü "Flotte", im Abschnitt <b>Flottenaufstellung</b>, als Tooltip bei den Kreuzern und Schlachtschiffen.

Die unterstützenden Einheiten müssen sich dabei in der selben Flotte befinden. Schickt man z.B. zwei Flotten parallel, dann unterstützen sich diese nicht. Es wird daher eimpfohlen immer eine Flotte zu schicken in der sich alle Schiffe befinden.

Durch die Unterlegenheit ihrer Jäger benötigen Z'tah-ara gut die anderthalbfache Menge Jäger zur Deckung ihrer Großkampfschiffe, während die K'Tharr wegen ihrer stärkeren Jäger und der Verabscheuung unnötigen Schutzes deutlich weniger benötigen.
Die Ewigen und die Ishtar befinden sich um den Richtwert herum.

Fehlender Jägersupport hat jedoch keinen negativen Einfluss auf den Jagdbootsupport, der von den Großkampfschiffen geleistet wird.


