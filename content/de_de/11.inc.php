Als Cheaten bezeichnet man das Ausnutzen von Programmlücken oder Bugs. Zum Melden von Bugs ist jeder User verpflichtet. Nutzt er einen solchen aus, muss er mit dem Verlust seines Accounts rechnen.

Achtung: Das Spielen mit mehreren Accounts zählt unter Cheaten und wird keinesfalls geduldet. Diese Art bezeichnet man auch als Multi.

Die Nutzung von BOTS (Programme, die vorgegebene Befehle immer wieder aufrufen, ähnlich dem mIRC) fallen ebenfalls in den Bereich des Cheatens und sind nicht erlaubt.