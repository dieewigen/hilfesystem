<b>Galaxie</b>

In Die Ewigen unterteilt sich eine Galaxie in viele Sektoren. Jeder Sektor besteht aus 1 bis x Systemen. Jeder Spieler besitzt genau eines dieser Systeme, allerdings kann es vorkommen, dass nicht jedes System eines Sektors besetzt ist.

Der Zusammenhalt eines Sektors spielt eine wichtige Rolle im Spiel und sollte von jedem Sektormitglied gefördert und geachtet werden. Sektorinterne Angriffe, sind nicht möglich.

In der greifbaren Welt (RL) repräsentiert eine Galaxie ein abgeschlossenes Spielsystem. Es ist nicht möglich, innerhalb des Spiels etwas von einer Galaxie in die andere zu transferieren.

Jeder Spieler darf genau einen Account pro Galaxie/Server haben.