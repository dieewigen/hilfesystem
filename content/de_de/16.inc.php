<b>Deffbund</b>

Deffbund ist eine allgemeine Bezeichnung für ein Allianz- und sektorübergreifendes Bündnis von Spielern, die sich gegenseitig deffen.

Zum Beispiel kann ein Deffbund aus befreundeten Spielern bestehen, aus Mitgliedern eines ehemaligen Sektors oder einer ehemaligen Allianz, Spielern aus einer bestimmten Stadt oder Spielern, die eine bestimmte Spielweise bevorzugen.

Spieler, die andere Spieler deffen, ohne dass sie in deren Sektor oder in deren Allianz bzw. Meta sind, werden als <i>Fremddeffer</i> bezeichnet.
Fremddeffer stellen für Angreifer einen unberechenbaren Faktor dar.

Die Aktivitäten eines Deffbundes müssen sich natürlich nicht aufs Deffen beschränken.