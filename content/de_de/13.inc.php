<b>Zeitung erstellen:</b>

a. zur Verwaltung wechseln

b. auf "Neue Zeitung" klicken

c. nun geben wir einen Name für unsere Zeitung an. Dieser sollte aussagekräftig sein und unsere Ziele darlegen

d. wenn ein Logo vorhanden ist (auf Größeachten) wird dieses angegeben

e. noch einen Herausgebernick wählen, oder den Ingamenick nutzen


<b>Eine Ausgabe für die Zeitung erstellen:</b>

a. zur Verwaltung wechseln

b. eigene Zeitung auswählen

c. auf "Neue Ausgabe" klicken

d. nun einen geeigneten Titel für die Ausgabe eingeben 

e. einen gewünschten Preis angeben 

f. den Text für die Ausgabe eingeben


<b>Formatierung eines Textes:</b>

a. [ BR ]

Gibt einen Zeilenumbruch an. Ein normles ENTER wird nicht als Zeilenumbruch angesehen. Daher bitte darauf achten, das dies korrekt formatiert wird.


b. [ CENTER ]

Durch diesen TAG wird der Text zwischen center und /center zentriert dargestellt.


c. [ BLOCKTEXT ]

Der Text zwischen blocktext und /blocktext wird, wie der Name schon sagt, als Blocktext formatiert, das heißt jede Zeile ist gleich lang.


d. [ TABLE ]

Hiermit kann eine Tabelle erstellt werden. Der Aufbau ist wie im HTML geregelt, das heißt eine Tabelle besteht aus Zeilen TR und Spalten TD.
Ein Beispiel für eine Tabelle mit einer Zeile und 2 Spalten:

[ TABLE ]
[ TR ]
[ TD ] Text Spalte 1 [ /TD ]
[ TD ] Text Spalte 2 [ /TD ]
[ /TR ]
[ /TABLE ]


e. [ B ]

Der Text zwischen b und /b wird fett dargestellt.


f. [ U ]

Der Text zwischen u und /u wird unterstrichen dargestellt.


g. [ I ]

Der Text zwischen i und /i wird kursiv (schräg gestellt) dargestellt.


h. [ COLOR=#RRGGBB ]

Mit color kann man die Farbe eines Textes angeben. Die Angabe erfolgt als HTML Farbcode, das heißt im Format #RRGGBB wobei RR = der Hex Farbwert für rot, GG für grün und BB für blau ist.
Ein Text in rot würde dann so aussehen: [ COLOR=#FF0000 ] roter Text [ /COLOR ]


i. [ SIZE=x ]

Gibt die Größe der Schriftart an. Diese wird in pt = Punkt angegeben und ist die gleiche Größe, wie sie in einem Schreibprogramm ist.
Ein normalgroßer Text wäre dann hier: [ SIZE=10 ] normaler Text [ /SIZE ]


j. [ URL ]

Hiermit kann ein Link eingefügt werden. Die formatierung wird hier automatisch vorgenommen, wenn man die Daten über den Button eingibt.


<b>Wie sollte eine Zeitung aussehen?</b>

1. Ausmaß einer Zeitung:

Eine Zeitung sollte mindestens eine Seite Text (Bildschirmhöhe 1024x768) aufweisen können und dies bei einer Standardschriftgröße von 10. Zeitungen die dies nicht aufweisen, werden von unserer Redaktion zu 95% nicht in das Sortiment aufgenommen.


2. Inhalt einer Zeitung:

Der Inhalt einer Zeitung sollte an unsere Galaxie gebunden sein und Spielinterne News berichten. Dies können normale RPG, Satiere, Kritische Berichte oder einfach Hilfestellungen sein.
Auch Zeitungen, die sich mit den RL News beschäftigen können unter umständen veröffentlicht werden, solange dies im Rahmen bleibt.


3. Gestaltung der Zeitung:

Eine Zeitung sollte so gestaltet sein, das sie für die Leser übersichtlich und leicht verständlich ist, das heißt gut gesetzte Absätze, hervorgehobene Überschriften und eventuell farbliche Hervorhebung bestimmter Elemente.
Zeitungen, welche keine Formatierung haben, also der Text einfach hintereinander zusammengesetzt ist, werden von unserer Redaktion meist abgelehnt, da diese dem Leser nicht zumutbar sind.


4. Preis einer Zeitung:

Der Preis für eine Zeitung sollte immer so angesetzt werden, das man ein Verhältnis zwischen Inhaltlichem Interesse ausgedrückt durch das Topic und der Notwendigkeit eines Preises finden sollte.
Für unseren Newsletter wäre ein Preis z.B. unangebracht, wobei bei einer gut ausgearbeiteten Zeitung schon der Höchstpreis verlangt werden kann.
Man sollte so z.B. seine erste Ausgabe mit einem sehr niedriegen oder garkeinem Preis ausstatten, um dem Leser zu zeigen, was man zu bieten hat und das interesse an folgenden Ausgaben zu wecken.


<b>Wie nehme ich Kontakt mit einem Herausgeber auf?</b>

Das ganze ist im Grunde recht einfach. Man muß nur auf der Übersicht auf den Herausgeber klicken und schon landet man auf unserem Nachrichtensystem über welches man dem Herausgeber eine Hyperfunk zukommen lassen kann.

Es gibt ein paar kleine Sachen zu beachten:

Es werden keine Koordinaten des Herausgebers angezeigt, um dessen Identität zu wahren. Der Herausgeber einer Zeitung bekommt die HF wie eine normale HF und sieht die Koordinaten und den Nick des Absenders. Sollte der Herausgeber nun auf diese HF antworten, so wird diese nun auch als normale HF übertragen, also seine Koords sind dann auch für den Absender sichtbar.
