<b>Optionen</b>

Menü: "Optionen"

In den Optionen kann man seine Userdetails und das Grafikpaket einrichten, das Zugangspasswort ändern, seinen Account selbst löschen oder in den Urlaubsmodus versetzen.


<b>Abschnitte :</b>

<b>Informationen / Premiumaccount buchen</b>

Auto-Logout
Gibt die Zeit an, die bis zum automatischen Ausloggen/Botcheck aus dem Spiel verbleibt. Je nach Server unterschiedlich, wird in festgelegten Zeitabständen ein Fenster angezeigt, in dem eine mit Zufallsgenerator erzeugte undeutliche Zahl zwischen 1 und 100 in Worten angezeigt wird. Diese sogenannte Bot-Schutz-Abfrage hat den Zweck, zu überprüfen, ob wirklich ein Mensch am Rechner sitzt, damit man nicht mittels programmierter Routinen cheaten kann. Man muss diese Zahl in dem darunter befindlichen Feld anklicken, dann kann man weiterspielen.

Premiumaccount-Laufzeit und
Premiumaccount für 5 Credits pro Tag buchen (gilt für alle DE/EA-Server):
Hier wird angezeigt, ob ein Premiumaccount (PA) besteht, bzw. hier kann man einen PA einrichten.
Die Vorteile, die ein PA bringt, sind unter <a href="https://help.bgam.es/index.php?thread=de_de&post=44">Premiumaccount</a> nachzulesen. Beachte, dass es bei regelmäßigen On-Zeiten pro Server Creditgeschenke gibt - und das pro Server, wogegen ein eingerichteter PA für alle Server gilt.


<b>Userdetails</b>

Ein Klick auf die Schaltfläche "Userdetails" öffnet eine Unterseite, in der man beliebige Informationen eingeben kann, z.B. Onlinezeiten, Kontaktmöglichkeit für andere Spieler, oder was auch immer.
Es gibt drei Fenster, in die man Texte eintragen kann: eines, das für alle Spieler sichtbar ist, eines nur für den Sektor und eines nur für die Allianz. Hier kann man unterschiedliche oder auch dreimal den gleichen Text eintragen. Lediglich copyrightgeschütztes Material, HTML-Codes oder irgendwelche Skripte und diskriminierende, pornografische oder geschmacklose Inhalte sind verboten. Es sind jeweils maximal 10.000 Zeichen erlaubt. 
Hat man alle Daten korrekt und zu seiner Zufriedenheit eingetragen, klickt man unten auf die Schaltfläche "Details ändern".
Hier gibt es keinen Zurück-Button, deshalb muss man ein beliebiges anderes Menü aufrufen, um von hier wieder weg zu kommen. 
Wie das Ergebnis aussieht, kann man überprüfen, indem man in die Sektoransicht geht (Menü: "Sektor") und dort auf den eigenen Nick klickt.
Durch einen Klick auf einen Spielernamen in einem Sektor wechselt man in ein Menü, in dem man dem betreffenden User ein HFN schreiben kann, und unterhalb davon sieht man die von diesem Spieler eingegebenen Userdetails. Bei anderen Spielern werden deren Userdetails angezeigt, beim eigenen Namen die eigenen.
Gegebenenfalls muss man erneut in das Optionen-Menü gehen, falls man weitere Änderungen vornehmen möchte.


<b>Desktopversion oder mobile Version / Wischgesten-Mobilversion</b>

Hier kann man umschalten, ob einem die Version für Rechner oder die Version für Handys angezeigt werden soll. DE erkennt dank einer Drittanbieter-Bibliothek, von welcher Geräteart es aufgerufen wird, und die benötigte Version wird automatisch eingestellt.
Beachte: Falls man die Version umstellt, wird sie erst nach dem nächsten Login wirksam.

Die Wischgesten bei der mobilen Version lassen sich ebenfalls aktivieren oder deaktivieren. Allerdings macht es ohnehin keinen Unterschied aus, ob man in der mobilen Version die Top-Leiste antippt oder auf ihr nach rechts wischt: Mit beiden Methoden kommt man in das Menü der mobilen Version.
In der Topleiste der mobilen Version nach links zu wischen, bringt einen in den Chat. Bei deaktivierten Wischgesten braucht man nur durch Antippen in das Menü zu gehen und dort den obersten Menüpunkt "Chat" auszuwählen.


<b>Allgemeine Einstellungen</b>

Grafikpackpfad:
Dieser Punkt erlaubt die Festlegung eines eigenen Grafikpacks. Lokale Grafikpakete erhöhen die Ladegeschwindigkeit des Spiels je nach Internetzugangsgeschwindigkeit enorm. Alternative Grafikpacks wurden früher von dem Spieler erstellt, DE sah dann einfach anders aus, aber diese Möglichkeit wird heutzutage nicht mehr wirklich benutzt.

Sektorbilder:
Der Sektorkommandant kann im SK-Menü (Menü: "Sektorpolitik / SK-Menü", wird aber nur dem Sektorkommandanten angezeigt) seinem Sektor einen Namen und ein Bild zuordnen, die allen Spielern bei der Sektoransicht (Menü: "Sektor") angezeigt werden. Hier kann man einstellen, ob das Sektorbild oberhalb oder unterhalb der Sektorinformationen angezeigt werden soll, oder gar nicht.

Desktopversion:
Die mobile Version sieht immer gleich aus, doch die Desktop-Variante hat zwei Versionen: die aktuelle Version (Standard) und die Retro-Version (Classic) für die älteren Spieler, die sich nicht an Änderungen gewöhnen können und die alte DE-Oberfläche bevorzugen.
Beachte aber, dass in der Version "Classic" die Sternenkarte der Vergessenen Systeme nicht angezeigt wird. (Für die mobile Version ist die Sternenkarte nicht geeignet, deshalb wird sie dort grundsätzlich nicht angezeigt.)
Auch hier wirkt sich eine Umstellung erst nach dem nächsten Login aus.

Chat deaktivieren (betrifft nicht EFTA/EA):
Mit dieser Funktion lässt sich der gesamte Chat aus- und einschalten. Verständigung mit den anderen Sektor- und Allianzmitgliedern ist dann nur noch über HFN möglich. Diese Einstellung wirkt sich <u>nicht</u> auf den Chat in der EA und ebenfalls nicht auf den EFTA-Chat aus. Diese sind nicht deaktivierbar.
Vorsicht: Dass der Chat deaktiviert wurde, wird den anderen Spielern im Sektor angezeigt (Menü: "Sektorpolitik", Abschnitt: "Spielerinformationen"); die Ablehnung der sektorinternen Kommunikation kann zum Unmut bei einigen Sektormitgliedern führen und sich auf deren Verteidigungsbereitschaft im Falle eines Angriffs auswirken.

Server-Chat-Channel deaktivieren und
globaler Chat-Channel deaktivieren:
Hier lassen sich der Serverchat (blau) und der Globale Chat (braun) deaktivieren, danach sind nur noch der Sektorchat (weiß) und der Allianzchat (grün) sichtbar. 

Berater aktivieren:
Hier kann man den DE-eigenen Berater <i>Fluxurion</i> aktivieren bzw. deaktivieren. Fluxurion hilft Anfängern bei den ersten Schritten in DE. Er erklärt die Benutzeroberfläche, die Funktionen und gibt Tipps, was zu bauen und zu forschen ist.
Beachte aber, dass selbst die besten Tipps von Fluxurion nicht vor Angriffen durch andere Spieler schützen, sobald man sich nicht mehr in Sektor 1 befindet.

Missionshilfe aktivieren:
Hier kann man einstellen, dass eine Meldung angezeigt wird, wenn es eine oder mehrere abgeschlossene Missionen gibt (Menü: "Missionen") und man somit eine neue Mission starten könnte. Diese Meldung erscheint oberhalb des jeweils aufgerufenen Menüs, gleich unterhalb der Rohstoffleiste. Diese Meldung bietet auch gleich einen Link in das Missionsmenü an.

IRC-Name:
Der IRC-Name dient dazu den Namen für den externen IRC-Service festzulegen.
Da IRC in DE kaum mehr verwendet wird, ist dieser Eintrag nur von sekundärer Bedeutung.

Schwarzmarkt-Reminder:
Hier lässt sich eine Meldung ein- und ausschalten, die darüber informiert, wenn Produkte im Schwarzmarkt (Menü: "Schwarzmarkt") freigeschaltet sind. Der kleinste einstellbare Wert, wann diese Meldung wieder angezeigt werden soll, ist 15 Minuten. Bei 0 wird der Reminder deaktiviert. Diese Meldung erscheint oberhalb des Menüs links, zählt detailliert auf, welche Produkte im Schwarzmarkt erworben werden können und bietet zugleich einen Link zum Schwarzmarkt an.

Einstellungen speichern:
Sämtliche Änderungen im Abschnitt Allgemeine Einstellungen werden nur übernommen, wenn sie abschließend durch Klick auf die Schaltfläche "Einstellungen speichern" bestätigt werden.

 
<b>Einstellungen für die nächste Runde</b>

Rasse und
Spielername:
In DE ist es möglich, nach jeder Runde seinen Nick und seine Rasse zu ändern (während der Runde ist dies nicht möglich, doch hier kann man gleich nach Rundenbeginn die entsprechende Voreinstellung treffen). Die Länge von einstellbaren Spielernamen ist begrenzt, und es sind nur Buchstaben und keine Zahlen erlaubt. An ASCII-Zeichen sind nur _ (Unterstrich), - (Bindestrich) und = (Gleichheitszeichen) erlaubt; zudem können manche Server keine Umlaute darstellen. 

Daten speichern:
Im Falle einer ungültigen Eingabe eines Spielernamens erhält man beim Anklicken der Schaltfläche "Daten speichern" eine Fehlermeldung.


<b>Account löschen</b>
Im Prinzip wird hier alles bereits ingame in diesem Abschnitt erklärt. 
Natürlich kann auch in DE jeder User freiwillig entscheiden, ob und wann er seinen Account nicht mehr nutzen möchte. Hierfür besteht an dieser Stelle die Möglichkeit, diesen zu löschen. Man sollte dies auch immer machen, wenn man kein Interesse mehr am Spiel hat, da es verboten ist, einen Account zu verschenken/verleihen.
Der Account wird für 3 Tage in den Löschmodus versetzt und anschließend durch das Script gelöscht. Binnen dieser Zeit hat man die Chance sich wieder einzuloggen und damit die Löschung zu stoppen.
Der Löschvorgang muss mit dem Passwort und mit <i>zwei</i> angehakten Kontrollkästchen bestätigt werden. Danach muss nochmals durch Anklicken der Schaltfläche "Account löschen" alles bestätigt werden.
Dieser Löschvorgang ist nicht ist dem Inaktivenscript zu verwechseln: Loggt sich ein Spieler für längere Zeit nicht bei DE ein, so wird er in Sektor 1 versetzt, sein Spieleraccount und sein Serveraccount bleiben jedoch erhalten. Er wird in einen Sondermodus versetzt und der Serveraccount wird nach 5000 Tagen gelöscht.


<b>Urlaubsmodus</b>
Der Urlaubsmodus in DE bewirkt, dass man nicht angegriffen werden kann, allerdings steht auch die Energiegewinnung still: Kollektoren und Kriegsartefakte produzieren keine Energie, auch der Planetare Rohstoffertrag steht auf 0.
Auch hier wird ingame in diesem Abschnitt bereits das wesentliche erklärt: Der Urlaubsmodus kann für mindestens 1 und maximal 21 Tage eingestellt werden, ein Urlaubsmodus von bis zu drei Tagen kostet Credits, danach ist er kostenlos. 
Sämtliche Forschungs- und/oder Bauaufträge, die bereits in Auftrag gegeben wurden, werden noch zu Ende geführt. Das gleiche gilt für bevorstehende Kämpfe. Man kann also keine laufenden Angriffe durch Aktivierung des Urlaubsmodus verhindern. Ist der Urlaubsmodus aktiviert, so erscheint im Sektor dessen Planetennummer in Klammern.
Der Urlaubsmodus kann vorzeitig abgebrochen werden, allerdings beträgt die Mindestlaufzeit 3 Tage. Der Abbruch erfolgt, indem man sich in den Account einloggt. Sollten noch nicht 3 Tage vergangen sein, so wird die maximale Urlaubsdauer und der frühestmögliche Abbruchtermin angezeigt.
Ein "Freikaufen" ist vor Ablauf von 3 Tagen nicht möglich.



