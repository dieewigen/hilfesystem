<b>Sektorkampf</b>

Der Sektorkampf gilt nur für gesamte Sektoren, nicht für einzelne Systeme.

Der Sinn des Sektorkampfes besteht im Erobern der Sektorartefakte anderer Sektoren mittels der Sektorflotte.

Die Flugdauer für die Sektorflotte  beträgt 12 Kampfticks. Man kann andere Sektoren angreifen oder auch für 1-3 "Wochen" verteidigen (1 Woche bedeutet 1 KT).
Es können nur Sektoren angegriffen werden, die eine Sektorraumbasis besitzen. (Anmerkung: Sektorartefakte erscheinen ebenfalls nur in Sektoren, die eine Sektorraumbasis haben).

Man kann pro Angriff nur ein Artefakt erobern. Greifen mehrere Sektoren einen anderen Sektor an, so erhält der Sektor mit größerer Sektorflotte das Artefakt. Im Falle gleichstarker Sektorflotte (wenn alle Sektorflotten mit gleicher Schiffsanzahl fliegen), so wird durch Zufall bestimmt, wer bei Erfolg das Artefakt erhält. Je grösser die Übermacht der Angreiferflotte(n), umso grösser die Wahrscheinlichkeit auf Erfolg.
Ab dem vierfachen Wert einer Sektorflotte gegenüber der anderen wird die schwächere Sektorflotte genullt.

DE-technischer Hintergrund:
Die Sektorschiffe regen durch eine auf das Artefakt abgestimmte Strahlung dieses an, einen Hyperraumsprung auszuführen, daher ist es nicht notwendig, dass man im Sektorkampf die Oberhand behält, sondern nur lange genug durchhält, bis das Artefakt springt.

Formel zur Berechnung der Wahrscheinlichkeit, dass man ein Artefakt erobert:
Angreifer / Verteidiger * 100 = Wahrscheinlichkeit

In der Regel kommt bzw. bleibt daher das Sektorartefakt zu/bei jenem Sektor, der den Sektorkampf gewinnt, doch besteht eine gewisse Wahrscheinlichkeit, dass der Angreifer es bekommt, auch wenn er den Kampf verliert; dies dient dazu, dass auch unterlegene Sektoren eine Chance haben, Sektorartefakte zu erobern.

Wird das Sektorartefakt erobert, so wird es sofort nach Einschlag zum Sektor transferiert – die Sektorflotte hat jedoch eine Rückkehrzeit von 12 KT, bevor sie wieder zur Verfügung steht.

Im Sektorstatus werden Angriffe oder Verteidigungen auf die Sektorraumbasis angezeigt. Die Nummern der Sektoren werden in eckigen Klammern angegeben.