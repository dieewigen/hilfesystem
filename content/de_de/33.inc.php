<b>Jagdbootsupport</b>

Jagdboote benötigen, um ihre volle Effektivität zu erreichen, Unterstützung von größeren Schiffen. Eine kleinere Menge von Jagdbooten kann sich jedoch noch immer selbst gut genug organisieren. Jagdboote brauchen ab 500 Schiffen die Unterstützung durch Kreuzer, Schlachtschiffe oder Trägerschiffe.

Die Effektivität der Jagdboote ist daher abhängig davon, wie stark sie unterstützt werden. 500 Jagdboote haben volle Funktion, die restlichen Schiffe müssen von den Großschiffen unterstützt werden. Kreuzer unterstützen ungefähr 10 Jagdboote, Schlachtschiffe ungefähr 20 Jagdboote und Träger schließlich 35 Jagdboote.
Das sind Richtwerte; die genauen Werte findet man im Menü "Flotte", im Abschnitt <b>Flottenaufstellung</b>, als Tooltip bei den Jagdbooten.

Nicht unterstützte Jagdboote haben nur halbe Feuerkraft und halbe Lähmkraft.

Achtung: Kreuzer und Träger unterstützen Jagdboote nur zu 80 bzw. 90 Prozent. Ein vollständiger Support kann nur mit Schlachtschiffen erreicht werden. Es werden dabei umso mehr Schlachtschiffe nötig, je mehr Jagdboote sich in der Flotte befinden. 

Die unterstützenden Einheiten müssen sich dabei in der selben Flotte befinden. Schickt man z.B. zwei Flotten parallel, dann unterstützen sich diese nicht. Es wird daher eimpfohlen immer eine Flotte zu schicken in der sich alle Schiffe befinden.

Die Unterstützungswerte durch die Großkampfschiffe weichen je nach Rasse vom Grundwert ab. 
Dabei unterstützen die Z'tah-ara ihre Jagdboote am besten, während die K'Tharr sie am schlechtesten unterstützen können. 
Die Ewigen befinden sich nahe dem Grundwert, die Ishtar leicht darüber.

Auch wenn Großkampfschiffe im Zuge eines Gefechts durch feindliche Einheiten geblockt werden, so wirkt sich das nur auf ihre konventionellen Waffen aus; die Qualität des Jagdbootsupports ist davon nicht betroffen.


