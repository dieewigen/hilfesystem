<b>IRC</b>

IRC steht für Internet Relay Chat. Es ist eine große Chat-Plattform über die ein großer Teil von DE abläuft.
Fast alle Sektoren haben ihren eigenen channel, ebenso Allianzen und Metaallianzen. Dort können schnell wichtige Dinge besprochen werden (z.B. die Organisation von atts und deffs) oder auch einfach je nach Lust und Laune geplaudert, zusammen gelacht oder diskutiert werden.
Ein häufig benutzter IRC-Client (ein IRC-Programm) ist mIRC. Eine gute Liste von IRC-Clients findet sich in der englischen Wikipedia.
Es gibt für DE auch einen Webservice, dieses ist im Menü unter dem Punkt Community-IRC zu finden.
Es empfiehlt sich jedoch die Benutzung eines Stand-Alone-Clients.

In den letzten Jahren wurde IRC jedoch aufgrund der einfacher zu handhabenden Alternativen immer seltener in DE verwendet.

<b>Chat:</b>
Da es den DE-eigenen Chat gibt, kann die Verständigung der Spieler untereinander auch ohne IRC erfolgen. Es gibt jeweils einen eigenen Chat für den Sektor, für die Allianz, für den Server (RDE, SDE, xDE, EDE, CDE, DDE) und seit 2015 sogar einen serverübergreifenden globalen Chat.
Ein persönlicher Chat zwischen zwei oder mehreren Spielern ist jedoch nicht möglich.

<b>Meldung an die Allianz des Angriffsziels:</b>
Im Menü "Sektorstatus" haben die Spieler im Sektor die Möglichkeit, im Falle eines Angriffs, durch Anklicken der Buchstaben AI im Abschnitt <b>Systemstatus</b>, der Allianz des Angegriffenen diesen Angriff auf ihr Sektormitglied zu melden; diese Meldung erscheint im Allianzchat des angegriffenen Spielers in roter Schrift. Diese Funktion ermöglicht zudem allen Mitgliedern der Allianz des Angegriffenen für beschränkte Zeit eine Einsicht in den Sektorstatus.

<b>Discord:</b>
Seit 2018 gibt es auch einen DE-Discord-Server.
Zugang über Menü: "Community / DE-Discord".

Sonstiges:
Und nicht zuletzt ermöglichen Kommunikationsprogramme auf Mobiltelefonen (z.B. WhatsApp)eine raschere, verlässlichere und einfachere Kommunikation und Vernetzung der Spieler untereinander.