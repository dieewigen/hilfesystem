<b>Technologien - Forschungen</b>

Die Forschungsprojekte sind ein Teil der <i>Technologien</i> und sind Voraussetzung für weitere Forschungen, Gebäude und Einheiten.

Eine Aufstellung über alle Technologien kann im Spiel unter dem Menüpunkt "Technologien" abgerufen werden. Die Technologien beinhalten <i>Gebäude</i>, <i>Forschungen</i>, <i>Basisschiffe</i> und <i>V-Systeme</i>.
Eine Aufstellung über alle Forschungen kann im Spiel unter dem Menüpunkt "Technologien / Forschungen" abgerufen werden, wobei man den Filter aktiviert, der nur die Forschungen innerhalb der Technologien darstellt.

Links oben im Technologien-Menü ist eine Schaltfläche in Form eines Zahnrades, die ein Pop-up-Menü öffnet; hier kann man die Darstellung der Technologien so einstellen, dass einem nur die noch zu erledigenden Technologien angezeigt werden, bzw. dass die bereits erledigten Technologien ausgeblendet werden.
Für bessere Darstellung auf dem Handy kann man die Technologien auch nur einspaltig darstellen lassen.
Kosten, Voraussetzungen, Dauer und Beschreibung: Ebenso kann man einstellen, dass die Kosten, die Voraussetzungen bzw. eine Beschreibung dargestellt oder als Tooltip angezeigt werden. Die Dauer wird immer angezeigt und lässt sich nicht ausblenden.
Und zuletzt kann man ein akustisches Signal einstellen, das meldet, wenn eine Technologie abgeschlossen ist.
Die Änderungen werden aktuell, sobald man das Menü Technologien das nächste mal aufruft.

Mit den Schaltflächen "alle", "Gebäude", "Forschungen", "Basisschiffe" und "V-Systeme" aktiviert man einen Filter, der wahlweise nur einen bestimmten Teil der Technologien anzeigt, oder alle.

Durch Anklicken des Feldes startet man die jeweilige Forschung. Die  benötigte Zeit zur Fertigstellung wird danach angezeigt. Es ist möglich, Technologien abzubrechen, wobei die Kosten rückerstattet werden.
Stehen für die Forschung unzureichende Ressourcen zur Verfügung wird dies durch die Meldung " Es sind nicht alle benötigten Rohstoffe/Voraussetzungen vorhanden " gemeldet.


