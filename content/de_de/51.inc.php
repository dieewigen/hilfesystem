<b>Runde</b>

Die Ewigen ist kein Endlosspiel, sondern besteht auch mehreren nacheinander kommenden Spielrunden. Die einzige Ausnahme davon ist EDE, das als Endlosrunde konzipiert ist.

Für alle anderen Server gilt:
Die Runde ist zu Ende wenn ein Spieler gewonnen hat und sich den Titel <i>Der Erhabene</i> verdient hat.

Auf den Servern SDE und xDE wird zwischen Rundenende und Beginn der nächsten Runde noch eine mehrtägige <b>Battleround</b> (BR) angeboten.
Für die Battleround erhält jeder Spieler, der nicht in Sektor 1 ist, 10.000 Kollektoren zu denen, die er hat, dazu, weiters 9.000.000.000 Multiplex, 4.500.000.000 Dyharra, 1.000.000.000 Iradium, 500.000.000 Eternium und 100.000 Tronic, alles ebenfalls zusätzlich zu den bereits vorhandenen Rohstoffmengen.
Die Energievorräte sind somit so gut wie unbegrenzt. Man kann jede Menge Einheiten bauen.
Die Battleround ist vergleichbar mit einem Autocrash-Rennen: Es soll in der BR nicht mehr darum gehen, noch mehr Kollektoren anzuhäufen, sondern Einheiten zu schrotten.
Am Ende der BR gibt es keinen Sieger, es soll der Spaß am Crashen vorrangig sein. Die BR eignet sich auch gut dazu, das Kampfverhalten bestimmter Einheitentypen zu testen.
Die BR endet kurz vor Beginn der nächsten Runde, wenn alle Ticks stehen bleiben.

Mit Beginn der neuen Runde werden alle Werte bis auf den Premiumaccountstatus, die Rundenpunkte und die Credits zurückgesetzt, die getroffenen Voreinstellungen bezüglich Spielername und Rasse werden aktualisiert, und jeder Spieler startet mit einem leeren Planeten.
In SDE und xDE gleich in einem Spielersektor, in RDE in Sektor 1, aus dem man erst herauskommt, wenn man mindestens 10 Kollektoren gebaut hat, oder sobald man 5.000.000 Punkte erreicht.

In RDE beginnt die nächste Runde automatisch nach dem Sieg des Erhabenen; hier gibt es keine BR.

CDE ist wie eine Dauer-BR zu betrachten. Nach dem Reset nach dem Erhabenensieg beginnt man gleich mit 10.000 Kollektoren, 9.000.000.000 Multiplex, 4.500.000.000 Dyharra, 1.000.000.000 Iradium, 500.000.000 Eternium und 100.000 Tronic von neuem.


