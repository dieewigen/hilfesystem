<b>Zufallsereignis</b>

Ins Spiel sind sog. Zufallsereignisse eingebaut.

Das kann ein Komet sein, der voll mit Tronic auf deinem Planeten landet und dir einen reichen Ressourcen-Regen beschert, das kann aber auch eine Piratenattacke sein, die dir ein paar deiner wertvollen Kollektoren klaut.
Nein, nicht erschrecken, das ist nur ein Scherz. Es gibt keine Zufallsereignisse, bei denen man etwas verliert. Sämtliche Zufallsereignisse bestehen ausschließlich aus Geschenken: meist Tronic, aber selten auch Kollektoren, Schiffe, Türme, Agenten oder Sonden. 
Es werden jedoch niemals Artefakte, Kriegsartefakte oder Palenium verschenkt; ebenso gibt es keine Kollektorengeschenke, solange man in Sektor 1 ist.

Natürlich heißen sie absichtlich Zufallsereignisse, denn der Server berechnet sie per Zufall, und treffen können sie jeden zu jeder Zeit. Auch mehrmals, ist doch gut bei Tronic-Regen!
Und die Meldungen sind amüsant formuliert, sie sollen auch ein bisschen unterhalten.

Allerdings beschränken sie sich nur auf den Planeten, nicht auch auf Schiffe oder Kämpfe. Es kann also nicht passieren, dass bei einem Kampf ein Zufall eintritt und ein Spieler trotz totaler Unterlegenheit gewinnt.