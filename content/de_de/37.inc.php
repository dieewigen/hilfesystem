<b>Kollektoren</b>

liefern die notwendige Energie zur Rohstoffumwandlung. Um diesen Energiegewinn zu erhöhen, baut man Kollektoren. Je nach Anzahl dieser Kollektoren, erhöht sich die erzeugte Energie.

Die Energieverteilung auf die einzelnen Rohstoffe wird prozentual (von der Gesamtenergie) festgelegt. Hierbei sollte immer darauf geachtet werden, dass diese 100% nicht über- bzw. unterschritten werden. In diesem Fall erhält man eine Fehlermeldung.


Je mehr Kollektoren man hat, umso höher ist ihre Punktebewertung. Detailliert sieht dieses wie folgt aus:

