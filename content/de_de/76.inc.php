<b>Battleround (BR)</b>

Auf den Servern SDE und xDE wird zwischen Rundenende (Erhabenensieg) und Beginn der nächsten Runde noch eine mehrtägige Battleround (BR) angeboten.
In RDE beginnt die neue Runde automatisch unmittelbar nach dem Sieg des Erhabenen;
in EDE gibt es kein Rundenende und damit keinen nächsten Rundenbeginn;
DDE hat als Testserver keine BR, hier wird nach dem EH-Sieg der Beginn der nächsten Runde angekündigt und freigeschaltet;
und  auf CDE können die Spieler einstellen, ob sie eine normale Runde oder eine Battleround spielen wollen: hier läuft seit Jahren dauernd eine Battleround.

Für die Battleround erhält jeder Spieler, der nicht in Sektor 1 ist, 10.000 Kollektoren zu denen, die er hat, dazu; weiters 9.000.000.000 Multiplex, 4.500.000.000 Dyharra, 1.000.000.000 Iradium, 500.000.000 Eternium und 100.000 Tronic, alles ebenfalls zusätzlich zu den bereits vorhandenen Rohstoffmengen.
Der Planetare Rohstoffertrag erhöht sich während der BR auf 800.000 M, 100.000 D, 40.000 I und 20.000 E pro WT (das ist das 200-fache der normalen Werte).

Außerdem werden alle Technologien freigeschaltet, die noch ausständig sind, und das Artefaktgebäude wird auf 20 Artefaktplätze erweitert, falls es diese noch nicht hat.

Die Ticks laufen 3 min WT und 12 min KT.

Die Battleround ist vergleichbar mit einem Autocrash-Rennen: Es soll in der BR nicht mehr darum gehen, noch mehr Kollektoren anzuhäufen, sondern Einheiten zu schrotten.

Die BR endet kurz vor Beginn der nächsten Runde, wenn alle Ticks stehen bleiben.
Am Ende der BR gibt es keinen Sieger, es soll der Spaß am Crashen vorrangig sein.

Die BR eignet sich auch gut dazu, das Kampfverhalten bestimmter Einheitentypen zu testen.


<b>CDE</b>
Eine Ausnahme stellt die BR in CDE dar:
Hier gibt es einen Erhabenenkampf und einen Erhabenen am Ende der BR.
Im Grunde handelt es sich um eine normale Runde, aber es wird unter BR-Bedingungen gespielt. Die Startwerte zu Rundenbeginn (Ressourcen, Kollektoren und Planetarer Rohstoffertrag) jedes Spielers entsprechen denen einer BR, ebenso sind alle Technologien bereits freigeschaltet. Das Artefaktgebäude ist voll ausgebaut (allerdings leer).

Da die Community auch die Tickzeiten festlegen kann, sind auch diese abweichend von denen der BR in SDE und xDE. (Seit Jahren ist die Einstellung WT 1 min, KT 3 min unverändert).
Auch die Rundendauer kann von der Community voreingestellt werden (zwischen 5.000 und 70.000 WT), ebenso die Haltezeit für den EH (zwischen 480 und 2.880 WT).
Theoretisch sind alle Spieler bei Rundenbeginn in Sektor 1, aber sobald man on kommt, wird man in einen zufälligen Sektor versetzt, da die Bedingungen für den Verbleib in Sektor 1 (weniger als 5.000.000 Punkte und weniger als 10 Kollektoren) gleich von Anfang an überschritten sind – wie gesagt, das gilt nur, wenn die CDE-Community sich für eine BR als nächste Runde entschieden hat, diese ist jedoch seit mindestens fünf Jahren unverändert voreingestellt.

Aufgrund der kurzen Tickzeiten ist es nur zufällig möglich, Deff von Sektor oder Allianz zu erhalten, daher spielt man in DCE nicht auf Kollektoren, sondern auf Kriegsartefakte. Mit einer Mischflotte hat man die größten Chancen, Kriegsartefakte zu erhalten. (Auch der Energieertrag von Kollektoren und Kriegsartefakten kann eingestellt werden; fast jede Runde liegen Kollektoren bei 100 und Kriegsartefakte bei 150 Energie pro WT).


