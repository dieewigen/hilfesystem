<b>Artefakte</b>

In DE werden mehrere Arten von Artefakten unterschieden:


<b><u>I. Sektorartefakte:</u></b>

Am Anfang einer Runde befinden sich keine Sektorartefakte im Spiel. Sobald die Anzahl der Sektorraumbasen groß genug ist, beginnt der Zufallsgenerator die Artefakte zu verteilen. Über unwahrscheinlich starke Hyperraumbeben erscheinen plötzlich Artefakte in der Nähe der Raumbasis, angezogen von den Unmengen Tronic, die bei deren Bau verwendet worden sind. Ob und welche Sektorartefakte vorhanden sind, sieht man in der Sektoransicht (Menü: "Sektor").

Es besteht eine geringe Wahrscheinlichkeit dass ein Artefakt den Sektor wechselt:
Einige Artefakte haben besondere Modifikatoren, welche diese Wahrscheinlichkeit noch weiter beeinflussen; sie können auch während der Runde von alleine ihren Aufenthaltsort ändern.

Der jeweilige BK erhält bei einem Wechsel eines Sektorartefakts eine Nachricht.

Pro Sektor kann nur ein Sektorartefakt vorhanden sein (Ausnahme sind die Gabe der Reichen-Artefakte, diese können als zweites Sektorartefakt vorhanden sein, ohne die Wahrscheinlichkeit zu erhöhen, dass eines davon in einen anderen Sektor springt).

Die Sektorartefakte können des Weiteren mittels der Sektorflotten im Sektorkampf erobert werden. Beachte, dass es vorkommen kann, falls bereits ein Sektorartefakt vorhanden ist (ausgenommen der Gabe der Reichen), dass es auch das soeben eroberte Artefakt sein kann, das mit dem nächsten WT den Sektor wechselt.

Derzeit gibt es folgende Sektorartefakte:

<i>Die Schale von Sabrulia</i>
Die Schale von Sabrulia manipuliert die Hyperraumkonstante und führt somit zu einer um 2% erhöhten Energieausschüttung der Kollektoren.

<i>Der Spiegel von Calderan</i>
Der Spiegel von Calderan verursacht durch eine temporale Anomalie unter Umständen eine Verdoppelung eines Kollektors, da dieser auf einer ähnlichen Hyperfrequenz arbeitet. Die Chance hierfür beträgt 1%.

<i>Der Spiegel von Coltassa</i>
Dieser Spiegel verursacht durch eine temporale Anomalie unter Umständen eine Verdoppelung eines Kollektors, da dieser auf einer ähnlichen Hyperfrequenz arbeitet. Die Chance hierfür beträgt 2%.

<i>Die Schale von Kesh-Ha</i>
Die Schale von Kesh-Ha manipuliert die Hyperraumkonstante und führt somit zu einer um 1% erhöhten Energieausschüttung bei den Kollektoren.

<i>Die Schale von Kesh-Na</i>
Die Schale von Kesh-Na manipuliert die Hyperraumkonstante und führt somit zu einer um 1% erhöhten Energieausschüttung bei den Kollektoren.

<i>Die Schale von Kesh-Za</i>
Die Schale von Kesh-Za manipuliert die Hyperraumkonstante und führt somit zu einer um 1% erhöhten Energieausschüttung bei den Kollektoren.

<i>Der Strom von Kiz-Murat</i>
Der Strom von Kiz-Murat erhöht das Planetare Grundeinkommen um 1.000 Multiplex.

<i>Der Strom von Kiz-Joar</i>
Der Strom von Kiz-Joar erhöht das Planetare Grundeinkommen um 500 Dhyarra.

<i>Der Strom von Kiz-Benir</i>
Der Strom von Kiz-Benir erhöht das Planetare Grundeinkommen um 400 Iradium.

<i>Der Strom von Kiz-Vokl</i>
Der Strom von Kiz-Vokl erhöht das Planetare Grundeinkommen um 200 Eternium.

<i>Das Grab des Ra</i>
Senkt die Chance von feindlichen Agenteneinsätzen. Die maximale Erfolgschance liegt bei 50%.

<i>Der Schild des Herakles</i>
Der Schild des Herakles verändert das Raum-Zeit-Gefüge soweit, dass die primitiven Zielsysteme von Sonden nicht mehr in der Lage sind sich innerhalb des Sektors zu orientieren und somit kein Ziel finden. Es können keine Sonden in diesen Sektor geschickt werden.

<i>Die Gabe der Reichen</i>
Die Gabe der Reichen manipuliert die Hyperraumkonstante und führt somit zu einer erhöhten Energieausschüttung bei den Kollektoren. Es gibt insgesamt 10 dieser Artefakte. Sie befinden sich immer in den schlechtesten Spielersektoren, und sie sind um jeweils 5% gestaffelt: von 50% erhöhter Energieausschüttung der Kollektoren für den schlechtesten Sektor, bis 5% erhöhter Energieausschüttung der Kollektoren für den zehntschlechtesten Sektor.
Die Gabe der Reichen-Artefakte können nicht mit einer Sektorflotte erobert werden.


<b><u>II. Spielerartefakte:</u></b>

<b>II.1. Artefakte, die durch ihr Vorhandensein wirken</b>

Diese Artefakte müssen im Artefaktgebäude gelagert werden (Menü: "Artefakte"). Sie wirken (so wie ein Sektorartefakt) durch ihre Anwesenheit. Man erhält sie durch den täglichen Allianzbonus, durch Angriffe auf Aliens in den NPC-Sektoren oder als Fundstücke in den Vergessenen Systemen. Sie können auch in den Auktionen erworben werden. 

<i>Pesara-Artefakt</i>
Dieses Artefakt verringert die Baukosten von Schiffen.

<i>Vakara-Artefakt</i>
Dieses Artefakt verringert die Baukosten von Türmen.

<i>Geangrus-Artefakt</i>
Dieses Artefakt erhöht die Chance, einen Agenteneinsatz erfolgreich durchzuführen.

<i>Geabwus-Artefakt</i>
Dieses Artefakt erhöht die Chance, einen feindlichen Agenteneinsatz abzuwehren.

<i>Agsora-Artefakt</i>
Dieses Artefakt verringert die Bau- bzw. Ausbildungskosten im Geheimdienst.

<i>Turak-Artefakt</i>
Dieses Artefakt erhöht die Feuerkraft von Türmen.

<i>Turla-Artefakt</i>
Dieses Artefakt erhöht die Lähmkraft von Türmen.

<i>Empala-Artefakt</i>
Dieses Artefakt erhöht die Leistungsfähigkeit der Planetaren Schilderweiterung gegen EMP-Waffen.

<i>Recarion-Artefakt</i>
Dieses Artefakt erhöht die Kraft, mit der Schiffe nach Schlachten im Heimatsystem recycelt werden.

<i>Autacon-Artefakt</i>
Dieses Artefakt verringert die Auktionspreise. Maximal 50% für alle Artefakte zusammen. Bei eigenen Auktionen kann man somit auf maximal 75% Preisnachlass kommen.


<b>II.2. Benutzbare Artefakte</b>

Diese Artefakte müssen wie die Spielerartefakte vorerst im Artefaktgebäude zwischengelagert werden (Menü: "Artefakte"). Ihre Funktionsweise liegt jedoch nicht in der Anwesenheit, sondern in der Benutzung; sie erzielen dabei eine bestimmte Wirkung. Sie werden durch die Aktivierung verbraucht und verschwinden danach aus dem Artefaktgebäude. Man kann sie so lange im Artefaktgebäude lagern, bis man sie benützt (wohl spätestens, wenn man den Platz im Artefaktgebäude braucht). Diese Artefakte erhält man durch den täglichen Allianzbonus, durch Angriffe auf Aliens in den NPC-Sektoren, als Fundstücke in den Vergessenen Systemen oder in den Auktionen.

<i>Kollimania-Artefakt</i>
Dieses Artefakt erzeugt 5-10 Kollektoren.

<i>Artasecundus-Artefakt</i>
Dieses Artefakt erhöht Artefaktgebäudestufe um 1, wobei die Maximalstufe auch überschritten werden kann.

<i>Waringa-Artefakt</i>
Dieses Artefakt erzeugt 1-3 Kriegsartefakte.

<i>Tronicar-Artefakt</i>
Dieses Artefakt erzeugt 15-20 Tronic.

<i>Sekkollus-Artefakt</i>
Dieses Artefakt erzeugt 1-2 Sektorkollektoren.

<i>Creditrüssel-Artfakt</i>
Dieses Artefakt erzeugt einen gigantischen Hyperraumrüssel, der dem großen Ishtarus 3-5 Credits aus seinem Geheimlager wegrüsselt.


<b>II.3. Flottenartefakte</b>

II.3.A) Basisschiffartefakte:
Diese Artefakte können in einem Basisschiff eingebaut werden (Menü: "Artefakte"), jedoch müssen sie dazu sich erst im Artefaktgebäude befinden. Markiere dazu das Artefakt und gib dann an, in welches Basisschiff es transferiert werden soll. Das Basisschiff darf nicht unterwegs sein, und es muss noch mindestens ein Artefaktplatz frei sein. Pro Basisschiff können maximal sechs Artefakte ausgerüstet werden.

II.3.B) Benutzbare Flottenartefakte:
Diese Artefakte erhöhen die Erfahrungspunkte der Flotten (Menü: "Artefakte").
Diese Artefakte erhält man durch den täglichen Allianzbonus, durch Angriffe auf Aliens in den NPC-Sektoren, als Fundstücke in den Vergessenen Systemen oder in den Auktionen.


Es gibt folgende Flottenartefakte:

<i>Recadesto-Artefakt</i>
Basisschiffartefakt
Dieses Artefakt stört die Kraft des feindlichen Recyclotrons und wird in einem Basisschiff verwendet.

<i>Empdestro-Artefakt</i>
Basisschiffartefakt
Dieses Artefakt verringert die Leistungsfähigkeit der Planetaren Schilderweiterung gegen EMP-Waffen des Ziels und wird in einem Basisschiff verwendet.

<i>Pekasch</i>
Benutzbares Flottenartefakt
Dieses Artefakt erhöht bei Benutzung die Angriffserfahrungspunkte jeder Flotte um 10.000 Punkte. Nach der Aktivierung des Artefakts verliert es für immer seine Wirkung.

<i>Pekek</i>
Benutzbares Flottenartefakt
Dieses Artefakt erhöht bei Benutzung die Verteidigungserfahrungspunkte jeder Flotte um 10.000 Punkte. Nach der Aktivierung des Artefakts verliert es für immer seine Wirkung.

<i>Feuroka-Artefakt</i>
Basisschiffartefakt
Dieses Artefakt erhöht die Feuerkraft der Schiffe und wird in einem Basisschiff verwendet.

<i>Bloroka-Artefakt</i>
Basisschiffartefakt
Dieses Artefakt erhöht die Lähmkraft der Schiffe und wird in einem Basisschiff verwendet.


<b>Verschmelzen von Artefakten</b>
Die Spielerartefakte können miteinander verschmolzen werden, um ihre Wirkung zu erhöhen oder aber um ein neues Artefakt zu erzeugen (Menü: "Artefakte"):

- Zwei gleiche Artefakte (Spielerartefakte und Basisschiffartefakte, jedoch keine benutzbaren Artefakte) mit der selben Stufe können zu einem Artefakt der nächsthöheren Stufe verschmolzen werden, das meist wirksamer ist. Kosten: 5 Tronic. (Zwei gleiche Artefakte mit verschiedenen Stufen können nicht miteinander verschmolzen werden).

- Zwei verschiedene Artefakte (egal welcher Stufe, und hier auch alle Artefakte) können zu einem zufälligen anderen Artefakt (der Stufe 1) verschmolzen werden. Kosten: 10 Tronic.

Um Artefakte der Basisschiffe verschmelzen zu können, muss man sie in das Artefaktgebäude transferieren. Das Basisschiff muss dafür am Heimatplaneten sein.
Dies erfüllt die Allianzaufgabe "Führt Artefaktverschmelzungen und Artefaktupgrades durch".

<b>Zerstören von Artefakten</b>
Unabhängig von Aktivierung oder Verschmelzung kann man jedes Spielerartefakt auch zerstören, um daraus Palenium zu gewinnen (Menü: "Artefakte"). Für das Zerstören eines Artefakts mit Bonusstufe 1 erhält man 100 Palenium, für ein Artefakt mit Bonusstufe 2 erhält man 200 Palenium, usw.
Palenium wird für die Forschung der Titanen benötigt, weiters für den Ausbau der Artefaktplätze in den Basisschiffen sowie für die Upgrades des Basissterns für die Battlegrounds.


WICHTIG:
Jedes neu hinzugekommene oder erworbene Spielerartefakt kommt zuerst in das Artefaktgebäude, daher muss auch Platz im Artefaktgebäude vorhanden sein. Erst von dort kann es, wenn es ein Basisschiffartefakt ist, in ein Basisschiff transferiert werden oder benutzt werden, wenn es ein benutzbares Artefakt ist, oder mit einem anderen Artefakt verschmolzen werden (das sich ebenfalls im Artefaktgebäude befinden muss), oder zur Paleniumgewinnung zerstört werden.

Wenn kein Platz mehr im Artefaktgebäude ist:
o) Beim Allianzbonus erhält man in diesem Fall kein Artefakt!
o) Bei einem Sieg gegen ein Alien erhält man statt der üblichen Meldung: "Du konntest folgende Artefakte erbeuten: … [Name des Artefakts], Allianzartefakt", die Meldung: "Da es im Artefaktgebäude keinen freien Platz gibt, hast Du kein Artefakt erhalten., Allianzartefakt", und man erhält nur das Allianzartefakt, das an die Allianz geht.
o) Versucht man eine Artefaktmission zu beenden, erhält man die Fehlermeldung: "Im Artefaktgebäude ist kein freier Platz." Man kann die Mission nicht abschließen und somit auch keine neue Artefaktmission starten.
o) Versucht man in den Vergessenen Systemen ein Artefakt als Fundstück zu bergen, so erhält man die Meldung: "Im Artefaktgebäude ist kein freier Platz", und man kann das Artefakt erst bergen, bis wieder Platz vorhanden ist.
o) In den Auktionen kann man auf keine Auktion bieten, bei der man Artefakte erhalten kann. Die Option "bieten" wird in diesem Fall nicht angezeigt.

Gegenmaßnahmen:
- Artefaktgebäude ausbauen
- benutzbare Artefakte einlösen (vor allem Artascendus)
- Basisschiffartefakte in ein Basisschiff transferieren
- Artefakte verschmelzen
- Artefakte zerstören
- den Allianzleiter bitten, die Artefaktgebäudeerweiterung weiter auszubauen


<b><u>III. Andere Artefakte:</u></b>

<i>Kriegsartefakte</i>
Kriegsartefakte erhält man bei Verlust größerer Mengen an Schiffen (bei Angriffen erhält man z.B. pro 500.000 verlorenen Flottenpunkten 1 Kriegsartefakt, bei Verteidigung erhält man z.B. pro 450.000 verlorenen FP 1 Kriegsartefakt) oder durch Aktivierung eines Waringa-Artefakts. Auch im Rahmen des täglichen Allianzbonus erhält man ein Kriegsartefakt, und auch im Schwarzmarkt sind Kriegsartefakte in begrenzter Anzahl erhältlich. 

Die Werte können je nach Server unterschiedlich sein. Da die erhaltenen Erfahrungspunkte an die verlorenen Einheitenpunkte gekoppelt sind, wirken sich diesbezügliche Boni auf beide Werte aus.

Sie benötigen keinen Lagerplatz, es können beliebig viele Kriegsartefakte vorhanden sein; die Anzahl der Kriegsartefakte kann im Menü: "Ressourcen", im Abschnitt "Ressourcenertrag", nachgesehen werden.
Kriegsartefakte wirken wie Kollektoren: pro erhaltenem Kriegsartefakt werden jeden WT 100 Energie mehr erzeugt (auf dem EDE-Server 200). Sie erzeugen weiterhin Energie, auch wenn die Kollektoren kurzfristig ausfallen (z.B. während des Erforschens eines Vergessenen Systems mittels Sonden), aber ein Kollektorenbonus (z.B. ein Sektorartefakt, das die Energieausschüttung der Kollektoren erhöht) wirkt sich nicht auf die Kriegsartefakte aus.
Kriegsartefakte können von anderen Spielern nicht erobert werden. Sie bleiben bis zum Rundenende vorhanden. Im Spielerjargon werden sie oft nur "Artis" genannt.

<i>Allianzartefakte</i>
Allianzartefakte werden benötigt, um Allianzgebäude zu bauen. Jede Allianz erhält alle 96 Ticks ein Allianzartefakt. Zusätzlich erhält die Allianz eines Spielers, der erfolgreich ein Alien angreift (NPC-Sektoren), ein Allianzartefakt, und auch bei Erfüllung einer Allianzaufgabe erhält die Allianz eines. Allianzartefakte unterliegen keiner Lagerbeschränkung.
Die Anzahl der vorhandenen Allianzartefakte kann man im Menü: "Allianz / Projekte" nachsehen.

<i>Allianz-Rundensiegartefakte</i>
Bei Erfüllung der aktuellen Allianzaufgabe erhält die Allianz 100 Siegartefakte plus einem Zeitbonus. Die Allianz-Rundensiegartefakte sind ausschlaggebend für die Allianzwertung in der Rangliste. Bei Rundenende gewinnt nicht die Allianz mit den meisten Punkten, sondern die mit den meisten Rundensiegartefakten.
Die Anzahl der Rundensiegartefakte kann man im Menü: "Rangliste / Allianz / Siegartefakte" nachsehen. 



