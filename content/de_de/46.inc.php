<b>Punkte</b>

Einheiten (Flotte und Türme), Rohstoffe und Kollektoren bringen Punkte.


<b>Einheiten</b>
Der Punktewert einer Einheit beträgt ein Zehntel ihres Energiewertes.
Der Energiewert setzt sich zusammen aus den Rohstoffen, die für den Bau aufgewendet werden mussten, nach dem Schlüssel 1:2:3:4 für Multiplex, Dyharra, Iradium und Eternium.

Punktezahl = [(1x Multiplex) + (2x Dhyarra) + (3x Iradium) + <nobr>(4x Eternium)] / 10</nobr>

Ein Jäger der Ewigen kostet (ohne Reduktion durch Pesara-Artefakte) 1000 Multiplex und 250 Dhyarra, so ergibt sich:
[1000 + (2x250)] / 10 = 150 Punkte

Werden Tronic für den Bau von Einheiten benötigt, so wird für diese der Faktor 1000 gerechnet.
Ein Schlachtschiff kostet 50.000 Multiplex, 20.000 Dyharra, 2.000 Iradium, 4.000 Eternium, 2 Tronic und bringt also [50000+(2x20000)+(3x2000)+(4x4000)] / 10 = 11.200 Punkte, hinzu kommen 2x 1000 Punkte für das verbaute Tronic: 13.200 Punkte für ein Schlachtschiff.

Der Punktewert von Einheiten bleibt stets gleich, auch wenn die Baukosten durch allfällige Boni im Laufe der Runde sinken.


<b>Rohstoffe</b>
Rohstoffe ergeben auch Punkte, jedoch nur 1/250stel vom Wert. 100.000 Multiplex bringen demnach 400 Punkte.


<b>Kollektoren</b>
Kollektoren bringen nach einem eigenen Schlüssel Punkte. Je mehr Kollektoren man hat, umso höher ist ihre Punktebewertung.
Die detaillierten Punkte sind in der Hilfe unter <a href="https://help.bgam.es/index.php?thread=de_de&post=37">Kollektor</a> zu finden.


