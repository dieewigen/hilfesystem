Wie funktioniert das SE Spionage System? 

SE hat 3 Unterschiedliche Spionagefunktionen, zwei davon sind aktiv eine passiv. Die Funktionsweise ist bei allen dreien die gleiche, ihr braucht das Gebäude um die Funktion zu aktivieren, mit dem Gebäude erhaltet ihr automatisch eine 50 prozentige Genauigkeit der Funktion, desweiteren ist es ratsam die zur Funktion gehörigen Forschungen voranzutreiben, pro Forschungstufe erhaltet ihr weitere 3 prozent Genauigkeit. Ein Beispiel zum verdeutlichen: Ihr habt den Hyperraum Scanner und die dazugehörigen Scanner Modulatoren auf Stufe 7, somit habt ihr 71% Genauigkeit, also 71% Chance das ihr einen Angriff auf euren Planeten seht.


Wie bekomme ich Punkte?

Durch den Bau von Schiffen und Verteidigungsanlagen, und seid Beta 02 auch durch Forschen und den Ausbau von Gebäuden. Generel gilt 1 Punkt für 1000 verbaute Ressourcen, bei den niedrigen Forschungs und Gebäude Stufen wird allerdings abgerundet, so geben die ersten Gebäude und Forschungen noch keine Punkte. 
Sonden und Agenten bringen KEINE Punkte.


Wo liegen die max. Level der Gebäude und Forschungen? 

Das ist Unterschiedlich. Bei allen 4 Minen liegt die maximale Stufe bei 20, die Werft hat max Level 15, Spionage Gebäude haben jeweils nur eine Stufe. Bei Forschungen gilt derzeit generel das maximal Level 15. Ausnahmen sind zum Beispiel die Planetare Verwaltungszentrale welche 14 Stufen hat. Sobald ihr das Maximallevel erreicht habt wird dieses angezeigt.


Welche Zeiteinheiten gibt es bei SE? 

SE benutzt einen sogenannten Puls, weitläufig auch als Tick bekannt. Es gibt nur diese eine Zeiteinheit in der alles berechnet wird. Wie lang ein Puls ist erkennt ihr aus der sich bildenden Differenz zwischen den Anzeigen Serverzeit und Letzter Puls.
Ein Puls dauert 6 Minuten, also genau 10 pro Stunde.


Warum gibt es 2 Treibstoffe? 

Bei SE gibt es 2 Treibstoffe für eure Schiffe, Tn2x4 einen Festreibstoff und Tn4x8 einen Flüssigtreibstoff, der Unterschied zwischen diesen Beiden ist in der dazugehörigen Gebäude Beschreibung zu finden, eine kurze einfache Erklärung wäre: Tn4x8 ist seltender, beschleunigt jedoch eure Angriffe.


Wie funktioniert das Kampfscript?  

Mit dem neuen Kampfscript ist es theoretisch möglich das jede Einheit jede andere angreift, ausnahmen bilden da nur Transporter und ähnliche da sie keine Waffen besitzen. Angegriffen wird in einer festgelegten Reihenfolge welche zum Teil in den Beschreibungen der Einheiten nachzulesen ist, allerdings ist noch zu erwähnen das nicht jede Einheit gegen jede andere volle Wirkung hat, z.B. sind Jäger gegen die stark gepanzerten Schlachtschiffe fast machtlos, andersrum haben es auch Schlachtschiffe recht schwer gegen die wendigen Jäger, denoch ist es wohl für einen Jäger fast unmöglich den direkten Treffer eines Schlachtschiffes zu überleben.

Bekannte Funktionen aus anderen Spielen wie etwa Verteidigung fremder Planeten oder Gruppenangriffe gibt es bei SE nicht.


Wieviel Prozent der Ressourcen stehle ich bei einem Angriff? 

Derzeit sind es maximal 50 Prozent der angestrebten Ressource. Für spätere Versionen sind Modifikatinen dieses Wertes geplan, auch soll einst im Kriegsfalle zwischen 2 Allianzen mehr gestohlen werden können.


Wie kann ich Planeten kolonisieren? 

Du benötigst ein Kolonieschiff und eine entsprechend ausgebaute Interplanetare Verwaltungszentrale. Pro Ausbaustufe des Gebäudes kann 1 Planet verwaltet werden. Das Maximallevel liegt bei Stufe 14. Ein normales Angreifen des gewünschten Planeten mit einem Kolonieschiff genügt zum Kolonisieren.
Ein Übernehemen einer Kolonie von einem anderen Spieler gelingt nur, wenn die verteidigende Flotte volllkommen zerstört wird. Fremde Gebäude können nur mit dem großen Kolonieschiff Therius Satar VI K.-S übernommen werden.


Wie kann ich Ressourcen tranferieren?  

In der Kolonieübersicht findet Ihr das Dialogfeld M R A
Über den R Link erreicht ihr ein Flottenmenue, über das ihr Rohstofftransporte anfordern könnt. Beachtet jedoch, dass jede Transporterflotte einen Flottenslot der Navigationskontrolle belegt.


Wie stationiert man Schiffe auf Kolonien? 

Schicke Deine Schiffe einfach über das Militärmenue auf einen Deiner Planeten. Das Script erkennt automatisch die Kolonie und stationiert die Schiffe dort bis sie über den "heimkehren" Botton zurückgerufen werden. Bedenke, dass jede stationierte Flotte einen Slot in der "Navigationstechnik" belegt.


Wie kann ich mehr als eine Flotte verwalten? 

Mit dem Ausbau der Forschung "Navigationstechnik" kann die Anzahl der Flotten die gleichzeitig unterwegs sind erhöht werden. Der Maximalausbau liegt derzeit bei Stufe 15. Die Flottenverwaltung stellt somit eine begrenzte Ressource dar, die taktisch klug eingesetzt werden sollte. 


Gibt es einen Schutz für Neueinsteiger?

Ja es gibt einen sogenannten Newbschutz.
Neue Accounts stehen unter Newbschutz.
Dieser wird aufgehobenn wenn einer der folgenden Punkte erfült ist:
- der Account ist äter als 1200 Pulse
- der Account hat mehr als 500 Punkte
- der Account fliegt selbst Angriffe auf andere Accounts oder gründet eine Kolonie

Erweiterter Angriffsschutz (Angriffssperre) 

Sobald ein Spieler einen anderen angreift wird dieses vermerkt und ein Timer auf 180 Pulse gesetzt. Jeder weitere Angriff erhöht den Timer um weitere 180 Pulse. 
Ist ein die Anzahl der Angriffe 4 wärend noch ein Timer existent ist, ganz gleich wie hoch dieser ist, wird der Angreifer für die Restzeit des Timers auf dieses Ziel gesperrt 

Der Timer lässt es also theoretisch zu, ein Ziel alle 181 Pulse anzugreifen ohne das es jemals zu einer Angriffsperre kommt, er verhindert aber das zu viele Angriffe aufeinander, von nur einer Person auf ein und dasselbe Ziel, erfolgen und kann in dem Falle bis zu einer 720 Pulse langen Angriffsperre führen. Oder andersrum, nach vier Angriffen hintereinander müsst ihr Euch ersteinmal ein anderes Ziel suchen. 


Was ist ein Quadrant? 

Das Universum von SE ist in Quadranten eingeteilt, in jedem Quadranten können 25 Planeten platz finden.
Quadrant ist das äquivalent zu Sektor/Sonnensystem. Die Quadrantenansicht wird gecached. Die Aktualisierung erfolgt etwa alle 1,5 stunden (wann genau, seht ihr direkt unter dem letzten Planeten). Also wundert euch nicht, wenn ihr euch respektive eure Kolo nicht sofort seht.


Was hat es mit den Kolonien auf sich? 

Die Kolonien in SE sind genaugenommen Wirtschaftskolonien auf dennen nichts ausser der 4 im Spiel vorkommenden Minen gebaut werden können, Flotten um diese zu beschützen müssen vom Hauptplaneten aus dort stationiert werden. Die erwirtschafteten Ressourcen können mit Transportschiffen abgeholt werden.
Neue Kolonien können mit einem Kolonieschiff "Therius Satar V K.-S" erschlossen werden.


Kann man den Ertrag der Minen auch noch anders steigern ohne sie auszubauen?

Nein, aber Kolonien bekommen Bonusressourcen wenn du sie länger behältst, denn routinierte Arbeiter sind bekanntlich gute Arbeiter.
Diesen Bonus könnt ihr in der Kolonie Übersicht in der Spalte HZ (Haltezeit) einsehen. Für 7200 komplette Pulse bekommt ihr 1% zuwachs, also 7200 = 1%, 14400 = 2% usw. Die Haltezeit ist ausspionierbar, natürlich mit den üblchen Ungenauigkeiten.
Dies sollte ein Anreiz sein sie gut zu verteidigen.


Wie kann ich einer Allianz beitreten? 

Du brauchs das PW Deiner gewünschten Allianz. Dieses bekommst Du im allgemeinen vom Leader.
Das führt natürlich dazu, dass man den Membern vertrauen können muss und eine gewisse Sicherheitspolitik wäre auch angebracht, sonst habt ihr schneller Spione als ihr denkt.


Wie kann ich eine feindliche Kolonie erobern? 

Das Invasieren einer Kolonie gelingt nicht zu 100%.
Sondern zu 15% + 4% * Stufe der Forschung Invasionstechnologie.

Die Forschung auf Stufe 15 gibt euch also eine 60% Chance, dass die Invasion erfolg hat. Zuzüglich der 15% die das Schiff mit sich bringt, zusammen also maximal 75%.

Eine Invasion ist jedoch nur erfolgreich, wenn alle Schiffe des Verteidigers im Orbit der Kolonie zerstört werden konnten. 


Wie komme ich auf meine Kolonie um dort zu bauen?

Im Menue links ist ein kleine Pulldown-menue untergebracht. Dort könnt ihr zwischen Euren Planten wechseln. Ist einer noch nicht dabei, einfach die Anzeige mit "Refresh" aktualisieren. Das Bauen geht wie auf dem Hauptplaneten mit eingeschränkten Auswahlmöglichkeiten.


Wie werden Rohstoffe recycelt?

Recyclen als Angreifer: 
Ihr Benötigt die entsprechende Forschung. Ab Stufe 1 schaltet diese die Recycler Frei. Je Stufe in dieser Forschung (max.15) können eure Recycler 2% der Trümmer recyceln maximal also 30%. 
Das Recyceln findet als letztes nach dem eigentlichen Kampf aber noch im selbigen statt. Recycelt wird alles was im Kampf vernichtet wurde. 

Recyceln als Verteidiger: 
Das ist wesentlich einfacher, durch das Gebäude recycelt ihr automatisch in jedem Kampf an eurem Planeten 
entsprechend der Stufe eures Gebäudes *3%, bei Stufe 5 also 15% maximal 60%. 


Reisezeiten
Die Reisegeschwindigkeit hängt von vielen Faktoren ab:
- Geschwindigkeit der einzelnen Schiffe
- Trägerkapazität der Kreuzer/Schlachtschiffe
- Reiseziel (naher Quadrant, ferner Quadrant, innerhalb eines Quadranten)
- Position eurer Kolonien
- verwendeter Treibstoff (Tn2x4 benö;tigt 1 Puls Tankzeit für 500 Schiffe)


Wie hole oder bringe ich Ressourcen zu den Kolonien?
Im Menue das Koloniefeld und Aktionen M R A benutzen.
M - Militär, z.B. Flotte hinschicken zum stationieren. 
R - Rohstofftransfer
A - Aufgeben (Zeitzünder von 120 Pulse starten um alle gebäude der Kolonie zu sprengen). Eine Kolonie muss mindestenz 720 Pulse in eurem besitzt sein bevor ihr diese löschen könnt. Das gibt dem Ehemaligem Besitzer genug zeit sie zurück zu erobern. Das heisst nach dem löschen sind sämtliche Gebäude wieder auf 0 auch liegen keine Resourcen auf der Kolonie. Sollte die Kolonie in dieser Zeit invasiert werden, so wird der Löschvorgang abgebrochen.


Wie transferiere ich Ressourcen von der Kolonie zum Hauptsystem?

Also 1. das Hauptsystem anwählen über das Menue 
2. Kolonieübersicht öffenen.
3. Bei der gewünschten Kolonie das Ressourcen-verschiffungsfenster mit Röffnen.
4. Anzahl der Frachter die beladen werde sollen wählen.
5. Rostoff wählen.
6. Auftrag Berechnen, überprüfen und Absenden.