1. Wie fange ich am besten an?
Dazu findest du links im Menü unter "Die ersten Schritte" Informationen.

2. Sind die Gebäude nur für mich nutzbar?
Nein, sämtliche Gebäude sind Fraktionsgebäude.

3. Kann ein anderer Spieler meinen Lagerkomplexinhalt abgreifen?
Nein, jeder Spieler hat seinen eigenen „Raum“ im Lagerkomplex auf den nur er zugreifen kann. Dies gilt auch für den Modulkomplex.

4. Ich kann kein Gebäude bauen – wieso geht das nicht?
Das liegt sicher daran, dass eine Bedingung nicht erfüllt wurde. Rote Bedingungen bedeuten, dass hier noch etwas fehlt.

5. Kann ich einer anderen Fraktion meine Module verkaufen?
Nein dies ist nicht möglich.

6. Wie kann ich mein Raumschiff vergrößern?
Indem du in der Raumwerft den Durchmesser erhöhst, diese Baustufe ist abhängig von der Raumwerftstufe.

7. Kann ich mehrere Hyperantriebe einbauen, um so schneller zu sein?
Nein, dies geht nicht.

8. Kann ich mehrere Forschungsmodule einbauen, um so schneller zu forschen?
Nein, dies geht nicht.

9. Kann ich mehrere Bergbaumodule einbauen, um so schneller Rohstoffe abbauen zu können?
Ja, dies ist möglich.

10. Was muss ich hier machen, um den Kreditbonus zu bekommen?
Dazu findest du links im Menü unter dem Punkt "Creditbonus" Informationen. 

11. Wie komme ich von dem System weg?
Dazu findest du links unter dem Punkt "Reisemöglichkeiten" Informationen.

12. Wie bekomme ich ein weiteres Schiff?
Pro Server kannst du nur ein Schiff haben, um ein weiteres Schiff zu haben, musst du dich auf einen anderen Server anmelden.