Willkommen bei Ablyon bzw. der erweiterten Die Ewigen-Archäologie. 

Da du hierher gekommen bist, hast du bereits EA-Namen, Schiffsnamen und Fraktion gewählt, daher gehe ich darauf nicht mehr ein.
Hier erkläre ich dir die ersten Klicks: (angegeben wird, worauf man klicken soll)

1. Im Chat vorstellen

Dies ist optional, beschleunigt aber den Aufbau wesentlich. 
Unten rechts befindet sich der EA (Erweiterte Archäoloige) - Chat. 
Standardmäßig ist der Fraktionschat aktiviert, das heißt, nur deine Fraktion kann deine Nachrichten mitlesen. 
Was das genau bedeutet, ist jetzt nicht so wichtig. Du solltest allerdings wissen, dass blauer Text für fraktionsinternen Chat steht, weißer Text der Chat zwischen allen EA-Spielern ist, und grün Systemnachrichten sind. 
Dort schreibst du am besten: "Hallo, ich bin neu hier, kann mir jemand ein paar Module per Sofortkauf kostengünstig anbieten?"
Falls jemand fragt, du benötigst zu Beginn vor allem Bergbaumodule (mind. 2). Gut wäre auch noch ein Überlichtantrieb (Hyperdrive) um später in andere Sonnensysteme reisen zu können.
Eine Betonung, dass du noch kein Geld hast, macht eventuell Sinn. 
Mit Enter schickst du den Text ab.
Im folgenden solltest du den Chat ein bisschen im Auge behalten und auf blaue Antworten Ausschau halten.


2. Rohstoffe abbauen

Um dein Raumschiff zu vergrößern und Module zu bauen, benötigst du Rohstoffe in deinem Lagerkomplex. Der Lagerkomplex ist über Transmitterstrecken in jedem Sonnensystem für dich verfügbar.
Klicke in der Systemansicht (das ist die Standardansicht, wenn du dich einloggst) rechts auf den Punkt Lagerkomplex.
Dort kannst du in der Spalte "Bergbaudauer" den gewünschten Rohstoff x Minuten lang abbauen.
Nicht in jedem Sonnensystem ist jeder Rohstoff vorhanden. Mehr dazu findest du links im Menü unter Rohstoffe.

3. Raumschiff vergrößern

In der Systemansicht rechts unter dem Punkt "Raumwerft" kannst du dein Raumschiff vergrößern.
Dort werden die dafür benötigten Rohstoffe angezeigt, die sich im Lagerkomplex befinden müssen.
Bei steigender größe werden dazu immer bessere Rohstoffe benötigt.
Die maximale Raumschiffgröße ist von der Raumwerftgröße abhängig und kann sich zwischen den Sonnensystemen entscheiden.
Je größer dein Raumschiff ist, desto mehr Module können in diesem eingebaut werden. 
 
4. Zastari (Geldwährung) verdienen

In der Systemansicht rechts unter dem Punkt "Handelskontor" kannst du Zastari verdienen.
Dort kannst du in der Spalte "Bergbaudauer" den gewünschten Rohstoff x Minuten lang abbauen und direkt verkaufen.
Nicht in jedem Sonnensystem ist jeder Rohstoff vorhanden. Mehr dazu findest du links im Menü unter Rohstoffe.


5. Module besorgen

Falls du im Chat erfolgreich warst, hat ein Mitspieler für dich die Module gebaut und in das Auktionszentrum gestellt. Ansonsten musst du dir die Module selbst erstellen. 


5.1 Module über das Auktionszentrum kaufen

In der Systemansicht links unter dem Punkt "Auktionszentrum" kannst du Module ersteigern und versteigern.
Klicke dort einfach unter dem Punkt "Bietern" auf "Suche starten" und es werden dir alle vorhandenen Module angezeigt. 
Beachte bitte, dass du die Module per Sofortkauf kaufen solltest, sonst musst du warten, bis die Auktion vorbei ist. 
Sollte keine Liste kommen, hat der Mitspieler die Module noch nicht eingestellt oder ein Anderer war schneller. 
In dem Fall solltest du im Chat freundlich nachfragen. 


5.2 Module selbst bauen

In der Systemansicht links unter dem Punkt "Fabriken" kannst du Module bauen.
Oben kannst du die Art der gewünschten Module auswählen.
Es erscheint dann eine Liste mit verschiedenen Modulen mit Rohstoffbedarf und Zastaripreisen.
Bei den Preisen bedeuten die Farben, ob du dir das Modul leisten kannst (grün = ja, gelb = fast, rot = nein)
Du musst solange Rohstoffe und Zastari sammeln, bis du das Module bauen kannst. 
Zur Stufe der Module: Du solltest auf eine hohe Stufe achten. 

Du musst selbst entscheiden, ob und wieviele kleinere Module du bauen willst, im späteren Verlauf wirst du diese sicherlich wieder wegwerfen. 
Solltest du aber im Chat keinen Erfolg gehabt haben, bringen einzelne Module niedrigerer Stufe einem doch etwas schneller weiter.


6. Wofür sind die Module überhaupt gut?

Bergbaumodule: Sie dienen zum Abbau von Rohstoffen in Asteroidenfeldern. Je besser sie sind, desto höher ist die Ausbeute.
Forschungsmodule: Mit ihrer Hilfe können Baupläne verbessert und somit bessere Module hergestellt werden.
Überlichtantrieb: Sie werden für den Überlichtflug benötigt. Je besser sie sind, desto kürzer ist die Reisezeit.
Waffen und Schilde: Sie werden für den Kampf gegen Piraten und anderes Gesindel benötigt.
Bergungsmodul: Wird für Bergungsmissionen benötigt. Bergungsgut sieht man häufig in den Sektoren. 
Klonmodul: Beim Erreichen des Maximalalters wird im Klonmodul ein neuer Körper erzeugt. Je besser das Klonmodul ist, desto weniger Schäden treten dabei auf.


7. Module in das Schiff einbauen
In der Systemansicht rechts unter dem Punkt "Modulkomplex" kannst du Module verwalten.

Auf der linken Seite siehst du deine im Modulkomplex eingelagerten Module, in der Mitte stehen deine zur Zeit verwendeten Module und 
rechts findet man eine Zusammenfassung über den aktuellen Schiffsstatus, insbesondere Förderkapazität und Frachtraum. 
Naheliegender Weise kann man mit den grünen Pfeilen die Module ein- und ausbauen. 
Du solltest versuchen, dein Schiff möglichst schnell mit diesen Modulen vollzubekommen, baue oder kaufe also so lange Module, bis dein Schiff voll ist. 

8. Weiteres Vorgehen 
Wenn dein Schiff auf dem aktuellen Stand deiner Fraktion ist, dann kannst du anfangen deinen Fraktionskollegen beim Ausbau zu helfen.

Wie du in andere Sonnensysteme und Sektoren gelangst findest du links im Menü unter Reisemöglichkeiten.







