Es gibt eine Vielzahl von Rohstoffen in den Weiten des Weltraumes und diese Übersicht kann nur einen kleinen unvollständigen Teil wiedergeben.

1. Abbaubare Rohstoffe in den Sonnensystemen
Die folgenden Rohstoffe können in Asteroidenfeldern in den Sonnensystemen abgebaut werden:
Eisen	         
Titan	         
Mexit	         
Dulexit	         
Tekranit	         
Ylesenium	         
Serodium	         
Rowalganium	         
Sextagit	         
Octagium

In den Randgebieten findet man nur die einfachen Rohstoffe gehäuft, die selteneren Rohstoffe gibt es jedoch nur in Richtung des Galaxiezentrums.


2. Besondere Rohstoffe
Besondere Rohstoffe werden in einem Spezialfrachtraum an Bord deines Raumschiffes gelagert.
Diese Rohstoffe findet man in treibenden Raumtrümmern, die man mit einem Bergungsmodul bergen kann.
Man kann diese auch in den Stationen der Bao-Nada erwerben, dazu benötigt man als Währung Baosin.


3. Dunkle Materie
Diese kann man von den Wachschiffen in den Hyperraumblasen erbeuten. Bisher wurde sie nur für spezielle Forschungen benötigt, aber weitere Nutzungmöglichkeiten sind nicht ausgeschlossen.