Dieser Bonus gilt nur für Server in Verbindung mit Die Ewigen.

Der Creditbonus ist eine Belohnung für die Aktivität in der erweiteren Archäologie. Er wird jeden Tag um ca. 01:00 an die Spieler ausgezahlt.

Wie erhalte ich den Bonus?

Auf der Systemseite im Infocenter befindet sich das Feld "Spendenübersicht", hier ist die Summe der Spenden zu finden. Vom Durchschnitt der täglichen Spenden aller Fraktionen müssen 50% am jeweiligen Tag eingezahlt worden sein, um den Bonus zu erhalten. (Ein Archäologiejahr entspricht einem Tag Realzeit.)
In der Spendenübersicht wird der von Dir benötigte Spendenwert angezeigt.
Da die Auszahlung um 01:00 Uhr ist, kann sich der Wert noch bis dahin ändern, man sollte also einen Puffer einplanen, wenn man nicht solange online ist.

Was sind Spenden?
Als Spenden werden folgende Handlungen gewertet:
1. Das Einlagern von Rohstoffen in Gebäuden und Forschungen
2. Das Einzahlen von Geld in die Fraktionskase
3. Das Einzahlen von Geld für die Sektorerforschung
4. Das Einzahlen von Geld für Forschungen