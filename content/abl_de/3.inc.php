Es gibt mehrere Reisemöglichkeiten um andere Gebiete zu erreichen.

1. Flug mit einem Überlichtantrieb
Die Reisezielauswahl erfolgt über die Sektorkarte. Diese kann von der Systemansicht über den die 2. Grafik von links oben erreicht werden.
Je besser der Überlichtantrieb ist, desto kürzer ist die Reisezeit und je weiter die Reichweite.
Um den Flug zu starten klickt man einfach ein Zielsystem an und klickt dann auf "Start". 

Es gibt auch eine Reisehilfe, welche die Navigation in weit entfernte Gebiete erleichtert.

2. Flug über das Hyperraumblasensystem

Den Wissenschaftlern ist ein großer Durchbruch bei den Hyperraumblasen gelungen.
Es wurde eine Hyperraumblase entdeckt, über die 2 Sonnensysteme direkt miteinander verbunden werden können. 
Um die Schnellreisemöglichkeit nutzen zu können, muss in beiden Sonnensystemen ein Hyperraumaufrissprojektor vorahnden sein und beide Sonnensysteme der eigenen Fraktion angehören.
Die Reisezeit beträgt insgesamt 2 Stunden, wobei pro Stufe der beteiligten Hyperraumaufrissprojektoren 1 Minute abgezogen wird.
Um diese neuartige Technologie nutzen zu können, muss sich an Bord des Schiffes ein Überlichtantrieb befinden, der den Tunnel stabilisiert.
Die Verwendung erfolgt wie bisher über die Sektorkarte und dort wird angezeigt wie lang eine Reise dauert, wobei automatisch die schnellste Verbindung verwendet wird.

3. Flug über die Omega-Brücke
Die Omega-Brücke ist ein altes Bauwerk der ERBAUER, das weit entfernte Systeme miteinander verbindet.
Die Omega-Brücke wird rechts oben in der Systemansicht mit einem Symbol angezeigt. 

Es gibt in den Randbereichen der Ablyon-Galaxie mehrere Stationen. 
Desweiteren verfügt jedes Heimatsystem der Fraktionen und das jeweilige Deepfraction-System über einen Omega-Brücken-Punkt, der jedoch nur von der eigenen Fraktion nutzbar ist.
Weitere bekannte Punkte sind Aganra, ein bei F666 gelegener Brücken-Punkt. 
