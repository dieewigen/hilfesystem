Hier gibt es eine kurze Übersicht über die Parteien, die bisher bekannt sind:

<b>Die ERBAUER</b>
Die ERBAUER sind eine uralte Rasse, deren Technologie so hochstehend ist, dass sie schon an Magie grenzt. Über viele Galaxien verstreut kann man noch auf ihre Hinterlassenschaften stoßen und wertvolle Artefakte finden. Was aus den ERBAUERN geworden ist und ob und wo diese leben ist unbekannt.

<b>DX61a23</b>
Die DX61a23 sind eine von den ERBAUERN erschaffene Rasse, die auf der Suche nach ihren Schöpfern sind. Dazu haben sie von verschiedenen Galaxien aus Portale in die Ablyon-Galaxie geöffnet und diese Portale auch anderen Rassen zur Verfügung gestellt. Was nach dem Durchgang durch die Portale mit ihnen passiert ist, ist unbekannt.

<b>Die Wächter der ERBAUER</b>
Die Wächter wurden vor Äonen von den ERBAUERN zum Schutz der Ablyon-Galaxie eingesetzt. Sie können dazu auf viele Seltar-Raumschiffe und ein umfangreiches Überwachungssystem zurückgreifen. 

<b>Das Überwachungssystem der ERBAUER</b>
Das Überwachungssystem der ERBAUER besteht aus einer Vielzahl von Sensorstationen und Computergroßklustern, die zusammen einen künstliche Intelligenz bilden.

<b>Das Volk der Bao-Nada</b>
Das Volk der Bao-Nada ist ein Dienervolk der Wächter der ERBAUER. Sie helfen bei Bedarf den einzelnen Fraktionen und sind auf den Handel spezialisiert.

<b>Fraktion 1 bis 6</b>
Die von den DX61a23 geschaffenen Portale wurden auch von anderen Rassen genutzt. Diese gruppieren sich in 6 unterschiedlichen Fraktionen, die sich nicht unbedingt freundlich gegenüberstehen.

<b>F666 - Der alte Feind</b>
Über diese Wesen ist wenig bekannt, auch wie ihr wirklicher Name lautet. Die Fraktionen nennen sie in ihrer Angst Fraktion 666, die Wächter der ERBAUER nennen sie nur den alten Feind. Über ihre Pläne und Ambitionen ist nur wenig bekannt. Bisher ist nur sicher, dass sie dafür sorgen, dass ein altes und unglaublich mächtiges Wesen im Zentrumsgebiet der Ablyon-Galaxie nicht wider erwacht. Denn dieses würde zu einer Gefahr, die weit über die Galaxie hinausgehen würde. Dazu sind ihnen alle Mittel recht, selbst die Zerstörung einer ganzen Galaxie.   

<b>Executor Karlath</b>
Er ist das aktuelle Sprachrohr des alten Feindes/F666. Es ist nicht bekannt welcher Rasse er angehört und ob es noch andere Executoren gibt. Eins ist aber gewiß, das Leben anderer Wesen ist ihm nichts wert und er opfert diese bedenkenlos für seine Pläne. 

<b>Das WESEN</b>
Laut dem alten Feind/F666 befindet sich im Zentrumsgebiet der Ablyon-Galaxie ein altes und unglaublich mächtiges Wesen. Dieses schläft zur Zeit und so sollte es auch besser bleiben. Es stand bereits einmal kurz vor dem Erwachen und die Auswirkungen bekamen alle Fraktionen zu spüren. 

<b>Die Piraten</b>
Die Piraten hatten sich in der Vergangenheit primär im Kernbereich der Ablyon-Galaxie aufgehalten. Durch die kriegerischen Aktivitäten der Fraktionen haben sie sich aber inzwischen auch in den weiter außen liegenden Bereichen breit gemacht und belagern dort die Sonnensysteme. 

<b>Der Reporter</b>
Der Reporter ist der Name eines großen Informationsnetzwerkes, welches alle Bürger immer auf dem aktuellen Stand halten möchten. Die einzelnen Reporter treten immer nur unter einem einzigen Namen auf.

