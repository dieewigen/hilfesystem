In einem Steinbruch können Sandsteine mit einem Steinmetzwerkzeug abgebaut werden.
Um einen Steinbruch der Stufe 1 anzulegen benötigt man folgende Rohstoffe:
8x Lindenholzstamm
3x 10 Nägel
4000 Aktionspunkte

Geländevoraussetzung: Wiese mit Gebirge-Nachbarfeld (N,O,S,W)

Pro weiterem Stufenausbau werden die gleichen Rohstoffe benötigt, wobei die benötigten Aktionspunkte sich nach folgender Formel Formel berechnen: Zielstufe * 1000
Das Gebäude kann pro Gebäudestufe eine Einheit Rohstoff zur Verfügung stellen, welcher dann abgebaut/geerntet werden kann.
Je höher die Gebäudestufe ist, desto schneller füllt sich der Rohstoffbestand im Gebäude.