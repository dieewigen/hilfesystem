In einer Erzmine kann Erz mit einer Bergmannpicke abgebaut werden.

Um eine Erzmine der Stufe 1 anzulegen benötigt man folgende Rohstoffe:
10x Lindenholzstamm
4x 10 Nägel
4000 Aktionspunkte

Geländevoraussetzung: Wiese mit Gebirge-Nachbarfeld (N,O,S,W)

Pro weiterem Stufenausbau werden die gleichen Rohstoffe benötigt, wobei die benötigten Aktionspunkte sich nach folgender Formel berechnen: Zielstufe * 1000

Das Gebäude kann pro Gebäudestufe eine Einheit Rohstoff zur Verfügung stellen, welcher dann abgebaut/geerntet werden kann.

Je höher die Gebäudestufe ist, desto schneller füllt sich der Rohstoffbestand im Gebäude.
