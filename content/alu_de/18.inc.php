Efta besteht im Grunde derzeit aus drei Teilen: 
- das Lösen der Temporärtransmitter, 
- das Kämpfen gegen Totenköpfe,
- das Ausbauen des Heldenturms und das Lösen der Quests
um den Heldenturm und die einmaligen Quests lösen zu können, benötigt man Rohstoffe, die in Gebäuden produziert werden.
folgende Gebäude gibt es derzeit in Efta: (in Klammern steht der Nutzen des Gebäudes)
1. Holzfällerhütte (nichts --> Lindenholzstamm) (man benötigt eine Holzfälleraxt)
2. Getreidefarm (nichts --> Getreide) (man benötigt eine Sense)
3. Steinbruch (nichts --> Sandstein) (man benötigt ein Steinmetzwerkzeug)
4. Erzmine (nichts --> Kupfererz) (man benötigt eine Bergmannspicke)
5. Erzschmelze (Kupfererz + Lindenholzstamm --> Kupferbarren) 
6. Windmühle (Getreide --> Mehl) 
7. Brunnen (leerer Wassereimer --> voller Wassereimer) 
8. Bäckerei (Mehl + voller Wassereimer + Lindenholzstamm --> Brot) (der Eimer ist danach kaputt)
9. Destille (2 Getreide + voller Wassereimer + Lindenholzstamm + leere Glasflasche --> Oller Fusel) 
Hinweis: jede Nutzung der Gebäude benötigt 250 Aktionspunkte (kurz: AP), und das Gebäude muss noch Kapazitäten übrig haben.
Um ein Gebäude zu bauen, sucht man sich eine freie Stelle auf der Karte aus und klickt dort auf "Baumenü". Der Steinbruch, sowie die Erzmine benötigen entsprechende Vorkommen, daher kann man diese nur direkt neben einem Berg bauen. Die Holzfällerhütte kann nur im Wald gebaut werden. Der Rest kann auf jeder freien Wiese gebaut werden. 
Welche Taktik man bei der Standortwahl wählt, bleibt eine individuelle Frage. 
Jedes Gebäude benötigt 4000 AP, sowie Ressourcen. Wie viele Ressourcen das sind, steht im Baumenü. 
Dieses Gebäude wird damit auf Stufe 1 gebaut. Jede weitere Stufe kostet erneut die Bauressourcen. 
Der Bau jeder Stufe (einschließlich der ersten) wird mit 50 Erfahrungspunkte belohnt.
