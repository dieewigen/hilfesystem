Die Abenteuer sind Deine wichtigste Quelle für den Erfahrungsgewinn Deines Charakters.

Jeden Tag stehen Dir 2 Stunden Spielzeit zur Verfügung, die Du mit der Erfüllung von Aufgaben verbringen kannst.

Jeweils um 0 Uhr erhält man wieder 2 Stunden Spielzeit.

Die Aufgaben können unterschiedlich lange dauern und mehr oder weniger Gold und Erfahrung geben. Man sollte also genau überprüfen, welche Aufgabe für einen am besten geeignet ist.

