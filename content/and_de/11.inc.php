1. Akt

Du befindest Dich auf dem Planeten Andalur, der sich in den Tiefen des magischen Universums befindet.

Die Welt besteht aus vielen Kontinenten und unzähligen Inseln, auf denen die verschiedensten Lebensformen existieren.

Wie auf jeder Welt, tobt auch dort der Kampf zwischen Chaos und Ordnung.

Der dunkle Nekromant Anlamar griff bereits vor Jahrhunderten nach der Macht über Andalur und seinen Horden von Untoten überzogen die Welt mit Leid und Tod.

Nur Dank einer Gruppe von unerschrockenen Kämpfern konnte das Böse besiegt und der Nekromant Anlamar vernichtet werden.

Doch was Niemand wußte war, dass Anlamar vor seinem Tode seine Essenz magisch gebunden und mit einem schwarzmagisches Ritual für die Ewigkeit konserviert hatte.

Und nun, nach Jahrhunderten des Wachsens, droht die Rückkehr des Nekromanten und der Schatten von Anlamar fällt über die Welt.  