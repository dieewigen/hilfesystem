Wenn Du keine Lust hast, lange Anleitungen zu lesen, dann bist Du hier genau richtig. Mit kurzen Stichworten wird Dir hier das Spiel erklärt.

Charakter:
Eine Übersicht über deinen Charakter. Über das Plus-Symbol bei den Werten können diese gegen Gold gesteigert werden. Die Felder Rechts oben stellen den Rucksack dar.

Nachrichten:
Hier erhältst Du Nachrichten von anderen Spielern und vom Herold. Du kannst hier andere Spieler anschreiben.

Abenteuer:
Hier kannst Du zwischen 3 Aufgaben wählen die Dir Gold, Erfahrung und einen Credit einbringen können.

Feldzug:
Jeder Feldzug dauert 1 Stunde, wobei diese Zeit nicht von der Abenteuerzeit abgezogen wird.
Die Gegner sind stärker als normal, geben auch auch mehr Gold und Erfahrung.
Chance von 10% auf Creditgewinn.

Bergwerk:
Hier kannst du bis zu 12 Stunden für Gold arbeiten. Die Arbeit kann abgebrochen werden, Du erhältst dann aber kein Gold.

Marktplatz:
Hier kannst du Waren gegen Gold kaufen. Jeden Tag um 0 Uhr, wenn Du einen Gilden-/Feldzug gewonnen oder wenn Du eine Stufe aufgestiegen bist, bekommt der Händler neue Ware, die Deinem aktuellem Level angepasst ist. Für 10 Credits wird Dir direkt neue Ware angeboten.

Gilde:
Gilde gründen, oder einer Gilde beitreten.
Unter Boni können die Gildenmitglieder gemeinsam die Gilde ausbauen.

Gildenfeldzug:
Ist wie der "normale" Feldzug, jedoch sind die Gegner viel stärker, dafür erhält man hier die Hilfe von Gildenmitgliedern.

Ehrenplatz:
Hier ist die Rangliste einsehbar und es können andere Spieler/Gilden zum Duell herausgefordert werden. Spielerduelle gehen alle 30 Minuten und Gildenduelle alle 11 Stunden. Bei den Gildenduellen nehmen alle Spieler teil, die in den letzten 24 Stunden online waren. War niemand online, so kämpft der Gildenmeister allein.

Optionen:
Charakterbild wählen, Sound aktivieren/deaktivieren, Account löschen
