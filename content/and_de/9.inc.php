Auf dem Ehrenplatz kann man einsehen wie gut die Spieler, bzw. Gilden sind.

Es gibt es unterschiedliche Ranglisten:
- Spieler sortiert nach Stufe/Erfahrung
- Spieler sortiert nach Ruhm
- Gilden sortiert nach Ruhm

Erfahrung können Spieler durch Aufgaben, Feldzüge und Gildenfeldzüge erlangen.
Ruhm können Spieler/Gilden durch Duelle erlangen.

Dazu klickt man einfach das gewünscht Ziel an und klickt dann auf Duell.

Je höher das Ziel in der Rangliste über einen steht, desto mehr Ruhmespunkte erhält man und das Ziel verliert Ruhmespunkte.
Sollte man jedoch verlieren, so ist es anders herum, man verliert Ruhmespunkte und das Ziel gewinnt welche dazu.

Sollte bei einem Spielerduell das Ziel weniger Ruhmespunkte als der Angreifer haben, so verliert das Ziel keine Ruhmespunkte.

Spieler können alle 30 Minuten ein Duell starten und Gilden alle 11 Stunden.

Es ist natürlich auch möglich sich die Spieler anzusehen, bzw. ihnen auch eine Nachricht zu schreiben.



 