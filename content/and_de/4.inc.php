Unter Feldzug sieht man eine Karte mit mehreren Markierungen.

Die Markierungen sind farbige Kreise, welche für das Element des Feldzuges stehen:
-Rot: Feuer
-Blau: Wasser
-Braun: Erde
-Grau: Luft

Im Kreis steht die Stufe des Gegners, wobei im Tooltip noch genauer angezeigt wird, wie stark der Gegner ist, denn es sind keine normalen Gegner der angegebenen Stufe. Folgende Stufen gibt es aktuell:
-Veteran: Ein erfahrener und starker Gegner.
-Champion: Ein besonderer Kämpfer, der schwer zu bezwingen ist.
-Elite: Einer der Besten der Besten.
-Boss: Ein wirklich harter Brocken, der schon so manchem Abenteurer den Allerwertesten vermöbelt hat.

Im Tooltip sieht man wieviele Ebenen man des Feldzuges bereits erledigt hat. Sobald man alle Ebenen meistern konnte, verschwindet der Feldzug von der Karte.

Die Dauer für einen Feldzug beträgt 1 Stunde und kann für 10 Credits sofort abgeschlossen werden.

Die Feldzugzeit wird nicht von der Abenteuerzeit abgezogen.

Bei jedem gewonnen Feldzug erhält man 5 Credits.

Die Feldzuggegner geben pro Stufe 100 Gold und 500 Erfahrung.