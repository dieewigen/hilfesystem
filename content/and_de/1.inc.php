Hier findest Du alle wichtigen Informationen über Deinen Charakter und seine Ausrüstung.

Im oberen linken Bereich sind die Gegenstandsfelder um das Charakterbild gruppiert.

Das Charakterbild kannst Du in den Optionen ändern. 

Im oberen rechten Bereich befindet sich Dein Rucksack.

Gegenstände kannst du an-/ablegen indem Du sie anklickst und im erscheinendem Menü die entsprechende Auswahl triffst.

Links unten steht Dein Name und Deine Stufe.

Neben der Stufe findest Du Deine aktuellen Erfahrungspunkte und den Wert den Du erreichen mußt, bevor Du eine weitere Stufe aufsteigen kannst.

