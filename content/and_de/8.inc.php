Im Gegensatz zu den "normalen" Feldzügen tritt man hier zusammen mit seiner Gilde gegen die Gegner an. Die Gegner haben dementsprechend eine viel höhere Stufe und allein kann man nicht viel erreichen.

Wie bei den Duellen nehmen nur die Gildenmitglieder an den Kämpfen teil, die innerhalb der letzten 24 Stunden im Spiel online waren.

Die Belohnung entspricht denen der "normalen" Feldzüge und welche Stufe dort das jeweilige Pendant hat.

Jedes Gildenmitglied hat seinen eigenen Gildenfeldzugbereich, der unabhänig von den Fortschritten der anderen Gildenmitglieder ist. 