Auf dem Marktplatz kannst du Waren gegen Gold kaufen. 

Jeden Tag um 0 Uhr, , wenn Du einen Gilden-/Feldzug gewonnen oder wenn Du eine Stufe aufgestiegen bist, bekommt der Händler neue Ware, die Deinem aktuellem Level angepasst ist. 

Für 10 Credits wird Dir direkt neue Ware angeboten, wobei dort die Chance auf bessere Gegenstände höher ist.

Die Gegenstände haben eine sehr unterschiedliche Qualität, die durch Hintergrundfarben dargestellt werden:
- braun: 1 Grundwertbonus und 1 Elementarbonus 
- grün: 2 Grundwertboni und 2 Elementarboni
- lila: 3 Grundwertboni und 3 Elementarboni
- blau: 4 Grundwertboni und 4 Elementarboni

Je besser die Qualität ist, desto teurer sind die Gegenstände natürlich.

Aber Vorsicht beim Kauf von Waren, Orlando ist ein wahres Schlitzohr und nimmt "gebrauchte" Ware nur noch zu einem viel geringerem Preis zurück.