Gilden dienen dazu, dass Spieler sich zusammenschließen können um gemeinsam die Gefahren der Welt zu bestehen.

Zur leichteren Kommunikation steht der Gilde ein Chat zur Verfügung bei dem die Beiträge für 48 Stunden gespeichert werden.

Für jeden Spieler wird der letzte Zeitpunkt, wann er im Spiel aktiv war, entweder in rot oder grün angegeben.
Die Farbgebung bedeutet, dass der Spieler innerhalb der letzten 24 Stunden aktiv/inaktiv war und dementsprechend an Gildenduellen/Gildenfeldzügen teilnimmt. 

Es gibt verschiedene Ränge in Gilden:

- Gildenmeister: Er ist das weise Oberhaupt der Gilde und kann Gildenduelle starten, seine Stellvertreter bestimmen, über Bewerber entscheiden und Gildenmitglieder entlassen.
- Stellvertreter: Sie bilden die rechte Hand des Gildenmeisters und können Gildenduelle starten und über Bewerber entscheiden.
- Mitglieder: Sie haben weniger politische Rechte, aber dafür ist ihre Kampfkraft sehr wichtig.
- Bewerber: Spieler, die sich bei der Gilde beworben haben und darauf hoffen, dass sie aufgenommen werden. Bewerber können nicht von den Gildenboni profitieren.

Der Gildenmeister kann bei Bedarf auch sein Amt an eine anderes Gildenmitglied übergeben, bzw. auch die Gilde auflösen.

Unter dem Punkt Boni können die Gildenmitglieder gemeinsam die Gilde ausbauen.

Die Boni werden dann ggf. bei den Kämpfen dazugerechnet. Die Boni gelten für Kämpfe aller Art. Auf der Charakterseite werden diese aber nicht mit einberechnet angezeigt, dort stehen die Grundwerte.

Besonders ist der Bereich auch für die Aufnahme von neuen Gildenmitgliedern, denn nach der Gründung können maximal 5 Mitglieder vorhanden sein.
Dieses Limit kann unter Boni bis auf 30 Mitgliedern erhöht werden, was jedoch sehr teuer ist und auf möglichst viele Mitglieder verteilt werden sollte. 

